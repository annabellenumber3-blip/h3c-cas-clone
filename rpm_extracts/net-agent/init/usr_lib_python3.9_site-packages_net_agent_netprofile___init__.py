import xml.etree.ElementTree as ET
from . netprofile_qos import NetProfileQos
from . netprofile_acl import NetProfileAcl
from . netprofile_drain import NetProfileDrain
from . netprofile_trunkport import NetProfileTrunkPort
from . netprofile_pvlan import NetProfilePvlan
from . netprofile_vspvlan import NetProfileVsPvlan
from . portprofile import PortProfile

__all__ = ['PortProfile', 'parse_netprofile_element', 'create_netprofile']

__netprofiles = {"qos": NetProfileQos, "acl": NetProfileAcl, "drain": NetProfileDrain, "trunkport": NetProfileTrunkPort, "pvlan": NetProfilePvlan, "pvlan_all": NetProfileVsPvlan}


def parse_netprofile_element(xml):
    type_, name_, uuid, root = "", "", "", None
    try:
        root = ET.fromstring(xml)
        type_ = root.attrib.get("type", "")
        name_ = root.attrib.get("name", "")
        uuid = root.attrib.get("uuid", "")
    except:
        pass
    return type_, name_, uuid, root


def create_netprofile(type_, md5, element):
    _net_profile_class = __netprofiles.get(type_, None)
    if _net_profile_class is None:
        return None

    netprofile = _net_profile_class(md5)
    return netprofile if netprofile.parse(element) else None

