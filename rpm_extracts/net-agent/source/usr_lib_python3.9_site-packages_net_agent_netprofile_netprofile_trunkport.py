#!/usr/bin/python3
import logging
import xml.etree.ElementTree as ET
import uuid
from . netprofile import NetProfile
from . netprofile import Rule
from .. base import net_lib

class TrunkPortRule(Rule):
    """ trunkport netprofile rule """
    def __init__(self):
        super(TrunkPortRule, self).__init__()
        self.in_vlan = 0

    def __str__(self):
        return "in_vlan=%s, action:%s" % (self.in_vlan, self.action)

class TrunkPortAction(object):
    """ define TrunkPortAction used as action of flow which describe trunkport parameters """
    def __init__(self):
        super(TrunkPortAction, self).__init__()
        self.out_vlan = 0

    def __str__(self):
        return "out_vlan=%d" % self.out_vlan

class NetProfileTrunkPort(NetProfile):
    def __init__(self, md5=""):
        super(NetProfileTrunkPort, self).__init__(md5)

    def parse(self, root):
        if root is None:
            logging.error("root is None")
            return False

        self.type = root.attrib.get("type", "")
        self.name = root.attrib.get("name", "")
        self.uuid = root.attrib.get("uuid", str(uuid.uuid3(uuid.NAMESPACE_DNS, self.name)))
        self.version = root.attrib.get("version", "")

        if self.name == "":
            net_lib.Utility.set_log_lasterr("name is None")
            return False

        for child in root:
            if child.tag != "flow":
                continue

            if 'vlan' not in child.attrib.keys():
                net_lib.Utility.set_log_lasterr("incomplete flow for trunkport: %s" % child.attrib)
                return False

            rule = TrunkPortRule()
            try:
                rule.in_vlan = int(child.attrib.get("vlan", ""))
                if rule.in_vlan >= 4096 or rule.in_vlan < 0:
                     raise ValueError("trunkport in_vlan is not in [0,4096)")
            except Exception as e:
                 net_lib.Utility.set_log_lasterr("invalid trunkport in_vlan, check your xml(%s)" % str(e))
                 return False

            for item in child:
                if item.tag != "action":
                    continue
                rule.action = self.parse_extra_action(item)
                if rule.action is None:
                        net_lib.Utility.set_log_lasterr("failed to parse trunkport action: %s" % item)
                        return False
                self.rules.append(rule)

        return True

    def format_xml(self):
        xml = "\n"
        try:
            root = ET.Element("netprofile")
            root.attrib["type"] = self.type
            root.attrib["name"] = self.name
            root.attrib["uuid"] = self.uuid
            root.attrib["version"] = self.version
            for rule in self.rules:
                flow = ET.SubElement(root, "flow")
                flow.attrib["vlan"] = str(rule.in_vlan)
                action = ET.SubElement(flow, "action")
                self.format_extra_action(rule.action, action)
            xml = ET.tostring(root).decode().replace("><", ">\n<")
        except:
            logging.exception("catch exception")

        return xml

    def parse_extra_action(self, item):
        if 'vlan' not in item.attrib.keys():
            net_lib.Utility.set_log_lasterr("invalid action for trunkport: %s" % item.attrib)
            return None

        act = TrunkPortAction()
        try:
            act.out_vlan = int(item.attrib.get("vlan", ""))
            if act.out_vlan >= 4096 or act.out_vlan < 0:
                     raise ValueError("trunkport out_vlan is not in [0,4096)")
        except Exception as e:
            net_lib.Utility.set_log_lasterr("invalid action vlan for trunkport, check your xml(%s)" % str(e))
            return None

        return act


    def format_extra_action(self, action, element):
        element.attrib["vlan"] = str(action.out_vlan)
