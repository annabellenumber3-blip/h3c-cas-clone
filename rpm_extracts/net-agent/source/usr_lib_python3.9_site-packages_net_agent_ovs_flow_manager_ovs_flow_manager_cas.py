    def _check_drain_pair_used(self, segbr, busibr, dm):
        all_connetced = dm.get_drain_bypass_all_connected()
        for drainbr, connetced_dict in all_connetced.items():
            for tmp_pair in connetced_dict.keys():
                if (segbr in tmp_pair or busibr in tmp_pair) and tmp_pair != tuple([segbr, busibr]):
                    return True

        return False

    def _is_bridge_connected(self, br0, br1, dm):
        connected = False
        if br0 in dm.get_drain_bypass_all_connected_keys():
            pair_dict = dm.get_drain_bypass_connected(br0)
            for tmp_pair, tmp_connected in pair_dict.items():
                if br1 in tmp_pair:
                    connected = tmp_connected
        else:
            pair = tuple([br0, br1])
            all_connetced = dm.get_drain_bypass_all_connected()
            for drainbr, connetced_dict in all_connetced.items():
                for tmp_pair, tmp_connected in connetced_dict.items():
                    if pair == tmp_pair:
                        connected = not tmp_connected

        return connected

    def _set_drain_bypass(self, drainbr, segbr, busibr, connected, dm):
        seg_busi_pair = tuple([segbr, busibr])
        pair_dict = dm.get_drain_bypass_connected(drainbr)
        if connected is not None:
            if seg_busi_pair not in pair_dict.keys():
                pair_dict[seg_busi_pair] = connected
                logging.info("update state for %s in %s, connected: %d." % (seg_busi_pair, drainbr, connected))
        else:
            if seg_busi_pair in pair_dict:
                logging.info("remove state for %s in %s." % (seg_busi_pair, drainbr))
                pair_dict.pop(seg_busi_pair, None)

        dm.update_drain_bypass_connected(drainbr, pair_dict)
        return

    def _start_drain_bypass(self, escape, dm):
        def set_drain_bypass_in_ovsdb(escape):
            escape_str = "True" if escape else "False"
            cmd = "ovs-vsctl set open_vswitch . other_config:drain_bypass=%s" % escape_str
            net_lib.Utility.cmd_execute(cmd)

        def get_drain_vlan_infos(escape):
            # return origin_vlan when enable bypass(escape).
            # return drain_vlan when disbale bypass(not escape).
            vlan_list = []
            for iface_, netprofile in dm.get_all_iface_drain_netprofiles().items():
                if netprofile.type == 'drain' and iface_.drain_vlan:
                    if escape:
                        vlan_list.append([iface_.name, iface_.origin_vlan])
                    else:
                        vlan_list.append([iface_.name, iface_.drain_vlan])

            return vlan_list

        def set_drop_flows_for_drain(escape):
            relpr = 10
            flows = []
            if escape:
                for br in dm.get_drain_bypass_all_connected_keys():
                    flows.append(Flow(bridge=br, cookie=COOKIE_DRAIN_DROP, table=0, priority=PRIORITY_OFFSET_DRAIN+relpr, actions='drop'))
                if flows:
                    self._txn.add_flows(flows)
            else:
                for br in dm.get_all_bridges():
                    flows.append(Flow(bridge=br.name, cookie="%d/-1" % COOKIE_DRAIN_DROP))
                if flows:
                    self._txn.del_flows(flows)

            return

        def connect_segbr_busibr(escape):
            drain_type = "vm_port"
            all_connetced = dm.get_drain_bypass_all_connected()
            for drainbr, connetced_dict in all_connetced.items():
                for tmp_pair, connected in connetced_dict.items():
                    segbr, busibr = tmp_pair
                    if escape and connected:
                        # escape means enable bypass.
                        # connected is True means drainbr is connected with segbr and busibr.
                        # need to disconnect drainbr with segbr and busibr, and connect segbr and busibr.
                        disconnect_bridge(drainbr, segbr)
                        disconnect_bridge(drainbr, busibr)
                        ensure_connect(segbr, busibr, drain_type)
                        connected = not connected
                    elif not escape and not connected:
                        # not escape means disable bypass
                        # connected is False means drainbr is not connected with segbr and busibr.
                        # need to disconnect segbr and busibr, and connect drainbr with segbr and busibr.
                        disconnect_bridge(segbr, busibr)
                        ensure_connect(drainbr, segbr, drain_type)
                        ensure_connect(drainbr, busibr, drain_type)
                        connected = not connected
                    connetced_dict[tmp_pair] = connected
                dm.update_drain_bypass_connected(drainbr, connetced_dict)

            return

        def set_vm_port_vlan(vlan_list):
            cmd = ""
            for iface_, vlan in vlan_list:
                vlan_str = "%s" % vlan if int(vlan) else []
                cmd += " -- set port %s tag=%s" %(iface_, vlan_str)
                iface = dm.get_interface_by_name(iface_)
                if iface:
                    bypass_revalidate_flow = True if str(iface.bypass_revalidate_flow) == "False" else False
                    cmd  += " -- set Interface %s other_config:bypass_revalidate_flow=%s" % (iface_, bypass_revalidate_flow)
            if cmd:
                cmd = "ovs-vsctl" + cmd
                net_lib.Utility.cmd_execute(cmd)

        if escape ==  dm.get_drain_bypass_state():
            logging.info("bypss_state remain same with event, no need to update bypass." )
            return 0

        #1. update state of drain bypass.
        set_drain_bypass_in_ovsdb(escape)
        dm.update_drain_bypass_state(escape)

        #2. add/del drop flow for drainbr.
        set_drop_flows_for_drain(escape)

        #3. connect/disconnect segbr and busibr
        connect_segbr_busibr(escape)

        #4. set vlan for vm ports.
        vlan_list = get_drain_vlan_infos(escape)
        set_vm_port_vlan(vlan_list)

        return 0

    def _apply_qos(self, bridge, iface, qos_netprofile):
        if qos_netprofile is None or not net_lib.GlobalConfig.enable_meter:
            logging.info("qos_netprofile is None or not enable meter")
            return

        all_flows = []
        for rule in qos_netprofile.rules:
            if rule.direction == DIRECTION_IN:
                table = TABLE_ID_IN_QOS
                in_port, out_port = iface.ofport, None
            else:
                table = TABLE_ID_OUT_QOS
                in_port, out_port = None, iface.ofport

            meter_id = self._ovsmeter.get_meter(bridge, iface, rule.action, qos_netprofile.name)
            if meter_id is None:
                logging.warn("failed to add qos flow for %s as get meter_id failed.", iface.name)
                continue

            action = OVSAction()
            action.add_set_meter(int(meter_id))
            action.add_actions(self.__get_default_action(table))
            mf = rule.matchfield.clone()
            pr = PRIORITY_OFFSET_QOS + rule.priority
            f = Flow(bridge=bridge.name, cookie=COOKIE_QOS, table=table, priority=pr, matchfield=mf, actions=action, in_port=in_port, out_port=out_port)
            all_flows += [f]

        self._txn.add_flows(all_flows)

    def _unapply_qos(self, bridge, iface, qos_netprofile):
        flow_in = Flow(bridge=bridge.name, cookie="%d/-1" % COOKIE_QOS, in_port=iface.ofport)
        flow_out = Flow(bridge=bridge.name, cookie="%d/-1" % COOKIE_QOS, out_port=iface.ofport)
        self._txn.del_flows([flow_in, flow_out])
        if qos_netprofile and net_lib.GlobalConfig.enable_meter:
            for rule in qos_netprofile.rules:
                self._ovsmeter.clean_meter(bridge, iface, rule.action, qos_netprofile.name)

