    def _unapply_pvlan(self, bridge, iface, netprofile, dm):
        logging.info("unapplying pvlan to iface: [%s] for bridge: [%s] with netprofle: [%s]", iface, bridge.name, netprofile)
        br = bridge.name
        origin_br = iface.origin_br
        primary_vlan = 0
        pvlan_type = ""
        second_vlan = 0
        flow_del = []
        is_last_vnet = True
        
        if not origin_br:
            logging.error("unapply_pvlan need to apply seg bridge, not origin bridge %s" % br)
            return

        for rule in netprofile.rules:
            primary_vlan = rule.primary_vlan
            pvlan_type = rule.action.pvlan_type
            second_vlan = rule.action.pvlan_vlan

        ifaces = net_lib.Utility.cmd_execute("ovs-vsctl -t 5 list-ifaces %s" % br, True)[1].split()
        if len(ifaces) > 2:
            is_last_vnet = False

        pe_isolated_br = "pvS" + str(second_vlan) + "_" + origin_br
        pe_isolated_br_ofport = OVSDb.get_ofport_by_iface(pe_isolated_br)
 
        if not vnet_join_new_br(iface, br, origin_br, True):
            logging.error("unapply_pvlan--->iface %s from second br %s join business br %s failed" % (iface.name, br, origin_br))
            return

        if pvlan_type == "primary_vlan":
            if is_last_vnet:
                pe_br_primary = "pv" + origin_br + "_P" + str(primary_vlan)
                net_lib.Utility.cmd_execute("ovs-vsctl -t 8 -- --if-exists del-br %s -- --if-exists del-port %s %s" % (br, origin_br, pe_br_primary))
        elif pvlan_type == "isolated_vlan":
            if is_last_vnet:
                pe_br_isolated = "pv" + origin_br + "_S" + str(second_vlan)
                net_lib.Utility.cmd_execute("ovs-vsctl -t 8 -- --if-exists del-br %s -- --if-exists del-port %s %s" % (br, origin_br, pe_br_isolated))
            else:
                net_lib.Utility.cmd_execute("ovs-appctl fdb/del %s %d %s" % (br, second_vlan, iface.mac))
                flow_del.append(Flow(bridge=br, cookie="%d/-1" % COOKIE_PVLAN, in_port=iface.ofport))
                flow_del.append(Flow(bridge=br, cookie="%d/-1" % COOKIE_PVLAN_LEARN, in_port=pe_isolated_br_ofport, dl_dst=iface.mac))
                flow_del.append(Flow(bridge=br, cookie="%d/-1" % COOKIE_PVLAN, in_port=pe_isolated_br_ofport, dl_dst=iface.mac))
        elif pvlan_type == "group_vlan":
            if is_last_vnet:
                pe_br_group = "pv" + origin_br + "_G" + str(second_vlan)
                net_lib.Utility.cmd_execute("ovs-vsctl -t 8 -- --if-exists del-br %s -- --if-exists del-port %s %s" % (br, origin_br, pe_br_group))
        self._txn.del_flows(flow_del)
        dm.delete_iface_pvlan_netprofile(iface)
    
    def clear_iface_pvlan(self, iface, dm):
        iface_pvlan = dm.get_pvlan_iface_netprofile(iface)
        if not iface_pvlan:
            logging.warn("pvlan iface %s info is none when clear pvlan!" % iface.name)
            return

        if os.path.exists("/sys/class/net/%s" % iface.name):
            logging.warn('iface %s still exist, no need clear pvlan here.' % iface.name)
            return

        br  = iface.br_name
        origin_br = iface.origin_br
        is_last_vnet = True
        flow_del = []
        pe_br=""

        if not origin_br:
            logging.error("clear_iface_pvlan need to apply seg bridge, not origin bridge %s" % br)
            return

        for elem in dm.get_all_interfaces():
            if elem.primary_vlan and elem.name != iface.name:
                if elem.origin_br == origin_br and iface.primary_vlan == elem.primary_vlan and iface.isolated_vlan == elem.isolated_vlan and iface.group_vlan == elem.group_vlan:
                    is_last_vnet = False
                    break

        if is_last_vnet:
            if iface.primary_vlan and iface.isolated_vlan:
                pe_br = "pv" + origin_br + "_S" + iface.isolated_vlan
            elif iface.primary_vlan and iface.group_vlan:
                pe_br = "pv" + origin_br + "_G" + iface.group_vlan
            elif iface.primary_vlan:
                pe_br = "pv" + origin_br + "_P" + iface.primary_vlan
            net_lib.Utility.cmd_execute("ovs-vsctl -t 8 -- --if-exists del-br %s -- --if-exists del-port %s %s" % (br, origin_br, pe_br))
        else:
            if iface.primary_vlan and iface.isolated_vlan:
                pe_isolated_br = "pvS" + iface.isolated_vlan + "_" + origin_br
                pe_isolated_br_ofport = OVSDb.get_ofport_by_iface(pe_isolated_br)
                flow_del.append(Flow(bridge=br, cookie="%d/-1" % COOKIE_PVLAN, in_port=pe_isolated_br_ofport, dl_dst=iface.mac))
                self._txn.del_flows(flow_del)
        dm.delete_iface_pvlan_netprofile(iface)
