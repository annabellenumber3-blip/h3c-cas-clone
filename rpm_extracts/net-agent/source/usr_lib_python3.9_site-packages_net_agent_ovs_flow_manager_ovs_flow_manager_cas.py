# a patch port of br1 to br0
def patch_port(br0, br1):
    if br0.startswith("="):
        br0 = br0.replace("=", "seg")
    if br1.startswith("="):
        br1 = br1.replace("=", "seg")

    port = "%s_%s" % (br0, br1)
    return port


def disconnect_bridge(br0, br1):
    to_port = patch_port(br1, br0)
    in_port = patch_port(br0, br1)

    net_lib.Utility.cmd_execute('''
        ovs-vsctl --if-exists del-port {br0} {to_port} -- --if-exists del-port {br1} {in_port}
    '''.format(**{'br0': br0, 'to_port': to_port, 'br1': br1, 'in_port': in_port}), True)
    net_lib.Utility.cmd_execute('''
        ovs-vsctl --if-exists remove Bridge {br1} other_config used_by_drain
        '''.format(**{'br1': br1}), True)


def ensure_connect(br0, br1, type):
    to_port = patch_port(br1, br0)
    in_port = patch_port(br0, br1)

    cmd = "ovs-vsctl --may-exist add-port %s %s -- set interface %s type=patch options:peer=%s -- set interface %s other_config:used_by_drain=true" %(br0, to_port, to_port, in_port, to_port)
    net_lib.Utility.cmd_execute(cmd)

    cmd = "ovs-vsctl --may-exist add-port %s %s -- set interface %s type=patch options:peer=%s -- set interface %s other_config:used_by_drain=true" %(br1, in_port, in_port, to_port, in_port)
    net_lib.Utility.cmd_execute(cmd)

    net_lib.Utility.cmd_execute('''
        ovs-vsctl set bridge {br1} other_config:used_by_drain=true
        '''.format(**{'br1': br1}), True)

    if type == "uplink":
        net_lib.Utility.cmd_execute('''
            ovs-vsctl set Interface {to_port} other_config:learn-forbid=true
            '''.format(**{'to_port': to_port}), True)

def recover_drian_vlan(iface, origin_vlan):
    vlan_str = "%s" % origin_vlan if int(origin_vlan) else []
    cmd = "ovs-vsctl -- --if-exists remove interface %s other_config drain_vlan -- set port %s tag=%s" %(iface, iface, vlan_str)
    net_lib.Utility.cmd_execute(cmd)

def ensure_drian_vlan(iface, vlan, drain_vlan):
    net_lib.Utility.cmd_execute('''
        ovs-vsctl -- set port {iface} tag={vlan}
        '''.format(**{'iface': iface, 'vlan': vlan}), True)

    net_lib.Utility.cmd_execute('''
        ovs-vsctl -- set interface {iface} other_config:drain_vlan={drain_vlan}
        '''.format(**{'iface': iface, 'drain_vlan': drain_vlan}), True)

def get_iface_cur_tag(iface):
    cmdret = net_lib.Utility.cmd_execute("ovs-vsctl -t 5 --verbose=db_ctl_base:syslog:off get Open_vSwitch . other_config:drain_bypass", log_err=False)[1].split()
    global_bypass = cmdret[0].strip('\'"') == "True" if len(cmdret) else False
    tag = 0
    if iface.br_name == "tbr-%s" % iface.name:
        tag = 0
    elif iface.primary_vlan:
        tag = 0
    elif iface.origin_br and not global_bypass:
        tag = 0 if iface.drain_vlan == "" else int(iface.drain_vlan)
    else:
        tag = 0 if iface.origin_vlan == "" else int(iface.origin_vlan)
    return tag

def get_veth_by_iface(iface):
    veth_name = ""
    if iface.br_name:
        cmd = "ovs-vsctl -t 5 list-ifaces %s | grep -E veth.*ovs" % iface.br_name
        veth_name = net_lib.Utility.cmd_execute(cmd, True)[1].split()
    return veth_name

def vnet_join_new_br(iface, src_br, dst_br, is_join_business_br=False, iface_config_extra_set=""):
    vnet_info = net_lib.Utility.get_vnet_info_with_try(iface)
    if not vnet_info:
        logging.error("get vnet info failed!")
        return False

    ext_ids = iface.external_ids
    other_cfg = iface.other_config
    tag = vnet_info['tag'] if vnet_info['tag'] != '1' else '[]'

    cmd = "ovs-vsctl --timeout=5 -- --if-exists del-port %s %s" % (src_br, iface.name)
    if net_lib.Utility.cmd_execute(cmd)[0] != 0:
        return False

    cmd = "ovs-vsctl --timeout=5 -- add-port %s %s" % (dst_br, iface.name)
    cmd += ' -- set Interface %s external-ids:attached-mac="%s"' % (iface.name, ext_ids['attached-mac'])
    cmd += ' -- set Interface %s external-ids:iface-id="%s"' % (iface.name, ext_ids['iface-id'])
    cmd += ' -- set Interface %s external-ids:vm-id="%s"' % (iface.name, ext_ids['vm-id'])
    cmd += ' -- set Interface %s external-ids:port-profile="%s"' % (iface.name, ext_ids['port-profile'])
    cmd += ' -- set Interface %s external-ids:iface-status=%s' % (iface.name, ext_ids['iface-status'])
    cmd += ' -- set Interface %s mtu_request=%s' % (iface.name, vnet_info['mtu'])
    if is_join_business_br:
        cmd += ' -- set Interface %s other_config:origin_vlan=%s' % (iface.name, tag if tag != "[]" else "0")
        cmd += ' -- set Port %s tag=%s' % (iface.name, tag)
    if iface_config_extra_set:
        cmd += iface_config_extra_set

    if 'type' in vnet_info and vnet_info['type'] == 'dpdkvhostuserclient':
        cmd += ' -- set Interface %s type=%s' % (iface.name, vnet_info['type'])
        cmd += ' -- set Interface %s options:vhost-server-path=%s' % (iface.name, vnet_info['path'])

    if 'bandwidth' in vnet_info:
        outbound = vnet_info['bandwidth'].get('outbound', None)
        rate = outbound.get('average', '0') if outbound else '0'
        burst = outbound.get('burst', '0') if outbound else '0'
        cmd += ' -- set Interface %s ingress_policing_rate=%s' % (iface.name, rate)
        cmd += ' -- set Interface %s ingress_policing_burst=%s' % (iface.name, burst)

    if iface.bind_ip:
        cmd += ' -- set Interface %s other_config:bind-ip="%s"' % (iface.name, iface.bind_ip)
    if iface.bind_ipv6:
        cmd += ' -- set Interface %s other_config:bind-ipv6="%s"' % (iface.name, iface.bind_ipv6)
    if iface.tos:
        cmd += ' -- set Interface %s other_config:tos="%s"' % (iface.name, iface.tos)

    if net_lib.Utility.cmd_execute(cmd)[0] != 0:
        return False

    # tc config will disppear after port deleted from bridge, need apply again
    if 'bandwidth' in vnet_info:
        inbound = vnet_info['bandwidth'].get('inbound', None)
        if inbound is not None:
            average = inbound.get('average', '0')
            rate = inbound.get('peak', '0')
            burst = inbound.get('burst', '0')
            cmd = "virsh domiftune %s %s --live --inbound %s,%s,%s" % (ext_ids['vm-id'],ext_ids['attached-mac'], average, rate, burst)
            if net_lib.Utility.cmd_execute(cmd)[0] != 0:
                return False

    logging.info("iface %s from br %s join br %s done." % (iface.name, src_br, dst_br))
    return True

class OVSFlowManagerCAS(OVSFlowBaseManager):

    def __init__(self):
        super(OVSFlowManagerCAS, self).__init__()
        self._defalut_actions = {
            TABLE_ID_BIND_IP: OVSAction().add_resubmit(None, TABLE_ID_IN_QOS),
            TABLE_ID_IN_QOS: OVSAction().add_resubmit(None, TABLE_ID_IN_ACL),
            TABLE_ID_IN_ACL: OVSAction().add_resubmit(None, TABLE_ID_DRAIN),
            TABLE_ID_DRAIN: OVSAction().add_resubmit(None, TABLE_ID_PVLAN),
            TABLE_ID_PVLAN: OVSAction().add_resubmit(None, TABLE_ID_IN_END),
            TABLE_ID_IN_END: OVSAction().add_xnormal(),
            TABLE_ID_OUT_START: OVSAction().add_resubmit(None, TABLE_ID_OUT_QOS),
            TABLE_ID_OUT_QOS: OVSAction().add_resubmit(None, TABLE_ID_OUT_ACL),
            TABLE_ID_OUT_ACL: OVSAction().add_resubmit(None, TABLE_ID_PVLAN_LEARNED),
            TABLE_ID_PVLAN_LEARNED: OVSAction().add_resubmit(None, TABLE_ID_OUT_END),
            TABLE_ID_OUT_END: OVSAction().add_output_outport()
        }
        self._ovsmeter = OVSMeter()

    @staticmethod
    def get_type_version():
        return "cas", "0.1"

    def __is_support_bridge(self, bridge):
        return bridge and (bridge.br_mode == BR_MODE_VEB
                           or bridge.br_mode == BR_MODE_CAS_VXLAN)

    def __get_default_action(self, table_id):
        if table_id == TABLE_ID_IN_ACL and not net_lib.GlobalConfig().enable_xnormal:
            return OVSAction().add_normal()
        return self._defalut_actions.get(table_id, OVSACTION_DROP)

    def install_base_flows(self, bridge):
        fail_mode = net_lib.Utility.cmd_execute("ovs-vsctl -t 5 get bridge %s fail_mode" % bridge.name, True)[1].split()
        if fail_mode[0] == "secure":
            logging.debug("Bridge %s: fail_mode=secure, no need to install base flows!", bridge.name)
            return

        cmdret = net_lib.Utility.cmd_execute("ovs-vsctl -t 5 --verbose=db_ctl_base:syslog:off get Open_vSwitch . other_config:bypass", log_err=False)[1].split()
        global_bypass = cmdret[0].strip('\'"') == "true" if len(cmdret) else False
        logging.debug("Enter install_base_flows bridge: %s", bridge.name)
        if not self.__is_support_bridge(bridge):
            return
        logging.debug("Inner install_base_flows bridge: %s", bridge.name)

        def get_default_flow(table_id):
            ac = self.__get_default_action(table_id)
            no_state_ac = OVSAction().add_ct_clear().add_actions(ac) if (table_id == TABLE_ID_IN_QOS or table_id == TABLE_ID_OUT_QOS) else OVSAction().add_actions(ac)

            def get_icmp6_no_state_flows(it):
                return Flow(bridge=bridge.name, table=table_id, cookie=COOKIE_BASIC, priority=PRIORITY_OFFSET_NO_STATE, ct_state="+trk",
                            actions=no_state_ac, dl_type='ipv6', nw_proto="icmp6", icmp_type=it)

            def get_dhcp_no_state_flows(dl_type, tp_src, tp_dst):
                return Flow(bridge=bridge.name, table=table_id, cookie=COOKIE_BASIC, priority=PRIORITY_OFFSET_NO_STATE, ct_state="+trk",
                            actions=no_state_ac, nw_proto="udp", dl_type=dl_type, tp_src=tp_src, tp_dst=tp_dst)

            flows = [Flow(bridge=bridge.name, table=table_id, cookie=COOKIE_BASIC, priority=PRIORITY_OFFSET_BASIC, actions=ac)]
            if table_id == TABLE_ID_BIND_IP:
                if not net_lib.GlobalConfig().enable_l2_acl:
                    flows.append(flows[0].clone())
                    flows[0].matchfield.dl_type = "ip"
                    flows[1].matchfield.dl_type = "ipv6"
            if table_id == TABLE_ID_IN_QOS or table_id == TABLE_ID_OUT_QOS:
                flows += [
                    get_icmp6_no_state_flows(130),
                    get_icmp6_no_state_flows(131),
                    get_icmp6_no_state_flows(132),
                    get_icmp6_no_state_flows(133),
                    get_icmp6_no_state_flows(134),
                    get_icmp6_no_state_flows(135),
                    get_icmp6_no_state_flows(136),
                    get_dhcp_no_state_flows("ip", 67, 68),
                    get_dhcp_no_state_flows("ip", 68, 67),
                    get_dhcp_no_state_flows("ipv6", 547, 546),
                    get_dhcp_no_state_flows("ipv6", 546, 547),
                ]
            return flows

        def get_default_flow_for_type_drain():
            return Flow(bridge=bridge.name, table=0, cookie=COOKIE_BASIC, priority=PRIORITY_OFFSET_DRAIN, actions='drop')

        # install flow in a reverse order
        flows = []
        if OVSDb.get_bridge_network_type(bridge.name) == BRIDGE_TYPE_DRAIN:
            flows.append(get_default_flow_for_type_drain())
        else:
            for tab in [TABLE_ID_OUT_END, TABLE_ID_PVLAN_LEARNED, TABLE_ID_OUT_ACL, TABLE_ID_OUT_QOS, TABLE_ID_OUT_START, TABLE_ID_IN_ACL, TABLE_ID_IN_QOS, TABLE_ID_BIND_IP, TABLE_ID_DRAIN, TABLE_ID_PVLAN, TABLE_ID_IN_END]:
                flows.extend(get_default_flow(tab))
        if global_bypass:
            flows.append(Flow(bridge=bridge.name, table=TABLE_ID_BIND_IP, cookie=COOKIE_BASIC, priority=PRIORITY_OFFSET_BYPASS, actions=OVSAction().add_normal()))
        self._txn.add_flows(flows)

    def install_raw_flows(self, bridge):
        br_flows = net_lib.GlobalConfig().get_flow(bridge.name)
        if br_flows:
            for fun_name, fun_flows in br_flows.items():
                if fun_name and fun_flows:
                    self._txn.add_raw(bridge.name, fun_flows)
                    self._txn.commit()

    def uninstall_base_flows(self, bridge):
        if not self.__is_support_bridge(bridge):
            return
        flow = Flow(bridge=bridge.name, cookie="%d/-1" % COOKIE_BASIC)
        self._txn.del_flows(flow)

    def clear_iface(self, bridge, iface):
        if not self.__is_support_bridge(bridge):
            return
        return self._clear_iface(bridge, iface)

    def apply_used_by_mirror(self, bridge, iface):
        if not self.__is_support_bridge(bridge):
            return
        logging.debug("Delete the bridge port mirror, and the destination bridge %s should restore the default basic flow table", bridge.name)
        if iface.is_local():
            self.install_base_flows(bridge)

    def del_used_by_mirror(self, bridge, iface):
        if not self.__is_support_bridge(bridge):
            return
        logging.debug("Delete the bridge port mirror dst-br %s flow table", bridge.name)
        flow = Flow(
            bridge=bridge.name,
            cookie="%d/-1" % COOKIE_USED_BY_MIRROR)
        self._txn.del_flows(flow)

    def apply_bypass(self, bridge, iface):
        if not self.__is_support_bridge(bridge):
            return
        logging.debug("add bypass high priority flow br=%s iface=%s", bridge.name, iface.name)
        flows = [Flow(bridge=bridge.name, cookie=COOKIE_BYPASS, table=TABLE_ID_BIND_IP,
                      priority=PRIORITY_OFFSET_BYPASS, in_port=iface.ofport,
                      actions=OVSAction().add_normal()),
                 Flow(bridge=bridge.name, cookie=COOKIE_BYPASS, table=TABLE_ID_OUT_START,
                      priority=PRIORITY_OFFSET_BYPASS, out_port=iface.ofport,
                      actions=OVSAction().add_output_outport())]
        self._txn.add_flows(flows)

    def del_bypass(self, bridge, iface):
        if not self.__is_support_bridge(bridge):
            return
        logging.debug("delete the bypass br=%s iface=%s", bridge.name, iface.name)
        flows = [Flow(bridge=bridge.name, cookie="%d/-1" % COOKIE_BYPASS, in_port=iface.ofport),
                 Flow(bridge=bridge.name, cookie="%d/-1" % COOKIE_BYPASS, out_port=iface.ofport)]
        self._txn.del_flows(flows)

    def apply_tcp_syn_forbid(self, bridge, iface):
        if not self.__is_support_bridge(bridge):
            return
        flow = Flow(
            bridge=bridge.name,
            cookie=COOKIE_TCP_SYN_FORBID,
            table=TABLE_ID_TCP_SYN_FORBID,
            priority=PRIORITY_OFFSET_TCP_SYN_FORBID,
            actions=OVSACTION_DROP)
        flow.matchfield.set_fields(
            out_port=iface.ofport,
            dl_type='ip',
            nw_proto='tcp',
            tcp_flags='+syn-ack')
        self._txn.add_flows(flow)

    def del_tcp_syn_forbid(self, bridge, iface):
        if not self.__is_support_bridge(bridge):
            return
        flow = Flow(
            bridge=bridge.name,
            cookie="%d/-1" % COOKIE_TCP_SYN_FORBID,
            table=TABLE_ID_TCP_SYN_FORBID,
            out_port=iface.ofport)
        self._txn.del_flows(flow)

    def apply_bind_ip(self, bridge, iface, ipv4, ipv6):
        if not self.__is_support_bridge(bridge):
            return
        self._apply_bind_ip(COOKIE_BIND_IP, TABLE_ID_BIND_IP, PRIORITY_OFFSET_BIND_IP, bridge, iface, ipv4, ipv6)

    def del_bind_ip(self, bridge, iface):
        if not self.__is_support_bridge(bridge):
            return
        self._del_bind_ip(COOKIE_BIND_IP, bridge, iface)

    def _unapply_acl(self, bridge, iface, acl_netprofile):
        self.del_acl(bridge, iface)

    def apply_acl(self, bridge, iface, aclrules, index, is_default, is_deault_rule):
        def is_ip_rule(ip_type, matchfield):
            dl_type_ipv4 = ["ip", "0x0800"]
            dl_type_ipv6 = ["ipv6", "0x86dd"]

            dl_type = dl_type_ipv4 if ip_type == 4 else dl_type_ipv6
            return matchfield.dl_type in dl_type

        if not self.__is_support_bridge(bridge):
            return
        has_state = net_lib.GlobalConfig().enable_state and any([r.statematch for r in aclrules])
        has_ipv4_rule = any([is_ip_rule(4, r.matchfield) for r in aclrules])
        has_ipv6_rule = any([is_ip_rule(6, r.matchfield) for r in aclrules])

        all_flows = []

        if has_state:
            all_flows = self.__instance_acl_once_state_flows(bridge, iface, has_ipv4_rule, has_ipv6_rule)

        for acl_rule in aclrules:
            if not is_deault_rule and not is_default and acl_rule.priority <= 4:
                continue
            all_flows.extend(self.__instance_one_aclrule(bridge, iface, acl_rule, has_state, index, is_default))

        self._txn.add_flows(all_flows)

    def del_acl(self, bridge, iface):
        if not self.__is_support_bridge(bridge):
            return
        flow_in = Flow(bridge=bridge.name, cookie="%d/-1" % COOKIE_ACL, in_port=iface.ofport)
        flow_out = Flow(bridge=bridge.name, cookie="%d/-1" % COOKIE_ACL, out_port=iface.ofport)
        self._txn.del_flows([flow_in, flow_out])

    def __instance_acl_once_state_flows(self, bridge, iface, has_ipv4_rule, has_ipv6_rule):
        def instance_one_direction(d, dl_type):
            if d == DIRECTION_IN:
                pre_t, table_mid, table = TABLE_ID_BIND_IP, TABLE_ID_IN_QOS, TABLE_ID_IN_ACL
                in_port, out_port = iface.ofport, None
            else:
                pre_t, table_mid, table = TABLE_ID_OUT_START, TABLE_ID_OUT_QOS, TABLE_ID_OUT_ACL
                in_port, out_port = None, iface.ofport
            action = self.__get_default_action(table)

            def new_state_flow(t, pr, cs, ac, cm=None):
                return Flow(bridge=bridge.name, cookie=COOKIE_ACL, table=t, priority=pr, actions=ac, in_port=in_port, out_port=out_port, dl_type=dl_type, ct_state=cs, ct_mark=cm)
            res = [
                new_state_flow(pre_t, 10, "-trk", OVSAction("ct(table=%d,zone=%d)" % (table_mid, get_iface_cur_tag(iface)))),
                new_state_flow(table, 70, "+trk+inv", OVSACTION_DROP),
                new_state_flow(table, 60, "+trk", OVSACTION_DROP, "1"),
                new_state_flow(table, 50, "+trk+est-rel+rpl", action),
                new_state_flow(table, 40, "-new-est+rel-inv", action),
                new_state_flow(table, 20, "+trk-est", OVSACTION_DROP),
            ]
            est_flow = new_state_flow(table, 30, "+trk+est", OVSAction("ct(commit,zone=%d,exec(load:1->NXM_NX_CT_MARK[]))" % get_iface_cur_tag(iface)))
            res.append(est_flow)
            if d == DIRECTION_OUT:
                est_flow_drop = est_flow.clone()
                est_flow_drop.matchfield.set_fields(reg_multiple_outport=FLOW_REG_MASK_MULTIPLE_OUTPORT)
                est_flow_drop.actions = OVSACTION_DROP
                res.append(est_flow_drop)
                est_flow.matchfield.set_fields(reg_multiple_outport=0)
            return res

        flows = []
        if has_ipv4_rule:
            flows += instance_one_direction(DIRECTION_IN, "ip")
            flows += instance_one_direction(DIRECTION_OUT, "ip")
        if has_ipv6_rule:
            flows += instance_one_direction(DIRECTION_IN, "ipv6")
            flows += instance_one_direction(DIRECTION_OUT, "ipv6")
        return flows

    def __convert_to_statefull_flow(self, new_flow, direction, tag, action):
        est_flow = new_flow.clone()
        est_flow.matchfield.set_fields(ct_state="+est-rel-rpl")
        new_flow.matchfield.set_fields(ct_state="+new-est")
        res = [est_flow, new_flow]

        if action == ACTION_ACCEPT:
            new_flow.actions = OVSAction("ct(commit,zone=%d)" % tag).add_actions(new_flow.actions)
        else:
            est_flow.actions = OVSAction("ct(commit,zone=%d)" % tag).add_actions(est_flow.actions)
            if direction == DIRECTION_OUT:
                est_flow_drop = est_flow.clone()
                est_flow_drop.matchfield.set_fields(reg_multiple_outport=FLOW_REG_MASK_MULTIPLE_OUTPORT)
                est_flow_drop.actions = OVSACTION_DROP
                res.append(est_flow_drop)
                est_flow.matchfield.set_fields(reg_multiple_outport=0)
        return res

    def __instance_one_aclrule(self, bridge, iface, acl_rule, has_state, index, is_default):
        def instance_acl_flows_common(d, mf):
            if d == DIRECTION_IN:
                table = TABLE_ID_IN_ACL
                in_port, out_port = iface.ofport, None
            else:
                table = TABLE_ID_OUT_ACL
                in_port, out_port = None, iface.ofport
            cookie = COOKIE_ACL
            action = self.__get_default_action(table) if acl_rule.action == ACTION_ACCEPT else OVSACTION_DROP
            if has_state and not is_default:
                pr = PRIORITY_OFFSET_ACL + acl_rule.priority + (index * 1000 if acl_rule.priority >= 10 else 0)
            else:
                pr = PRIORITY_OFFSET_ACL + acl_rule.priority

            f = Flow(bridge=bridge.name, cookie=cookie, table=table, priority=pr, matchfield=mf, actions=action, in_port=in_port, out_port=out_port)
            return self.__convert_to_statefull_flow(f, d, get_iface_cur_tag(iface), acl_rule.action) if has_state else [f]

        def make_up_flows(acl_rule, mf):
            flows = []
            if acl_rule.direction != DIRECTION_INOUT:
                flows += instance_acl_flows_common(acl_rule.direction, mf)
            else:
                flows += instance_acl_flows_common(DIRECTION_IN, mf)
                mf = acl_rule.matchfield.clone()
                mf.reverse()
                flows += instance_acl_flows_common(DIRECTION_OUT, mf)
            return flows

        def get_port_rule(port_min, port_max):
            if port_max == FLOW_MASK_ALL:
                return [port_min], [port_max]

            port_rules = port_rule_masking(int(port_min), int(port_max))
            tp_list, tp_mask_list = [], []
            for item in port_rules:
                if item.find('/') > -1:
                    tp_list.append(item.strip().split("/", 2)[0])
                    tp_mask_list.append(item.strip().split("/", 2)[1])
                else:
                    tp_list.append(item.strip().split("/", 2)[0])
                    tp_mask_list.append(FLOW_MASK_ALL)
            return tp_list, tp_mask_list

        flow_list = []
        pt_srcs, pt_src_masks = get_port_rule(acl_rule.matchfield.tp_src, acl_rule.matchfield.tp_src_mask)
        pt_dsts, pt_dst_masks = get_port_rule(acl_rule.matchfield.tp_dst, acl_rule.matchfield.tp_dst_mask)
        for idx_pt_src in range(len(pt_srcs)):
            for idx_pt_dst in range(len(pt_dsts)):
                mf = acl_rule.matchfield.clone()
                mf.tp_src = pt_srcs[idx_pt_src]
                mf.tp_src_mask = pt_src_masks[idx_pt_src]
                mf.tp_dst = pt_dsts[idx_pt_dst]
                mf.tp_dst_mask = pt_dst_masks[idx_pt_dst]
                flow_list.extend(make_up_flows(acl_rule, mf))
        return flow_list

    def clear_all(self, bridge):
        if not self.__is_support_bridge(bridge):
            return
        self._txn.del_flows(Flow(bridge=bridge.name))
        self._txn.add_flows(Flow(bridge=bridge.name, table=0, priority=0, actions=OVSAction().add_normal()))

    def apply_netprofiles(self, bridge, iface, netprofiles, dm):
        if not self.__is_support_bridge(bridge):
            return

        has_net_acl = False
        is_default_acl = False
        is_default_rule = True
        index_offset = 0
        if netprofiles:
            sort_netprofiles = []
            for netprofile in netprofiles:
                if netprofile.type == "drain" or netprofile.type == "trunkport" or netprofile.type == "pvlan":
                    sort_netprofiles.insert(0, netprofile)
                else:
                    sort_netprofiles.append(netprofile)
                if netprofile.name == DEFAULT_WHITE_NAME or netprofile.name == DEFAULT_BLACK_NAME:
                    is_default_rule = False

            for netprofile in sort_netprofiles:
                if netprofile.type == "qos":
                    self._apply_qos(bridge, iface, netprofile)
                    index_offset = index_offset + 1
                elif netprofile.type == "acl":
                    is_default_acl = True if netprofile.name == DEFAULT_WHITE_NAME or netprofile.name == DEFAULT_BLACK_NAME else False
                    self.apply_acl(bridge, iface, netprofile.rules, sort_netprofiles.index(netprofile)-index_offset, is_default_acl, is_default_rule)  # feature deliver in the sense of per netprofile
                    has_net_acl = True
                elif netprofile.type == "drain":
                    self._apply_drain(bridge, iface, netprofile, dm)
                    index_offset = index_offset + 1
                elif netprofile.type == "trunkport":
                    self._apply_trunkport(iface, netprofile, dm)
                    index_offset = index_offset + 1
                elif netprofile.type == "pvlan":
                    self._apply_pvlan(bridge, iface, netprofile, dm)
                    index_offset = index_offset + 1
                else:
                    logging.info("unknown netprofile type %s, skip add flow", netprofile.type)
        if not has_net_acl and iface.nwfilter:
            acl = dm.get_nwfilter_by_name(iface.nwfilter)
            if acl is None:
                logging.warn("can't find acl %s of iface %s, maybe nwfilter is not loaded yet (fallback)", iface.nwfilter, iface.name)
            else:
                self.apply_acl(bridge, iface, acl.aclrules, 0, False, True)
                logging.debug("setup acl flows for %s has been done (fallback).", iface)

    def unapply_netprofiles(self, bridge, iface, netprofiles, dm):
        if not self.__is_support_bridge(bridge) or len(netprofiles) == 0:
            if len(netprofiles) == 0:
                self._unapply_acl(bridge, iface, netprofiles)
            return
        has_acl = False
        for netprofile in netprofiles:
            if netprofile.type == "qos":
                self._unapply_qos(bridge, iface, netprofile)
            elif netprofile.type == "acl":
                has_acl = True
            elif netprofile.type == "drain":
                self._unapply_drain(bridge, iface, netprofile, dm)
            elif netprofile.type == "trunkport":
                self._unapply_trunkport(iface, netprofile, dm)
            elif netprofile.type == "pvlan":
                self._unapply_pvlan(bridge, iface, netprofile, dm)
            else:
                logging.info("unknown netprofile type %s, skip del flow", netprofile.type)
        if has_acl:
            self._unapply_acl(bridge, iface, netprofile)

    def _apply_drain(self, bridge, iface, drain_netprofile, dm):
        if drain_netprofile is None:
            logging.info("drain_netprofile is None")
            return

        br = bridge.name
        inbr = None  # in vnet
        outbr = None  # out vnet
        invlan = 0
        outvlan = 0
        for rule in drain_netprofile.rules:
            if rule.direction == DIRECTION_IN:
                inbr, invlan = rule.action.bridge, rule.action.vlan
            elif rule.direction == DIRECTION_OUT:
                outbr, outvlan = rule.action.bridge, rule.action.vlan

        # two drain_type:
        # uplink: drain to out of cvk.
        # vm_port: drain to vm port.
        drain_type = "uplink" #uplink or vm_port
        vm_ports=[]
        origin_br = iface.origin_br
        if origin_br:
            drain_type = "vm_port"

        # 1. pre check
        if not inbr or not outbr:
            logging.error("drain_netprofile is incomplete")
            return
        if not OVSDb.bridge_exists(inbr) or (outbr != inbr and not OVSDb.bridge_exists(outbr)):
            logging.error("drain bridge (%s, %s) not exists" % (inbr, outbr))
            return

        if drain_type == "vm_port":
            if inbr != outbr:
                logging.error("drain_type %s error: netprofile has two br: %s, %s." % (drain_type, inbr, outbr))
                return
            if OVSDb.get_bridge_network_type(inbr) != BRIDGE_TYPE_DRAIN or net_lib.Utility.uplink(inbr):
                logging.error("drain_type %s error: bridge network type error, or uplink error." % (drain_type))
                return

            if self._check_drain_pair_used(br, origin_br, dm):
                logging.error("%s or %s is used by other drain netprofile." % (br, origin_br))
                return

            bypass_state = dm.get_drain_bypass_state()
            vm_ports = net_lib.Utility.iface_ports(inbr)
            if not (len(vm_ports) == 2 or (len(vm_ports) == 0 and bypass_state)):
                logging.error("drain_type %s requires only two vm ports, but bridge has ports: %s." % (drain_type, vm_ports))
                return

        else:
            if not net_lib.Utility.uplink(inbr) or (outbr != inbr and not net_lib.Utility.uplink(outbr)):
                logging.error("drain bridge (%s, %s) uplink not exists" % (inbr, outbr))
                return
            if invlan == 0 or outvlan == 0:
                logging.error("drain vlan (%s, %s) is invalid" % (invlan, outvlan))
                return
        logging.debug("%s drain pre condition verified ok", iface.name)


        # 2. ensure connect all bridges
        if drain_type == "vm_port":
            logging.info("start to apply drain for iface %s, (%s, %s, %s)", iface.name, br, inbr, origin_br)
            if bypass_state:
                logging.warn("bypass is enabled when apply_drain for iface %s." % iface.name)
                if not self._is_bridge_connected(br, origin_br, dm):
                    ensure_connect(br, origin_br, drain_type)
                self._set_drain_bypass(inbr, br, origin_br, False, dm)
            else:
                if not self._is_bridge_connected(inbr, br, dm):
                    ensure_connect(inbr, br, drain_type)
                if not self._is_bridge_connected(inbr, origin_br, dm):
                    ensure_connect(inbr, origin_br, drain_type)
                self._set_drain_bypass(inbr, br, origin_br, True, dm)
        else:
            #connect brdige drain and busibr, for drain type uplink.
            ensure_connect(br, inbr, drain_type)
            ensure_connect(br, outbr, drain_type)

        # 3. reconfigure vnet for apply vm_port drian.
        if drain_type == "vm_port":
            origin_vlan = iface.origin_vlan
            drain_vlan = iface.drain_vlan
            if not drain_vlan or drain_vlan == origin_vlan:
                drain_vlan = dm.get_unused_drain_vlan(origin_vlan)
                iface.other_config['drain_vlan'] = drain_vlan

            logging.info("set drian vlan for port %s, vlan_info:(origin: %s, drain: %s)." % (iface.name, origin_vlan, drain_vlan))
            if bypass_state:
                origin_vlan_str = origin_vlan
                if origin_vlan_str == "0":
                    origin_vlan_str = "[]"
                ensure_drian_vlan(iface.name, origin_vlan_str, drain_vlan)
            else:
                ensure_drian_vlan(iface.name, drain_vlan, drain_vlan)

        # 4. construct flow in drain table
        def cf(relpr, ac, **kwargs):
            flow = Flow(
                bridge=bridge.name,
                cookie=COOKIE_DRAIN,
                table=TABLE_ID_DRAIN,
                priority=PRIORITY_OFFSET_DRAIN+relpr,
                actions=ac,
                **kwargs)
            return flow

        def cbf(br, relpr, ac, **kwargs):
            flow = Flow(
                bridge=br,
                cookie=COOKIE_DRAIN,
                table=0,
                priority=PRIORITY_OFFSET_DRAIN+relpr,
                actions=ac,
                **kwargs)
            return flow

        def __ensure_drain_bridge_flows(br, vlan, brbase, iface, isin):
            peer = OVSDb.get_ofport_by_iface(patch_port(brbase, br))
            mac = iface.mac
            if isin:
                from_ = peer
                to = net_lib.Utility.uplink(br)
            else:
                from_ = net_lib.Utility.uplink(br)
                to = peer
            # if flow construction change, don't forget to change delete flow method
            flows = [
                #- packet from 'from' flowed into 'to'
                cbf(br, 1, "%s" % to, in_port=from_, dl_vlan=vlan, dl_src=mac),
                #- packet from 'to' flowed into 'from'
                cbf(br, 1, "%s" % from_, in_port=to, dl_vlan=vlan, dl_dst=mac),
                #- packet from 'to' flowed into 'from'
                cbf(br, 1, "%s" % from_, in_port=to, dl_vlan=vlan, dl_dst='01:00:00:00:00:00', dl_dst_mask='01:00:00:00:00:00'),
                #- bridge only allows drain traffic, to ensure this given the default to drop
                cbf(br, 0, "drop", in_port=peer)
            ]
            return flows

        def __instance_drain_flows(br, iface, inbr, invlan, outbr, outvlan):
            peout = OVSDb.get_ofport_by_iface(patch_port(outbr, br))
            pein = OVSDb.get_ofport_by_iface(patch_port(inbr, br))
            mac = iface.mac
            tag = get_iface_cur_tag(iface)

            # if flow construction change, don't forget to change delete flow method
            flows = [
                #- in drain: packet from vnet is sent to pein with vlan, learning src mac. If no learning, ovnet can't normal
                cf(1, OVSAction().add_vlan(invlan, False).add_loutput(pein, tag), in_port=iface.ofport),  # need learn!
                #- in drain: packet from peout do tag xnormal to determine the output
                # dl_src=mac is added to associate flow to vnet
                cf(1, OVSAction().add_vlan(tag, True).add_xnormal(), in_port=peout, dl_vlan=outvlan, dl_src=mac),
                #- out drain: packet destinated to vnet from ovnet, lean+tag output
                cf(0, OVSAction().add_vlan(outvlan, False).add_loutput(peout, tag), dl_dst=mac),
                #- out drain: packet from pein destinated to vnet is sent to vnet
                # dl_dst=mac is added to associate flow to vnet
                cf(2, OVSAction("pop_vlan").add_set_output_reg(iface.ofport).add_resubmit(None, TABLE_ID_OUT_START), in_port=pein, dl_vlan=invlan, dl_dst=mac)
            ]

            for eth in net_lib.Utility.uplink(br, True):
                #- out drain: packet destinated to vnet from uplink, learn+tag output
                flows.append(cf(1, OVSAction().add_vlan(outvlan, True).add_loutput(peout, tag), in_port=eth, dl_vlan=tag, dl_dst=mac))

            # linux bridge + ovs mode
            for veth in get_veth_by_iface(iface):
                flows.append(cf(1, OVSAction().add_vlan(outvlan, True).add_loutput(peout, tag), in_port=veth, dl_vlan=tag, dl_dst=mac))

            return flows

        def get_drain_vlan_actions(drain_vlans, drain_vlan, port, skip):
            vlan_actions = ""
            for vlan in drain_vlans:
                if skip and vlan == drain_vlan:
                    continue
                vlan_actions = vlan_actions + ",mod_vlan_vid:%s,%s" % (vlan, port)

            return vlan_actions.strip(",")

        def get_origin_vlan_actions(origin_vlan):
            if origin_vlan == "0":
                vlan_actions = "strip_vlan"
            else:
                vlan_actions = "mod_vlan_vid:%s" % origin_vlan

            return vlan_actions

        def __instance_drain_flows_for_type_vm_port(inbr, iface, origin_vlan, drain_vlan):
            flows = []
            flows_del = []
            if not vm_ports:
                return flows, flows_del

            # vm packets send to ovs:
            vm_ports.sort()
            vm_from = vm_ports[0] # receive packets from vm_from port.
            vm_to = vm_ports[1] # send packets to vm_to port.
            patch_to = OVSDb.get_ofport_by_iface(patch_port(iface.origin_br, inbr)) # patch_to port connect brdige drain and business br.
            patch_from = OVSDb.get_ofport_by_iface(patch_port(br, inbr)) # patch_from port connect brdige drain and segbr.

            origin_vlan_actions = get_origin_vlan_actions(origin_vlan)
            flows = [
                cbf(inbr, 1, "%s" % vm_to, in_port=patch_from, dl_vlan=drain_vlan),
                cbf(inbr, 1, "%s,%s" %(origin_vlan_actions, patch_to), in_port=vm_from, dl_vlan=drain_vlan)
            ]

            drain_vlans, same_vlan_ports = dm.get_all_drian_vlan_by_origin_vlan(origin_vlan, origin_br)
            if drain_vlan not in drain_vlans:
                drain_vlans.append(drain_vlan)
            for vlan in drain_vlans:
                drain_vlan_actions = get_drain_vlan_actions(drain_vlans, vlan, "in_port", True)
                if drain_vlan_actions:
                    actions = "%s,%s,%s" %(origin_vlan_actions, patch_to, drain_vlan_actions)
                else:
                    actions = "%s,%s" %(origin_vlan_actions, patch_to)
                flows.append(cbf(inbr, 2, "%s" % actions, in_port=vm_from, dl_dst='01:00:00:00:00:00', dl_dst_mask='01:00:00:00:00:00', dl_vlan=vlan))

            # other packets send to vm.
            mac = iface.mac
            if origin_vlan == "0":
                origin_vlan_str = "0xffff" # (dl_vlan=0xffff) equl (vlan_tci=0x0000/0x1fff), this means no vlan tag in packet.
            else:
                origin_vlan_str = origin_vlan
            drain_vlan_actions = get_drain_vlan_actions(drain_vlans, drain_vlan, vm_from, False)

            flows.append(cbf(inbr, 2, "%s" % (drain_vlan_actions), in_port=patch_to, dl_dst='01:00:00:00:00:00', dl_dst_mask='01:00:00:00:00:00', dl_vlan=origin_vlan_str))
            flows.append(cbf(inbr, 1, "mod_vlan_vid:%s,%s" %(drain_vlan, vm_from), in_port=patch_to, dl_dst=mac, dl_vlan=origin_vlan_str))
            flows.append(cbf(inbr, 1, "%s" %(patch_from), in_port=vm_to, dl_vlan=drain_vlan))

            if [mac, drain_vlan] not in same_vlan_ports:
                same_vlan_ports.append([mac, drain_vlan])
            for port_mac1, vlan1 in same_vlan_ports:
                flows_del.append(Flow(bridge=inbr, cookie="%d/-1" % COOKIE_DRAIN, in_port=vm_from, dl_dst=port_mac1))
                for port_mac2, vlan2 in same_vlan_ports:
                    if port_mac2 != port_mac1:
                        flows.append(cbf(inbr, 2, "mod_vlan_vid:%s,%s" %(vlan1, "in_port"), in_port=vm_from, dl_dst=port_mac1, dl_vlan=vlan2))

            return flows, flows_del

        all_flows = []
        flows_del = []
        if drain_type == "vm_port":
            if not bypass_state:
                all_flows, flows_del = __instance_drain_flows_for_type_vm_port(inbr, iface, origin_vlan, drain_vlan)
        else:
            all_flows += __ensure_drain_bridge_flows(inbr, invlan, br, iface, True)
            all_flows += __ensure_drain_bridge_flows(outbr, outvlan, br, iface, False)
            all_flows += __instance_drain_flows(br, iface, inbr, invlan, outbr, outvlan)

        if flows_del:
            self._txn.del_flows(flows_del)
        if all_flows:
            self._txn.add_flows(all_flows)
        dm.update_iface_drain_netprofile(iface, drain_netprofile)

        # 5. send drain info to vSSM.
        if drain_type == "vm_port":
            net_lib.Utility.cmd_execute("dpi-ctl -d 1 -m %s -v %s -o %s" %(iface.mac, origin_vlan, drain_vlan))

        return

