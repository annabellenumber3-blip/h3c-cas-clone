def get_vnet_info_from_xml(iface):
    def get_info_from_ovsdb(attri):
        if attri == 'mtu':
            cmd = "ovs-vsctl get interface %s mtu" % iface.name
            mtu = net_lib.Utility.cmd_output_string(cmd).strip()
            return mtu if mtu else '1500'
        if attri == 'tag':
            cmd = "ovs-vsctl get port %s tag" % iface.name
            tag = net_lib.Utility.cmd_output_string(cmd).strip()
            return tag if tag else '[]'
        if attri == 'path':
            cmd = "ovs-vsctl get interface %s options:vhost-server-path" % iface.name
            path = net_lib.Utility.cmd_output_string(cmd).strip()
            return path if path else ""

    xml_str = net_lib.Utility.cmd_execute("virsh dumpxml %s" % iface.external_ids['vm-id'], log_dbg=False)[1]
    try:
        root = ET.fromstring(xml_str)
        if root.tag == "domstatus":
            root = root.find("domain")
        for interface in root.find("devices").findall("interface"):
            itype = interface.get("type","")
            if itype == "bridge" or itype == "vhostuser":
                mac = interface.find("mac").get("address", "")
                if mac != iface.external_ids['attached-mac']:
                    continue
                info = dict()
                vlan_tag = interface.find('vlan')
                if vlan_tag is not None:
                    info['tag'] = vlan_tag.find('tag').get("id", "1")
                else:
                    logging.error("can't get vlan tag from vm %s, get from ovsdb", iface.external_ids['vm-id'])
                    info['tag'] = get_info_from_ovsdb('tag')

                mtu_tag = interface.find("mtu")
                if mtu_tag is not None:
                    info['mtu'] = mtu_tag.get("size", "1500")
                else:
                    logging.error("can't get mtu from vm %s, get from ovsdb", iface.external_ids['vm-id'])
                    info['mtu'] = get_info_from_ovsdb('mtu')

                info['br_dp_type'] = 'system'  # just dpdk nic use netdev
                if itype == "vhostuser": # dpdk vnet
                    info['type'] = 'dpdkvhostuserclient'
                    info['br_dp_type'] = 'netdev'
                    path = interface.find("source").get('path', "")
                    if not path:
                        path = get_info_from_ovsdb('path')
                    info['path'] = path

                bandwidth_tag = interface.find('bandwidth')
                if bandwidth_tag is not None:
                    info['bandwidth'] = dict()
                    inbound = bandwidth_tag.find('inbound')
                    if inbound is not None:
                        inbound_dict = dict()
                        inbound_dict['average'] = inbound.get('average','0')
                        inbound_dict['peak'] = inbound.get('peak','0')
                        inbound_dict['burst'] = inbound.get('burst','0')
                        info['bandwidth']['inbound'] = inbound_dict
                    outbound = bandwidth_tag.find('outbound')
                    if outbound is not None:
                        outbound_dict = dict()
                        outbound_dict['average'] = str(int(outbound.get('average','0')) * 8)
                        outbound_dict['peak'] = str(int(outbound.get('peak','0')) * 8)
                        outbound_dict['burst'] = str(int(outbound.get('burst','0')) * 8)
                        info['bandwidth']['outbound'] = outbound_dict

                logging.debug("get vnet info: %s" % info)
                return info

    except Exception as e:
        logging.error("get vnet info from vm %s failed(%s)", iface.external_ids['vm-id'], str(e))
    return None

def get_vnet_info_with_try(iface, cnt=20):
    for n in range(cnt):
        logging.debug("trying %s time to get %s vnet info", str(n+1), iface.name)
        info = get_vnet_info_from_xml(iface)
        if info is not None:
            return info
        else:
            time.sleep(0.02)

    return None


class TrunkPortIfaceInfo(object):

    def __init__(self, origin_br):
        super(TrunkPortIfaceInfo, self).__init__()
        self.origin_br = origin_br
        self.patch_ports = {}

    def add_patch_port(self, spt_port, spi_port):
        self.patch_ports[spt_port] = spi_port

    def remove_patch_port(self, spt_port):
        self.patch_ports.pop(spt_port)

    def clear_patch_port(self):
        self.patch_ports = {}

    def __str__(self):
        return "origin_br: %s, patch_ports: %s" % (self.origin_br, self.patch_ports)

def iface_open_trunkport(iface, bridge, netprofile, dm):
    tbr_br = "tbr-%s" % iface.name
    if bridge == tbr_br:   # trunkport close -> open
        logging.warn('iface %s already open trunkport!' % iface.name)
        return False

    logging.info("start open iface %s trunkport..." % iface.name)
    vnet_info = get_vnet_info_with_try(iface)
    if not vnet_info:
        logging.error("open trunkport failed, can't get vnet info!")
        return False

    cmd = "ovs-vsctl -t 8 -- --if-exists del-br %s -- add-br %s" % (tbr_br, tbr_br)
    cmd += " -- set bridge %s other_config:br_mode=0 mcast_snooping_enable=false datapath_type=%s" % (tbr_br, vnet_info['br_dp_type'])
    cmd += " -- set port %s tag=[]" % tbr_br
    cmd += " -- set interface %s mtu_request=1500" % tbr_br
    if net_lib.Utility.cmd_execute(cmd)[0] != 0:
        return False

    tp_iface_info = TrunkPortIfaceInfo(bridge)
    cmd = "ovs-vsctl -t 5"
    del_cmd = "ovs-vsctl -t 5"
    for rule in netprofile.rules:
        spt_patch_port = "spt-%s.%s" % (iface.name, rule.in_vlan)
        spi_patch_port = "spi-%s.%s" % (iface.name, rule.in_vlan)
        del_cmd += " -- --if-exists del-port %s -- --if-exists del-port %s" % (spt_patch_port, spi_patch_port)
        cmd += " -- add-port %s %s -- set interface %s type=patch options:peer=%s -- set port %s tag=%s" % (tbr_br, spt_patch_port, spt_patch_port, spi_patch_port, spt_patch_port, rule.in_vlan)
        cmd += " -- add-port %s %s -- set interface %s type=patch options:peer=%s -- set port %s tag=%s" % (bridge, spi_patch_port, spi_patch_port, spt_patch_port, spi_patch_port, rule.action.out_vlan)
        tp_iface_info.add_patch_port(spt_patch_port, spi_patch_port)

    # add parent port spt&spi patch port
    tag = vnet_info['tag'] if vnet_info['tag'] != '1' else '0'
    spt_patch_port = "spt-%s.%s" % (iface.name, '0')
    spi_patch_port = "spi-%s.%s" % (iface.name, '0')
    del_cmd += " -- --if-exists del-port %s -- --if-exists del-port %s" % (spt_patch_port, spi_patch_port)
    cmd += " -- add-port %s %s -- set interface %s type=patch options:peer=%s -- set port %s tag=%s" % (tbr_br, spt_patch_port, spt_patch_port, spi_patch_port, spt_patch_port, '0')
    cmd += " -- add-port %s %s -- set interface %s type=patch options:peer=%s -- set port %s tag=%s" % (bridge, spi_patch_port, spi_patch_port, spt_patch_port, spi_patch_port, tag)
    tp_iface_info.add_patch_port(spt_patch_port, spi_patch_port)

    if net_lib.Utility.cmd_execute(del_cmd)[0] != 0:
        return False

    if net_lib.Utility.cmd_execute(cmd)[0] != 0:
        return False
    dm.update_tp_iface_info(iface, tp_iface_info)
    logging.debug("iface: %s, tp_info: %s" % (iface.name, dm.get_tp_iface_info(iface)))

    ext_ids = iface.external_ids
    other_cfg = iface.other_config

    cmd = "ovs-vsctl --timeout=5 -- --if-exists del-port %s %s" % (bridge, iface.name)
    if net_lib.Utility.cmd_execute(cmd)[0] != 0:
        return False

    cmd = "ovs-vsctl --timeout=5 -- add-port %s %s" % (tbr_br, iface.name)
    cmd += ' -- set Interface %s external-ids:attached-mac="%s"' % (iface.name, ext_ids['attached-mac'])
    cmd += ' -- set Interface %s external-ids:iface-id="%s"' % (iface.name, ext_ids['iface-id'])
    cmd += ' -- set Interface %s external-ids:vm-id="%s"' % (iface.name, ext_ids['vm-id'])
    cmd += ' -- set Interface %s external-ids:port-profile="%s"' % (iface.name, ext_ids['port-profile'])
    cmd += ' -- set Interface %s external-ids:iface-status=%s' % (iface.name, ext_ids['iface-status'])
    cmd += ' -- set Interface %s mtu_request=%s' % (iface.name, vnet_info['mtu'])

    if 'type' in vnet_info and vnet_info['type'] == 'dpdkvhostuserclient':
        cmd += ' -- set Interface %s type=%s' % (iface.name, vnet_info['type'])
        cmd += ' -- set Interface %s options:vhost-server-path=%s' % (iface.name, vnet_info['path'])

    if 'bandwidth' in vnet_info:
        outbound = vnet_info['bandwidth'].get('outbound', None)
        rate = outbound.get('average', '0') if outbound else '0'
        burst = outbound.get('burst', '0') if outbound else '0'
        cmd += ' -- set Interface %s ingress_policing_rate=%s' % (iface.name, rate)
        cmd += ' -- set Interface %s ingress_policing_burst=%s' % (iface.name, burst)

    if iface.bind_ip:
        cmd += ' -- set Interface %s other_config:bind-ip="%s"' % (iface.name, iface.bind_ip)
    if iface.bind_ipv6:
        cmd += ' -- set Interface %s other_config:bind-ipv6="%s"' % (iface.name, iface.bind_ipv6)
    if iface.tos:
        cmd += ' -- set Interface %s other_config:tos="%s"' % (iface.name, iface.tos)

    if net_lib.Utility.cmd_execute(cmd)[0] != 0:
        return False

    # tc config will disppear after port deleted from bridge, need apply again
    if 'bandwidth' in vnet_info:
        inbound = vnet_info['bandwidth'].get('inbound', None)
        if inbound is not None:
            average = inbound.get('average', '0')
            rate = inbound.get('peak', '0')
            burst = inbound.get('burst', '0')
            cmd = "virsh domiftune %s %s --live --inbound %s,%s,%s" % (ext_ids['vm-id'],ext_ids['attached-mac'], average, rate, burst)
            if net_lib.Utility.cmd_execute(cmd)[0] != 0:
                return False

    logging.info("open iface %s trunkport done." % iface.name)
    return True

def iface_close_trunkport(iface, bridge, dm):

    tbr_br = "tbr-%s" % iface.name
    if bridge != tbr_br:   # trunkport close -> open
        logging.warn('iface %s already close trunkport!' % iface.name)
        return False

    logging.info("start close iface %s trunkport..." % iface.name)

    tp_iface_info = dm.get_tp_iface_info(iface)
    if not tp_iface_info:
        logging.error("trunkport iface info is none when closing trunkport!")
        return False

    vnet_info = get_vnet_info_with_try(iface)
    if not vnet_info:
        logging.error("close trunkport failed, can't get vnet info!")
        return False

    tp_origin_br = tp_iface_info.origin_br
    ext_ids = iface.external_ids
    other_cfg = iface.other_config

    tag = vnet_info['tag'] if vnet_info['tag'] != '1' else '[]'
    cmd = "ovs-vsctl --timeout=5 -- --if-exists del-port %s %s" % (bridge, iface.name)
    if net_lib.Utility.cmd_execute(cmd)[0] != 0:
        return False

    cmd = "ovs-vsctl --timeout=5 -- add-port %s %s" % (tp_origin_br, iface.name)
    cmd += ' -- set Interface %s external-ids:attached-mac="%s"' % (iface.name, ext_ids['attached-mac'])
    cmd += ' -- set Interface %s external-ids:iface-id="%s"' % (iface.name, ext_ids['iface-id'])
    cmd += ' -- set Interface %s external-ids:vm-id="%s"' % (iface.name, ext_ids['vm-id'])
    cmd += ' -- set Interface %s external-ids:port-profile="%s"' % (iface.name, ext_ids['port-profile'])
    cmd += ' -- set Interface %s external-ids:iface-status=%s' % (iface.name, ext_ids['iface-status'])
    cmd += ' -- set Interface %s other_config:origin_vlan=%s' % (iface.name, tag if tag != "[]" else "0")
    cmd += ' -- set Port %s tag=%s' % (iface.name, tag)
    cmd += ' -- set Interface %s mtu_request=%s' % (iface.name, vnet_info['mtu'])

    if 'type' in vnet_info and vnet_info['type'] == 'dpdkvhostuserclient':
        cmd += ' -- set Interface %s type=%s' % (iface.name, vnet_info['type'])
        cmd += ' -- set Interface %s options:vhost-server-path=%s' % (iface.name, vnet_info['path'])

    if iface.bind_ip:
        cmd += ' -- set Interface %s other_config:bind-ip="%s"' % (iface.name, iface.bind_ip)
    if iface.bind_ipv6:
        cmd += ' -- set Interface %s other_config:bind-ipv6="%s"' % (iface.name, iface.bind_ipv6)
    if iface.tos:
        cmd += ' -- set Interface %s other_config:tos="%s"' % (iface.name, iface.tos)

    if 'bandwidth' in vnet_info:
        outbound = vnet_info['bandwidth'].get('outbound', None)
        rate = outbound.get('average', '0') if outbound else '0'
        burst = outbound.get('burst', '0') if outbound else '0'
        cmd += ' -- set Interface %s ingress_policing_rate=%s' % (iface.name, rate)
        cmd += ' -- set Interface %s ingress_policing_burst=%s' % (iface.name, burst)

    if net_lib.Utility.cmd_execute(cmd)[0] != 0:
        return False

    if 'bandwidth' in vnet_info:
        inbound = vnet_info['bandwidth'].get('inbound', None)
        if inbound is not None:
            average = inbound.get('average', '0')
            peak = inbound.get('peak', '0')
            burst = inbound.get('burst', '0')
            cmd = "virsh domiftune %s %s --live --inbound %s,%s,%s" % (ext_ids['vm-id'],ext_ids['attached-mac'], average, peak, burst)
            if net_lib.Utility.cmd_execute(cmd)[0] != 0:
                return False

    cmd = "ovs-vsctl --timeout=5"
    for spt_port in tp_iface_info.patch_ports:
        spi_port = tp_iface_info.patch_ports[spt_port]
        cmd += ' -- --if-exists del-port %s' % spt_port
        cmd += ' -- --if-exists del-port %s' % spi_port

    cmd += " -- --if-exists del-br %s" % tbr_br
    if net_lib.Utility.cmd_execute(cmd)[0] != 0:
        return False

    dm.delete_tp_iface_info(iface)

    logging.info("close iface %s trunkport done." % iface.name)
    return True

def iface_update_trunkport(iface, br, old_netprofile, new_netprofile, dm):
    if not br or not br.startswith('tbr-'):
        logging.error("update iface %s trunkport failed, bridge %s is not tbr bridge" % (iface.name, br))
        return False

    tp_iface_info = dm.get_tp_iface_info(iface)
    if not tp_iface_info:
        logging.error("trunkport iface info is none when updating trunkport!")
        return False

    old_vlan_map = dict()
    new_vlan_map = dict()
    for rule in old_netprofile.rules:
        old_vlan_map[rule.in_vlan] = rule.action.out_vlan
    for rule in new_netprofile.rules:
        new_vlan_map[rule.in_vlan] = rule.action.out_vlan

    logging.debug('old_vlan_map: %s, new_vlan_map: %s' % (old_vlan_map, new_vlan_map))

    del_items = dict()
    change_items = dict()
    for in_vlan in old_vlan_map:
        if in_vlan not in new_vlan_map:
            del_items[in_vlan] = old_vlan_map[in_vlan]
        else:
            if old_vlan_map[in_vlan] == new_vlan_map[in_vlan]:
                new_vlan_map.pop(in_vlan)
            else:
                change_items[in_vlan] = new_vlan_map[in_vlan]
                new_vlan_map.pop(in_vlan)

    if not (bool(del_items) or bool(change_items) or bool(new_vlan_map)):
        logging.error("update iface %s trunkport not need" % iface.name)
        return False
    else:
        logging.info('new vlan rules: %s, change vlan rules: %s, del vlan rules: %s' % (new_vlan_map, change_items, del_items))

    cmd = "ovs-vsctl -t 5"
    for in_vlan, out_vlan in new_vlan_map.items():
        spt_patch_port = "spt-%s.%s" % (iface.name, in_vlan)
        spi_patch_port = "spi-%s.%s" % (iface.name, in_vlan)
        cmd += " -- --if-exists del-port %s -- add-port %s %s -- set interface %s type=patch options:peer=%s -- set port %s tag=%s" % \
                (spt_patch_port, br, spt_patch_port, spt_patch_port, spi_patch_port, spt_patch_port, in_vlan)
        cmd += " -- --if-exists del-port %s -- add-port %s %s -- set interface %s type=patch options:peer=%s -- set port %s tag=%s" % \
                (spi_patch_port, tp_iface_info.origin_br, spi_patch_port, spi_patch_port, spt_patch_port, spi_patch_port, out_vlan)
        tp_iface_info.add_patch_port(spt_patch_port, spi_patch_port)

    for in_vlan, out_vlan in change_items.items():
        spi_patch_port = "spi-%s.%s" % (iface.name, in_vlan)
        cmd += " -- --if-exists set port %s tag=%s" % (spi_patch_port, out_vlan)

    for in_vlan, out_vlan in del_items.items():
        spt_patch_port = "spt-%s.%s" % (iface.name, in_vlan)
        spi_patch_port = "spi-%s.%s" % (iface.name, in_vlan)
        cmd += " -- --if-exists del-port %s -- --if-exists del-port %s" % (spt_patch_port, spi_patch_port)
        tp_iface_info.remove_patch_port(spt_patch_port)

    if net_lib.Utility.cmd_execute(cmd)[0] != 0:
        return False

