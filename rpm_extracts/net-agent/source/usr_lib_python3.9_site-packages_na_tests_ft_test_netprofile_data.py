#!/usr/bin/python3
"""
Test qos strategy -- test netprofile basic behaviour
"""
import unittest
from na_tests.common.utility import Utility
from na_tests.common import testbase


class TestNetProfileData(testbase.TestCaseBase):

    def myinit(self):
        super(TestNetProfileData, self).myinit()
        self._start_ovs = True
        self._start_netagent = True

    def test_add_netprofile(self):
        ret = Utility.cmd_execute('''
cd {test_dir}
NETFILENAME="qos_test"
ADDR1=1.0.0.1
get_netfileudp() {{
    cat <<EOF
<netprofile name="$NETFILENAME" type="qos" uuid="245324" version="1">
<flow direction="out" dl_type="ip" nw_dst="$ADDR1/24" nw_proto="udp" priority="15">
<action burst="80000" domain="interface" id="4" rate="30000" unit="kbps" />
</flow>
<flow direction="in" dl_type="ip" nw_dst="$ADDR1/24" nw_proto="udp" priority="15">
<action burst="80000" domain="interface" id="5" rate="20000" unit="kbps" />
</flow>
</netprofile>
EOF
}}

na-ctl --retry=-1 netprofile define --content "$(get_netfileudp)"
if test $? -ne 0; then
    exit 1
fi
ls netprofile/qos > tmp.txt  # TODO: bad
grep $NETFILENAME.xml tmp.txt
if test $? -ne 0; then
    exit 2
fi

NETFILENAME="errored_qos_test"
get_errored_netfile() {{  # intentionally to construct is an error netprofile
    cat <<EOF
<netprofile name="$NETFILENAME" type="qos" uuid="245324" version="1">
<flow direction="outerror" dl_type="ip" nw_dst="$ADDR1/24" nw_proto="udp" priority="15">
<action burst="80000" domain="interface" id="4" rate="30000" unit="kbps" />
</flow>
<flow direction="inerror" dl_type="ip" nw_dst="$ADDR1/24" nw_proto="udp" priority="15">
<action burst="80000" domain="interface" id="5" rate="20000" unit="kbps" />
</flow>
</netprofile>
EOF
}}
na-ctl netprofile define --content "$(get_errored_netfile)"
if test $? -eq 0; then  # expected failed, but if return status code is OK, then exit 
    exit 2
fi
'''.format(**{'test_dir': self._test_dir}), True)
        self.assertEqual(ret[0], 0, "test add qos strategy failed: shell return code:%d" % ret[0])

    def test_del_netprofile(self):
        self.test_add_netprofile()
        ret = Utility.cmd_execute('''
cd {test_dir}
NETFILENAME="qos_test"
na-ctl netprofile undefine "$NETFILENAME"
ls netprofile/qos > tmp.txt  # TODO: bad
grep $NETFILENAME.xml tmp.txt
if test $? -eq 0; then
    exit 1
fi
NETFILENAME="qos_test_fake"
if na-ctl netprofile undefine "$NETFILENAME"; then  # this is a faked netprofile, undefine should failed
    exit 2  
fi
'''.format(**{'test_dir': self._test_dir}), True)
        self.assertEqual(ret[0], 0, "test del bridge failed: shell return code:%d" % ret[0])

    def test_mod_netprofile(self):
        ret = Utility.cmd_execute('''
cd {test_dir}
NETFILENAME="qos_test"
ADDR1=1.0.0.1
get_netfileudp() {{
    cat <<EOF
<netprofile name="$NETFILENAME" type="qos" uuid="245324" version="1">
<flow direction="out" dl_type="ip" nw_dst="$ADDR1/24" nw_proto="udp" priority="15">
<action burst="80000" domain="interface" id="4" rate="30000" unit="kbps" />
</flow>
<flow direction="in" dl_type="ip" nw_dst="$ADDR1/24" nw_proto="udp" priority="15">
<action burst="80000" domain="interface" id="5" rate="20000" unit="kbps" />
</flow>
</netprofile>
EOF
}}

na-ctl --retry=-1 netprofile define --content "$(get_netfileudp)"
if $? neq 0; then
    exit 1
fi

# modified address
ADDR1=1.0.0.4
get_netfileudp_mod() {{
    cat <<EOF
<netprofile name="$NETFILENAME" type="qos" uuid="245324" version="1">
<flow direction="out" dl_type="ip" nw_dst="$ADDR1/24" nw_proto="udp" priority="15">
<action burst="70000" domain="interface" id="4" rate="50000" unit="kbps" />
</flow>
<flow direction="in" dl_type="ip" nw_dst="$ADDR1/24" nw_proto="udp" priority="15">
<action burst="60000" domain="interface" id="5" rate="40000" unit="kbps" />
</flow>
</netprofile>
EOF
}}
na-ctl netprofile define --content "$(get_netfileudp_mod)"
if test $? -ne 0; then
    exit 1
fi

get_netfileudp_mod > netfileudp_mod.xml
if test diff --ignore-all-space netfileudp_mod.xml $NETFILENAME.xml; then
    exit 2
fi

NETFILENAME="errored_qos_test"
get_errored_netfile() {{  # intentionally to construct is an error netprofile
    cat <<EOF
<netprofile name="$NETFILENAME" type="qos" uuid="245324" version="1">
<flow direction="outerror" dl_type="ip" nw_dst="$ADDR1/24" nw_proto="udp" priority="15">
<action burst="80000" domain="interface" id="4" rate="30000" unit="kbps" />
</flow>
<flow direction="inerror" dl_type="ip" nw_dst="$ADDR1/24" nw_proto="udp" priority="15">
<action burst="80000" domain="interface" id="5" rate="20000" unit="kbps" />
</flow>
</netprofile>
EOF
}}
na-ctl netprofile define --content "$(get_errored_netfile)"
if test $? -eq 0; then  # expected failed, but if return status code is OK, then exit 
    exit 3
fi
'''.format(**{'test_dir': self._test_dir}), True)
        self.assertEqual(ret[0], 0, "test mod netprofile failed: shell return code:%d" % ret[0])

    def test_view_netprofile(self):
        ret = Utility.cmd_execute('''
cd {test_dir}
NETFILENAME="qos_test"
ADDR1=1.0.0.1
get_netfileudp() {{
    cat <<EOF
<netprofile name="$NETFILENAME" type="qos" uuid="245324" version="1">
<flow direction="out" dl_type="ip" nw_dst="$ADDR1/24" nw_proto="udp" priority="15">
<action burst="80000" domain="interface" id="4" rate="30000" unit="kbps" />
</flow>
<flow direction="in" dl_type="ip" nw_dst="$ADDR1/24" nw_proto="udp" priority="15">
<action burst="80000" domain="interface" id="5" rate="20000" unit="kbps" />
</flow>
</netprofile>

EOF
}}

na-ctl --retry=-1 netprofile define --content "$(get_netfileudp)"
if test $? -ne 0; then
    exit 1
fi
na-ctl netprofile dump $NETFILENAME >netprofile_dumped.xml
if test $? -ne 0; then
    exit 11
fi
diff --ignore-all-space --ignore-blank-lines netprofile_dumped.xml {test_dir}/netprofile/qos/$NETFILENAME.xml
if test $? -ne 0; then
    exit 22
fi
# check the dump content
listrt=$(na-ctl netprofile list)
if test $? -ne 0; then
    exit 2
fi
if X"$listrt" != X$NETFILENAME; then
    exit 3
fi
'''.format(**{'test_dir': self._test_dir}), True)
        self.assertEqual(ret[0], 0, "test view netprofile: shell return code:%d" % ret[0])


if __name__ == '__main__':
    unittest.main()
