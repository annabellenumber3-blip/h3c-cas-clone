#!/usr/bin/python3

import unittest
from net_agent.base import net_lib

class TestNetLib(unittest.TestCase):

    # test ValidatyCheck class
    def test_validatycheck_class(self):
        validaty_check = net_lib.ValidatyCheck()

        self.assertEqual(validaty_check.check_int(0, 1, 2), False)
        self.assertEqual(validaty_check.check_int(1, 1, 2), True)
        self.assertEqual(validaty_check.check_int(1, 1, 2), True)
        self.assertEqual(validaty_check.check_mac("aa:bb:cc:dd:ee:ff"), True)
        self.assertEqual(validaty_check.check_mac("aa:bb:cc:dd:EE:ff"), True)
        self.assertEqual(validaty_check.check_mac("aa:bb:cc:dd:ee:ff:gg"), False)
        self.assertEqual(validaty_check.check_mac("3x:bb:cc:dd:ee:ff:gg"), False)
        self.assertEqual(validaty_check.check_ip("1.2.3.4", False), True)
        self.assertEqual(validaty_check.check_ip("1.2.333.4", False), False)
        self.assertEqual(validaty_check.check_ipv6("2003:ddff::1"), True)
        self.assertEqual(validaty_check.check_ipv6(":::1"), False)
        self.assertEqual(validaty_check.check_ipv6("1:::2"), False)
        self.assertEqual(validaty_check.check_ipv6("1:2:3:4:5:6:7:8"), True)
        self.assertEqual(validaty_check.check_ipv6("1:2:3:4::83453"), False)

    # test GlobalConfig class
    def test_globalconfig_class(self):
        global_config = net_lib.GlobalConfig()
        global_config.reload()
        self.assertEqual(global_config.enable_state, 1)
        self.assertEqual(global_config.enable_xnormal, 1)
        self.assertEqual(global_config.enable_l2_acl, 1)
        self.assertEqual(global_config.enable_bind_mac, 1)
        self.assertEqual(global_config.enable_meter, 1)

if __name__ == '__main__':
    unittest.main()