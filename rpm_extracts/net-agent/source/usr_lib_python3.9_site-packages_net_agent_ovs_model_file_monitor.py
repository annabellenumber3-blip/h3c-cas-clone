#!/usr/bin/python3

import logging
import pyinotify
from .. base import net_lib


class FileMonitor(object):

    def __init__(self, event_handler):
        super(FileMonitor, self).__init__()

        wm = pyinotify.WatchManager()
        watching_events = pyinotify.IN_MOVED_TO | pyinotify.IN_DELETE
        wm.add_watch(
            net_lib.OPENVSWITCH_RUN_DIR,
            watching_events,
            auto_add=True,
            rec=True)

        self._monitor_thread = pyinotify.ThreadedNotifier(wm, event_handler)
        self._started = False

    def start(self):
        self._monitor_thread.start()
        self._started = True

    def stop(self):
        logging.info("filemonitor thread pre-stop: %s", self._monitor_thread.is_alive())
        if self._started:
            self._monitor_thread.stop()
            self._monitor_thread.join()
            self._started = False
        logging.info("filemonitor thread post-join: %s", self._monitor_thread.is_alive())
