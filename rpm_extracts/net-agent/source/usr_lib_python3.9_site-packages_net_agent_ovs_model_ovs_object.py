FLOW_REG_BIT_INDEX_BINDIP = (4, 0)
FLOW_REG_BIT_INDEX_BINDMAC = (4, 1)
FLOW_REG_BIT_INDEX_MULTIPLE_OUTPORT = (0, 16)
FLOW_REG_MASK_MULTIPLE_OUTPORT = 1 << FLOW_REG_BIT_INDEX_MULTIPLE_OUTPORT[1]

FLOW_XNORMAL_FLAG_GOTO_OUTPORT_TABLE = 0

