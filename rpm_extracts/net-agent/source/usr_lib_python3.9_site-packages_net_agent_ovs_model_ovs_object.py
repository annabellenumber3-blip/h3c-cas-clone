class FlowMatchField(object):

    def __init__(self, in_port=None, out_port=None):
        super(FlowMatchField, self).__init__()

        self.in_port = in_port
        self.out_port = out_port

        self.dl_type = None
        self.nw_proto = None
        self.dl_vlan = None

        self.dl_src = None
        self.dl_src_mask = FLOW_MASK_ALL
        self.dl_dst = None
        self.dl_dst_mask = FLOW_MASK_ALL

        self.nw_src = None
        self.nw_src_mask = FLOW_MASK_ALL
        self.nw_dst = None
        self.nw_dst_mask = FLOW_MASK_ALL

        self.ipv6_src = None
        self.ipv6_src_mask = FLOW_MASK_ALL
        self.ipv6_dst = None
        self.ipv6_dst_mask = FLOW_MASK_ALL

        self.tp_src = None
        self.tp_src_mask = FLOW_MASK_ALL
        self.tp_dst = None
        self.tp_dst_mask = FLOW_MASK_ALL

        self.icmp_type = None
        self.icmp_type_mask = FLOW_MASK_ALL
        self.icmp_code = None
        self.icmp_code_mask = FLOW_MASK_ALL

        self.arp_op = None
        self.tcp_flags = None
        self.nd_target = None
        self.ct_state = None
        self.ct_mark = None

        self.reg_bind_ip = None
        self.is_bind_mac = False
        self.reg_multiple_outport = None

    def set_fields(self, **kw_args):
        for k, v in kw_args.items():
            if hasattr(self, k):
                setattr(self, k, v)

    def complete(self):
        self.dl_type = {
            "arp": "0x0806",
            "ip": "0x0800",
            "rarp": "0x8035",
            "ipv6": "0x86dd"
        }.get(self.dl_type, self.dl_type)
        self.nw_proto = {
            "udp": "17",
            "tcp": "6",
            "icmp": "1",
            "icmp6": "58"
        }.get(self.nw_proto, self.nw_proto)

        if self.dl_type is None and (self.nw_proto is not None
                                     or self.nw_src is not None
                                     or self.nw_dst is not None):
            self.dl_type = "0x0800" if self.nw_proto != "58" else "0x86dd"
            logging.warn("dl_type is None while nw_proto: %s, nw_src: %s, nw_dst: %s, set dl_type to %s",
                         self.nw_proto, self.nw_src, self.nw_dst, self.dl_type)

    def clone(self):
        return copy.copy(self)

    def reverse(self):
        self.in_port, self.out_port = self.out_port, self.in_port

        self.dl_src, self.dl_src_mask, self.dl_dst, self.dl_dst_mask = \
            self.dl_dst, self.dl_dst_mask, self.dl_src, self.dl_src_mask

        self.nw_src, self.nw_src_mask, self.nw_dst, self.nw_dst_mask = \
            self.nw_dst, self.nw_dst_mask, self.nw_src, self.nw_src_mask

        self.ipv6_src, self.ipv6_src_mask, self.ipv6_dst, self.ipv6_dst_mask = \
            self.ipv6_dst, self.ipv6_dst_mask, self.ipv6_src, self.ipv6_src_mask

        self.tp_src, self.tp_src_mask, self.tp_dst, self.tp_dst_mask = \
            self.tp_dst, self.tp_dst_mask, self.tp_src, self.tp_src_mask

    def format_xml(self, flow):
        for k, v in vars(self).items():
            if v and v is not FLOW_MASK_ALL:
                flow.attrib[k] = str(v)
        if "dl_type" in flow.attrib.keys():
            flow.attrib["dl_type"] = {
                    "0x0806": "arp",
                    "0x0800": "ip",
                    "0x8035": "rarp",
                    "0x86dd": "ipv6"
                    }.get(flow.attrib["dl_type"], flow.attrib["dl_type"])
        if "nw_proto" in flow.attrib.keys():
            flow.attrib["nw_proto"] = {
                "17": "udp",
                "6": "tcp",
                "1": "icmp",
                "58": "icmp6"
                }.get(flow.attrib["nw_proto"], flow.attrib["nw_proto"])

        def convert_to_field_and_mask_format(k, k_mask):
            if k_mask in flow.attrib.keys():
                flow.attrib[k] = "%s/%s" % (flow.attrib[k], flow.attrib[k_mask])
                flow.attrib.pop(k_mask, None)

        convert_to_field_and_mask_format("dl_src", "dl_src_mask")
        convert_to_field_and_mask_format("dl_dst", "dl_dst_mask")
        convert_to_field_and_mask_format("nw_src", "nw_src_mask")
        convert_to_field_and_mask_format("nw_dst", "nw_dst_mask")
        convert_to_field_and_mask_format("ipv6_src", "ipv6_src_mask")
        convert_to_field_and_mask_format("ipv6_dst", "ipv6_dst_mask")
        convert_to_field_and_mask_format("tp_src", "tp_src_mask")
        convert_to_field_and_mask_format("tp_dst", "tp_dst_mask")
        convert_to_field_and_mask_format("icmp_type", "icmp_type_mask")
        convert_to_field_and_mask_format("icmp_code", "icmp_code_mask")

    def __field_to_string(self, field_name, field, field_mask):
        if field is None or field_mask == 0:
            return ""
        elif field_mask == FLOW_MASK_ALL:
            return "%s=%s," % (field_name, field)
        else:
            return "%s=%s/%s," % (field_name, field, field_mask)

    def __to_string(self):
        self.complete()
        in_port = "LOCAL" if self.in_port == OVS_OFPP_LOCAL else self.in_port
        reg_index_vec = FLOW_REG_BIT_INDEX_BINDMAC if self.is_bind_mac else FLOW_REG_BIT_INDEX_BINDIP
        res = ""
        res += self.__field_to_string("in_port", in_port, FLOW_MASK_ALL)
        res += self.__field_to_string("reg%d" % FLOW_REG_INDEX_OUTPORT, self.out_port, 0xffff)
        res += self.__field_to_string("reg%d" % reg_index_vec[0], self.reg_bind_ip, 1 << reg_index_vec[1])
        res += self.__field_to_string("reg%d" % FLOW_REG_BIT_INDEX_MULTIPLE_OUTPORT[0], self.reg_multiple_outport, FLOW_REG_MASK_MULTIPLE_OUTPORT)
        res += self.__field_to_string("dl_type", self.dl_type, FLOW_MASK_ALL)
        res += self.__field_to_string("dl_vlan", self.dl_vlan, FLOW_MASK_ALL)
        res += self.__field_to_string("dl_src", self.dl_src, self.dl_src_mask)
        res += self.__field_to_string("dl_dst", self.dl_dst, self.dl_dst_mask)
        res += self.__field_to_string("nw_src", self.nw_src, self.nw_src_mask)
        res += self.__field_to_string("nw_dst", self.nw_dst, self.nw_dst_mask)
        res += self.__field_to_string("ipv6_src", self.ipv6_src, self.ipv6_src_mask)
        res += self.__field_to_string("ipv6_dst", self.ipv6_dst, self.ipv6_dst_mask)
        res += self.__field_to_string("tp_src", self.tp_src, self.tp_src_mask)
        res += self.__field_to_string("tp_dst", self.tp_dst, self.tp_dst_mask)
        res += self.__field_to_string("icmp_type", self.icmp_type, self.icmp_type_mask)
        res += self.__field_to_string("icmp_code", self.icmp_code, self.icmp_code_mask)
        res += self.__field_to_string("nw_proto", self.nw_proto, FLOW_MASK_ALL)
        res += self.__field_to_string("arp_op", self.arp_op, FLOW_MASK_ALL)
        res += self.__field_to_string("tcp_flags", self.tcp_flags, FLOW_MASK_ALL)
        res += self.__field_to_string("nd_target", self.nd_target, FLOW_MASK_ALL)
        res += self.__field_to_string("ct_state", self.ct_state, FLOW_MASK_ALL)
        res += self.__field_to_string("ct_mark", self.ct_mark, FLOW_MASK_ALL)
        return res

    def __str__(self):
        return self.__to_string()


class Flow(object):

    def __init__(self,
                 bridge=None,
                 cookie=None,
                 table=None,
                 priority=None,
                 matchfield=None,
                 actions=None,
                 **kw_args):
        super(Flow, self).__init__()
        self.bridge = bridge
        self.cookie = cookie
        self.table = table
        self.priority = priority

        self.matchfield = matchfield if matchfield else FlowMatchField()
        self.matchfield.set_fields(**kw_args)

        self.actions = actions

    def clone(self):
        return copy.deepcopy(self)

    def __str__(self):
        res = ""
        if self.priority is not None:
            res += "priority=%s," % self.priority
        if self.cookie is not None:
            res += "cookie=%s," % self.cookie
        if self.table is not None:
            res += "table=%s," % self.table
        if self.matchfield is not None:
            res += str(self.matchfield)
        if self.actions is not None:
            res += "actions=%s" % self.actions
        return res


class OVSAction(object):

    def __init__(self, actions=""):
        super(OVSAction, self).__init__()
        self._actions = actions

    def add_actions(self, action):
        self._actions += "," + action.get_actions()
        return self

    def add_set_reg(self, value, index, range_=""):
        self._actions += ",load:%s->NXM_NX_REG%d[%s]" % (value, index, range_)
        return self

    def add_set_bitmap(self, reg):
        return self.add_set_reg(1, reg[0], str(reg[1]))

    def add_ct_clear(self):
        self._actions += ",ct_clear"
        return self

    def add_normal(self):
        self._actions += ",normal"
        return self

    def add_resubmit(self, in_port, table_id):
        self._actions += ",resubmit(%s,%s)" % (in_port if in_port else "", table_id)
        return self

    def add_goto_table(self, table_id):
        self._actions += ",goto_table:%s" % (table_id)
        return self

    def add_output_reg(self, index, range_=""):
        self._actions += ",output:NXM_NX_REG%d[%s]" % (index, range_)
        return self

    def add_xnormal(self):
        return self._add_xnormal([FLOW_XNORMAL_FLAG_GOTO_OUTPORT_TABLE])

    def _add_xnormal(self, flags=None):
        flags = flags if flags is not None else []
        flag_value = reduce(lambda p, c: p + (1 << c), flags, 0)
        self.add_set_reg(flag_value, FLOW_REG_INDEX_NORMAL_FLAG)
        self.add_normal()
        self.add_set_reg(0, FLOW_REG_INDEX_NORMAL_FLAG)
        return self

    def add_output_outport(self):
        return self.add_output_reg(FLOW_REG_INDEX_OUTPORT, "0..15")

    def add_set_meter(self, meter_id):
        self._actions += ",meter:%d" % int(meter_id)
        return self

    def add_vlan(self, vlan, pop=False):
        self._actions += ",%spush_vlan:0x8100,set_vlan_vid:%d" % ("pop_vlan," if pop else "", vlan)
        return self
    
    def mod_vlan(self, vlan):
        if self._actions:
            self._actions += ",mod_vlan_vid:%d" % vlan
        else:
            self._actions += "mod_vlan_vid:%d" % vlan
        return self

    def add_set_output_reg(self, port):
        return self.add_set_reg(port, FLOW_REG_INDEX_OUTPORT)

    def add_loutput(self, port, vlan):
        self._actions += ",load:1->NXM_NX_REG0[],load:%s->NXM_NX_REG2[],%s,load:0->NXM_NX_REG0[],load:0->NXM_NX_REG2[]" % (vlan, port)
        return self

    def get_actions(self):
        return self._actions.lstrip(",")

    def __str__(self):
        if self._actions:
            return self._actions.lstrip(",")
        else:
            return "controller(id=2022)" if net_lib.Utility.is_log_flow_drop() else "drop"


