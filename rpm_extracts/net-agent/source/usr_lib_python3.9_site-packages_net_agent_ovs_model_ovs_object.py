class ACLRule(object):

    def __init__(self):
        super(ACLRule, self).__init__()
        self.action = OVSACTION_DROP
        self.direction = DIRECTION_IN
        self.priority = 0
        self.statematch = False
        self.matchfield = FlowMatchField()

    def __str__(self):
        return "action=%s, dir=%s, priority=%d, statematch=%s, match: %s" % (
            self.action, self.direction, self.priority, str(self.statematch).lower(), self.matchfield)


class ACL(object):

    def __init__(self, xml="", md5=""):
        super(ACL, self).__init__()
        self.name = ""
        self.md5 = md5
        self.aclrules = []
        if xml:
            try:
                self.parse_from_xml(xml)
            except:
                logging.exception("parse %s file exception", xml)

    @staticmethod
    def _parse_nw_proto_from_xml(xml_nw_proto):
        xml_nw_proto = {
                "tcp": "6",
                "udp": "17",
                "udplite": "136",
                "esp": "50",
                "ah": "51",
                "icmp": "1",
                "icmpv6": "58",
                "igmp": "2",
                "sctp": "132"
            }.get(xml_nw_proto, xml_nw_proto)
        return xml_nw_proto

    def parse_from_xml(self, xml):
        if xml == "":
            return

        root = ET.fromstring(xml)
        self.name = root.attrib.get('name', "")
        for child in root:
            if child.tag != 'rule':
                continue
            rule = ACLRule()

            if [k for k in ["action", "direction", "priority"] if k not in child.attrib.keys()]:
                logging.error("acl rule is incomplete: %s", child.attrib)

            # The translation almost thinks all fields as string except fewer exceptions
            rule.action = child.attrib.get('action', OVSACTION_DROP)
            rule.direction = child.attrib.get('direction', DIRECTION_IN)
            priority = child.attrib.get('priority', "0")
            rule.priority = int(priority) if priority.isdigit() else 0
            rule.statematch = 'false' != child.attrib.get('statematch', 'false')

            for item in child:
                if item.tag == 'all':
                    rule.matchfield.dl_type = 'ip'
                elif item.tag == 'all-ipv6':
                    rule.matchfield.dl_type = 'ipv6'
                elif item.tag in ('tcp', 'udp', 'icmp'):
                    rule.matchfield.nw_proto = item.tag
                    rule.matchfield.dl_type = 'ip'
                elif item.tag == 'tcp-ipv6':
                    rule.matchfield.nw_proto = 'tcp'
                    rule.matchfield.dl_type = 'ipv6'
                elif item.tag == 'udp-ipv6':
                    rule.matchfield.nw_proto = 'udp'
                    rule.matchfield.dl_type = 'ipv6'
                elif item.tag == 'icmpv6':
                    rule.matchfield.nw_proto = 'icmp6'
                    rule.matchfield.dl_type = 'ipv6'
                elif item.tag == 'ip':
                    rule.matchfield.nw_proto = self._parse_nw_proto_from_xml(item.attrib.get('protocol', None))
                    rule.matchfield.dl_type = 'ip'
                elif item.tag == 'ipv6':
                    rule.matchfield.nw_proto = self._parse_nw_proto_from_xml(item.attrib.get('protocol', None))
                    rule.matchfield.dl_type = 'ipv6'
                elif item.tag == 'mac':
                    rule.matchfield.dl_type = item.attrib.get('protocolid', None)
                    rule.matchfield.dl_type = 'ip' if rule.matchfield.dl_type == 'ipv4' else rule.matchfield.dl_type
                else:
                    logging.debug("tag:%s is unsupported", item.tag)
                    continue

                # Layer 2
                rule.matchfield.dl_src = item.attrib.get('srcmacaddr', None)
                rule.matchfield.dl_src_mask = item.attrib.get('srcmacmask', None)
                rule.matchfield.dl_dst = item.attrib.get('dstmacaddr', None)
                rule.matchfield.dl_dst_mask = item.attrib.get('dstmacmask', None)

                # Layer 3
                if rule.matchfield.dl_type == 'ip':
                    rule.matchfield.nw_src = item.attrib.get('srcipaddr', None)
                    rule.matchfield.nw_src_mask = item.attrib.get('srcipmask', None)
                    rule.matchfield.nw_dst = item.attrib.get('dstipaddr', None)
                    rule.matchfield.nw_dst_mask = item.attrib.get('dstipmask', None)
                elif rule.matchfield.dl_type == 'ipv6':
                    rule.matchfield.ipv6_src = item.attrib.get('srcipaddr', None)
                    rule.matchfield.ipv6_src_mask = item.attrib.get('srcipmask', None)
                    rule.matchfield.ipv6_dst = item.attrib.get('dstipaddr', None)
                    rule.matchfield.ipv6_dst_mask = item.attrib.get('dstipmask', None)

                if rule.matchfield.nw_proto == 'icmp' or rule.matchfield.nw_proto == 'icmp6':
                    rule.matchfield.icmp_type = item.attrib.get('type', None)
                    rule.matchfield.icmp_code = item.attrib.get('code', None)

                # Layer 4
                rule.matchfield.tp_src = item.attrib.get('srcportstart', None)
                rule.matchfield.tp_dst = item.attrib.get('dstportstart', None)
                # :) blame me to borrow tp_xxx_mask as xxxportend
                rule.matchfield.tp_src_mask = item.attrib.get('srcportend', FLOW_MASK_ALL)
                rule.matchfield.tp_dst_mask = item.attrib.get('dstportend', FLOW_MASK_ALL)
                # Fields are not assigned: in_port out_port arp_op tcp_flags
            self.aclrules.append(rule)
        self.aclrules.sort(key=lambda r: -r.priority)
