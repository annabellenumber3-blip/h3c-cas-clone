BRIDGE_INVALID_NAME = "%"  # magic bridge name for interface who can't find its bridge


class OVSDb(object):

    def __init__(self):
        super(OVSDb, self).__init__()

    @staticmethod
    def get_bridge_by_iface(ifname):
        brname = net_lib.Utility.cmd_execute(
            "ovs-vsctl --verbose=db_ctl_base:syslog:off -t 5 iface-to-br %s" % ifname,
                shell=True, log_err=False)[1].strip()
        if not brname:
            # It may be a local interface if iface-to-br fails
            if net_lib.Utility.cmd_execute("ovs-vsctl -t 5 br-exists %s" % ifname, shell=True)[0] == 0:
                brname = ifname
            else:
                brname = BRIDGE_INVALID_NAME
                logging.warn("Failed to get bridge name of iface %s", ifname)
        return brname

    @staticmethod
    def get_ofport_by_iface(ifname):
        ofport = net_lib.Utility.cmd_execute(
            "ovs-vsctl --verbose=db_ctl_base:syslog:off -t 5 get Interface %s ofport" % ifname,
                shell=True, log_err=False)[1].strip()
        ret = None
        if ofport.isdigit():
            ret = int(ofport)
        return ret

    @staticmethod
    def get_bridges():
        return net_lib.Utility.cmd_output_lines("ovs-vsctl -t 5 list-br")

    @staticmethod
    def bridge_exists(brname):
        return net_lib.Utility.cmd_call(["ovs-vsctl", "br-exists", brname]) == 0

    @staticmethod
    def get_bridge_network_type(brname):
        network_type = net_lib.Utility.cmd_execute("ovs-vsctl --if-exists get bridge %s other_config:network_type" % (brname))[1].strip('\n"')
        return int(network_type) if network_type else 0

    @staticmethod
    def list(table, columns, row=""):
        res = net_lib.Utility.cmd_output_string("ovs-vsctl --verbose=db_ctl_base:syslog:off --no-heading --format json --column=%s list %s %s" % (",".join(columns), table, row))
        try:
            res = json.loads(res).get("data", "")
        except:
            logging.exception("exception when parse res %s, returned by list %s %s %s", res, table, row, columns)
            res = ""
        return res

    @staticmethod
    def val_to_py(val):
        """Convert a json ovsdb return value to native python object"""
        if isinstance(val, collections.Sequence) and len(val) == 2:
            if val[0] == "uuid":
                return str(val[1])
            elif val[0] == "set":
                return [OVSDb.val_to_py(x) for x in val[1]]
            elif val[0] == "map":
                return {OVSDb.val_to_py(x): OVSDb.val_to_py(y) for x, y in val[1]}
        return val

    @staticmethod
    def wait_vswitch_bridge(brname, timeout=5):
        N = 10
        sleep_time = timeout * 1.0 / N
        return net_lib.Utility.wait_util(lambda: OVSDb.bridge_connectable(brname), N, sleep_time)

    @staticmethod
    def wait_ovsdb(timeout=5):
        N = 10
        sleep_time = timeout * 1.0 / N
        func = lambda: net_lib.Utility.cmd_call("ovs-vsctl -t 1 list Open_vswitch .") == 0
        return net_lib.Utility.wait_util(func, N, sleep_time)

    @staticmethod
    def bridge_connectable(brname):
        return net_lib.Utility.cmd_call("ovs-ofctl -t 5 probe " + brname) == 0


