class OVSDocument(object):

    def __init__(self):
        super(OVSDocument, self).__init__()
        self._dm = AgentDataManager()

        self.ovs_type = OVSDocument._load_last_flow_version()
        self.xnormal_version = 0
        self._flow_manager = None
        self._reconfigure_flow_manager()
        self._init_drain_bypass()

        self._flow_businesses = flow_business.get_all_flow_businesses(self._dm)

    def close(self):
        self._dm.close()
        self.ovs_type = "", ""
        self.xnormal_version = 0
        self._flow_manager = None

        self._flow_businesses = []

    def get_ovsdb_running(self):
        return self._is_ovs_daemon_running("ovsdb-server")

    def get_vswitchd_running(self):
        return self._is_ovs_daemon_running("ovs-vswitchd")

    def verify_ovs_version(self):
        ofver = net_lib.Utility.cmd_execute("ovs-ofctl --version | grep 'Open vSwitch'", True)
        ovsver = net_lib.Utility.cmd_execute("ovs-appctl version | grep 'Open vSwitch'", True)
        return ofver[0] == 0 and ovsver[0] == 0 and ofver[1].split(' ')[-1] == ovsver[1].split(' ')[-1]

    def _is_ovs_daemon_running(self, target):
        return net_lib.Utility.cmd_call(["ovs-appctl", "--timeout=2", "-t", target, "version"]) == 0

    @staticmethod
    def _load_last_flow_version():
        v = ["", ""]
        if os.path.isfile(_NET_AGENT_FLOW_VERSION_FILE):
            conts = net_lib.Utility.read_file(_NET_AGENT_FLOW_VERSION_FILE).strip().splitlines()
            for line in conts:
                if line.startswith("ovs-type"):
                    v[0] = line.split('=', 1)[-1]
                if line.startswith("ovs-version"):
                    v[1] = line.split('=', 1)[-1]
        return v[0], v[1]

    @staticmethod
    def _save_flow_version(ovs_type_, ovs_version_, xnormal_version_):
        with open(_NET_AGENT_FLOW_VERSION_FILE, "w") as f:
            f.write("ovs-type=%s\n" % ovs_type_)
            f.write("ovs-version=%s\n" % ovs_version_)
            f.write("xnormal-version=%s\n" % xnormal_version_)

    @staticmethod
    def _get_internal_version():
        ovs_internal = {"ovs_type": "open",
                        "xnormal_version": 0}
        res = net_lib.Utility.cmd_execute("ovs-appctl -T 10 show-internal-version")[1]
        if res:
            lines = res.splitlines()
            for line in lines:
                if line.startswith("feature-xnormal"):
                    xnormal_ver = line.split(":")[-1]
                    ovs_internal["xnormal_version"] = float(xnormal_ver) if xnormal_ver.isdigit() else xnormal_ver
                    break
            if 0 != ovs_internal.get("xnormal_version"):
                logging.info("the xnormal version is: %s", ovs_internal.get("xnormal_version"))
                ovs_internal["ovs_type"] = "cas" if res.startswith("cas,") else "open"
            else:
                logging.warn("set ovs type to open, as ovs does not support xnormal: %s ", res.strip())
        return ovs_internal

    def _reconfigure_flow_manager(self):
        """ reconfigure flow manager and clear old flows if ovs type change or agent flow manger upgrade
        """
        ovs_internal = OVSDocument._get_internal_version()
        ovs_type = ovs_internal.get("ovs_type", "open")
        self.xnormal_version = ovs_internal.get("xnormal_version", 0)
        current_flow_manager = ovs_flow_manager.get_flow_manager(ovs_type)
        old_type, old_version = self.ovs_type
        current_type, current_version = current_flow_manager.get_type_version()

        logging.info("reconfigure flow manger: (%s, %s) => (%s, %s)",
                     old_type, old_version, current_type, current_version)

        clear_flow_manager = self._flow_manager if self._flow_manager else current_flow_manager
        if (old_type or old_version) and (current_type != old_type or current_version != old_version):
            with auto_commit_flow(clear_flow_manager):
                for br in self._dm.get_all_bridges():
                    clear_flow_manager.clear_all(br)

        self.ovs_type = current_type, current_version
        self._flow_manager = current_flow_manager
        OVSDocument._save_flow_version(current_type, current_version, self.xnormal_version)

    def _init_drain_bypass(self):
        self._dm.reset_drain_bypass_info()
        cmd = "ovs-vsctl --if-exists get open_vswitch . other_config:drain_bypass"
        bypass_state = net_lib.Utility.cmd_output_string(cmd).strip()
        if bypass_state == "True":
            bypass_state = True
            logging.warn("drain bypass is enabled.")
        else:
            bypass_state = False
        self._dm.update_drain_bypass_state(bypass_state)

    def load_tp_iface_info(self, iface):
        if self._dm.get_tp_iface_info(iface):
            return

        tbr_br = "tbr-%s" % iface.name
        if iface.br_name != tbr_br:
            return

        origin_br = None
        info = None
        cmd = "ovs-vsctl list-ifaces %s" % tbr_br
        br_ifaces = net_lib.Utility.cmd_execute(cmd)[1].split()
        for br_iface in br_ifaces:
            if not br_iface.startswith('spt-%s' % iface.name):
                continue
            cmd = "ovs-vsctl --if-exists get interface %s option:peer" % br_iface
            spi_patch_port = net_lib.Utility.cmd_execute(cmd)[1].strip()
            if spi_patch_port and origin_br is None:
                cmd = "ovs-vsctl iface-to-br %s" % spi_patch_port
                origin_br = net_lib.Utility.cmd_execute(cmd)[1].strip()
                if not origin_br:
                    logging.error("can't get tp iface %s origin_br" % iface.name)
                    return
                info = TrunkPortIfaceInfo(origin_br)
            if info is not None:
                info.add_patch_port(br_iface, spi_patch_port)
        self._dm.update_tp_iface_info(iface, info)
        logging.debug("load tp iface %s info: %s", iface.name, self._dm.get_tp_iface_info(iface))

    def load_bridges(self, brname=""):
        bridges = []
        for br_data in OVSDb.list("Bridge", ["name", "other_config"], brname):
            br_mode = OVSDb.val_to_py(br_data[1]).get("br_mode", "0")
            bridges.append(Bridge(
                name=br_data[0],
                br_mode=int(br_mode) if br_mode.isdigit() else 0,
                exist=True))
        if not brname:
            self._dm.reset_active_bridge(bridges)
        elif bridges:
            self._dm.add_active_bridge(bridges[-1])
        else:
            logging.warn("bridge: %s does not exist, please check", brname)

