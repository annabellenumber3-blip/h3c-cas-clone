def isNicQosConfigured(bridge):
    speed = ""

    if not bridge:
        return False

    netcfg_files = glob.glob("%s/*" % LINUX_SUBNET_PATH)
    for f in netcfg_files:
        if f.endswith('.old'):
            continue
        ret = net_lib.Utility.cmd_execute("cat %s | grep ^vswitch | grep -w %s" % (f, bridge), shell=True)[0]
        if ret == 0:
            return True

    speed = net_lib.Utility.cmd_execute("ovs-vsctl -t 2 --if-exists get Bridge %s other_config:speed" % bridge, shell=True)[1].strip("\n")

    return True if speed else False

def getLinuxEngineBridgeByNic(nic):
    if not nic:
        return None

    if os.path.exists("%s/%s" % (LINUX_NETWORK_PATH, nic)):
        return nic

    netcfg_files = glob.glob("%s/*" % LINUX_NETWORK_PATH)
    for f in netcfg_files:
        if f.endswith('.old'):
            continue
        if not os.path.exists("/sys/class/net/%s/bonding" % nic):
            ret = net_lib.Utility.cmd_execute("cat %s | grep ^uplinks | grep -w %s" % (f, nic), shell=True, log_err=False)[0]
        else:
            ret = net_lib.Utility.cmd_execute("cat %s | grep ^bond_name | grep -w %s" % (f, nic), shell=True, log_err=False)[0]
        if ret == 0:
            return os.path.basename(f)
    return None

def getActiveUplinkOfBridge(bridge):
    ifaces = []

    netcfg = "%s/%s" % (LINUX_NETWORK_PATH, bridge)
    if os.path.exists(netcfg):
        uplinks = net_lib.Utility.cmd_execute("cat %s | grep ^uplinks=" % netcfg, shell=True)[1].split('=')[1]
        ifaces.extend(uplinks.strip().split())
        return ifaces

    for iface in net_lib.Utility.cmd_execute("ovs-vsctl -t 3 --verbose=db_ctl_base:syslog:off list-ifaces %s" % bridge)[1].splitlines():
        if isActiveNic(iface):
            ifaces.append(iface)
    return ifaces


