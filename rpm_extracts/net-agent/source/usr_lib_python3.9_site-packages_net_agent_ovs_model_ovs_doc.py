    # Refresh flows that involves some bridges and interfaces. Bridge contains
    # all its interfaces. This function handle the intersection properly.
    def _revalidate_flows(self,
                          bridges,
                          interfaces,
                          include_base=True,
                          remove_old=True):
        bridge_names = set([b.name for b in bridges if (not self._check_int_bridge(b.name))])
        logging.info("revalidate br:%s, ifaces:%s, include_base:%s, remove_old:%s",
                     bridge_names, interfaces, include_base, remove_old)
        br_ifaces = self._get_classified_interfaces()

        res = 'ok'

        def for_each_do(collections, do):
            for item in collections:
                do(item)

        # if interfaces present it must be in bridges
        if bridges:
            if include_base:
                for_each_do(bridge_names, self._teardown_bridge_base_flows)
                for_each_do(bridge_names, self._setup_bridge_base_flows)

            if interfaces:
                for_each_do(interfaces, self._teardown_iface_flows)
                for_each_do(interfaces, self._setup_iface_flows)
                bridge_names = set([i.br_name for i in interfaces])
            else:
                for brname in bridge_names:
                    for_each_do(br_ifaces.get(brname, []), self._teardown_iface_flows)
                for brname in bridge_names:
                    for_each_do(br_ifaces.get(brname, []), self._setup_iface_flows)
            res = 'revalidate all iface flow for bridges:%s ok' % bridge_names
        else:
            if interfaces:
                for_each_do(interfaces, self._teardown_iface_flows)
                for_each_do(interfaces, self._setup_iface_flows)
                bridge_names = set([i.br_name for i in interfaces])
            res = 'revalidate flow for iface:%s ok' % interfaces

        # not forget raw flows
        net_lib.GlobalConfig().reload()
        for brname in bridge_names:
            br = self._dm.get_bridge_by_name(brname)
            if br:
                self._flow_manager.install_raw_flows(br)

        return res

    def update_acl(self, nwfilter):
        res = 'ok'
        contents = []
        file_name = "%s/%s.xml" % (NWFILTER_DIR, nwfilter)

        def read_nwfilter_file():
            content = net_lib.Utility.read_file(file_name)
            if content:
                contents.append(content)
            return True if content else False

        if not net_lib.Utility.wait_util(read_nwfilter_file, 3, 0.5):
            res = "failed to read %s" % file_name
            return res

        md5 = net_lib.Utility.calculate_md5(contents[-1])
        old_acl = self._dm.get_nwfilter_by_name(nwfilter)
        if old_acl is not None and old_acl.md5 == md5:
            logging.debug("new acl is same with old acl %s, md5=%s, not deliver flow", nwfilter, md5)
        else:
            new_acl = ACL(contents[-1], md5)
            self._dm.update_nwfilter(new_acl)
            logging.debug("update_acl %s deliver flow", new_acl.name)
            for iface in self._dm.get_all_interfaces():
                if iface.nwfilter == new_acl.name:
                    br_name = self._dm.get_bridge_by_name(iface.br_name)
                    self._flow_manager.del_acl(br_name, iface)
                    if not iface.bypass:
                        self._flow_manager.apply_acl(br_name, iface, new_acl.aclrules, 0, False, True)
        return res

    def del_acl(self, acl_name):
        for iface in self._dm.get_all_interfaces():
            if iface.nwfilter == acl_name:
                logging.error("This should not happen. iface %s is still using acl %s", iface, iface.nwfilter)
                self._flow_manager.del_acl(self._dm.get_bridge_by_name(iface.br_name), iface)
        self._dm.delete_nwfilter(acl_name)

