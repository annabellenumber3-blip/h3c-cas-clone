    def reload_libvirt_nwfilters(self):
        t = time.time()
        res = net_lib.Utility.cmd_output_lines("timeout 2 virsh nwfilter-list")
        if not res:
            logging.warning("nothing returned by 'timeout 2 virsh nwfilter-list', time use: %d", time.time() - t)
            return False

        logging.debug("reload_libvirt_nwfilters: %s", res)

        new_nwfilters = []
        for line in res:
            line = line.strip()
            if line.startswith("UUID") or line.startswith("--------") or line == '':
                continue
            nwfilter = line.split()[-1]
            new_nwfilters.append(nwfilter)

        old_nwfilters = self._dm.get_all_nwfilters_name()

        with auto_commit_flow(self._flow_manager):
            for nwfilter in new_nwfilters:
                self.update_acl(nwfilter)

            for old_nwfilter in old_nwfilters:
                if old_nwfilter not in new_nwfilters:
                    logging.warning("delete non-existent nwfilter: %s", old_nwfilter)
                    self.del_acl(old_nwfilter)

        logging.debug("finish reload nwfilters from libvirt: %s", new_nwfilters)

        return True

