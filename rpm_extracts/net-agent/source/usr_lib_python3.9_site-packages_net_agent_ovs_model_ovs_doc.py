    def validate_nic_speed(self, in_init):
        logging.info("calling validate_nic_speed(in_init=%s)" % in_init)
        nicToSpeed = getNicSpeedFromConfig()
        global ActiveNics
        ActiveNics = None
        for eth in os.listdir("/sys/class/net/"):
            if not isActiveNic(eth):
                continue
            if in_init and _get_nic_speed(eth) < 1000000000:  # this indicates the speed for the interface is not ready, so validate later
                continue
            eth_ = eth
            if os.path.exists("/sys/class/net/%s/bonding_slave" % eth):
                eth_ = net_lib.Utility.cmd_execute("cd /sys/class/net/%s; find upper_*" % eth, shell=True)[1].strip("\n")[6:]  # 6: len of upper_

            bridge = net_lib.Utility.cmd_execute("ovs-vsctl -t 2 --verbose=db_ctl_base:syslog:off iface-to-br %s 2>/dev/null" % eth_, shell=True, log_err=False)[1].strip("\n")
            if not bridge:
                bridge = getLinuxEngineBridgeByNic(eth_)
            self._validate_one(eth, bridge if bridge else None, "", nicToSpeed)

    def _validate_one(self, eth, bridge, evt, nicToSpeed):
        eth_to_validate = {}
        if not evt:
            eth_to_validate[eth] = False
        else:
            ifaces = getActiveUplinkOfBridge(bridge)
            for iface in ifaces:
                eth_to_validate[iface] = False

        for iface in eth_to_validate:
            conf_speed = _get_config_speed(iface, nicToSpeed)
            tc_speed = _get_tc_speed(iface)
            if conf_speed >= 0 and conf_speed != tc_speed:
                eth_to_validate[iface] = True
                continue
            br_has_tos = None
            if not evt:
                br_has_tos = _is_tos_referred(bridge) if bridge else False
                evt = 'add' if br_has_tos else 'del'
            if evt == 'add':
                if tc_speed < 0 or tc_speed != _get_nic_speed(iface):
                    eth_to_validate[iface] = True
                    continue
            elif evt == 'del':
                if br_has_tos == None:
                    br_has_tos = _is_tos_referred(bridge) if bridge else False
                if tc_speed > 0 and not br_has_tos and conf_speed < 0:
                    logging.debug("del tc qdisc for %s, as no referred function to this" % iface)
                    net_lib.Utility.cmd_execute("tc qdisc del dev " + iface + " root")
                    continue

        logging.debug("eth_to_validate: %s" % eth_to_validate)

        for iface, need_validate in eth_to_validate.items():
            if need_validate:
                if isNicQosConfigured(bridge):
                    logging.info("validate subnet speed config for bridge %s (of iface %s)" % (bridge, iface))
                    validate_s = "<request><operation>validate</operation><vswitch><name>%s</name></vswitch></request>" % bridge
                    net_lib.Utility.cmd_execute("python3 /opt/bin/ovs_subnet.pyc --xml=\"%s\"" % validate_s, shell=True)
                else:
                    logging.info("validate network_priority speed config for iface %s." % iface)
                    net_lib.Utility.cmd_execute("python3 /opt/bin/ovs_network_priority.pyc validate --iface=%s" % iface, shell=True)

