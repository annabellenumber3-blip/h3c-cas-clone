    def define_portprofile(self, content, save=True):
        md5 = net_lib.Utility.calculate_md5(content)
        # duplication check portprofile through md5
        for profile in self._dm.get_all_portprofiles():
            if md5 == profile.md5:
                logging.debug("new portprofile is same with old profile %s, md5=%s, not deliver flow", profile.name, md5)
                return "ok", ERROR_CODE_OK
        # profile instantiation, xml parse, update flow if iface using portprofile
        new_portprofile = PortProfile(md5)
        if not new_portprofile.parse_from_xml(content):
            logging.error("failed to parse portprofile: %s", content)
            return "invalid argument: %s" % net_lib.Utility.get_lasterr(), ERROR_CODE_INVALID_ARGUMENT

        if save and not net_lib.Utility.save_file("%s/%s.xml" % (PORTPROFILE_DIR, new_portprofile.name), new_portprofile.format_xml()):
            res = "write file failed for %s" % new_portprofile.name
            logging.error(res)
            return res, ERROR_CODE_FAILED

        logging.debug("update_portprofile %s deliver flow", new_portprofile.name)
        old_profile = self._dm.get_portprofile_by_name(new_portprofile.name)
        old_netprofiles = self._dm.get_netprofiles_by_references(old_profile.netprofiles) if old_profile is not None else []
        new_netprofiles = self._dm.get_netprofiles_by_references(new_portprofile.netprofiles)
        for iface in self._dm.get_all_interfaces():
            if iface.port_profile == new_portprofile.name:
                bridge = self._dm.get_bridge_by_name(iface.br_name)
                self._flow_manager.unapply_netprofiles(bridge, iface, old_netprofiles, self._dm)
                self._flow_manager.apply_netprofiles(bridge, iface, new_netprofiles, self._dm)

        self._dm.update_portprofile(new_portprofile)
        return "ok", ERROR_CODE_OK

    def define_netprofile(self, content, save=True):
        profile_type, profile_name, uuid, element = parse_netprofile_element(content)
        if not profile_type or not profile_name:
            logging.error("failed to parse netprofile: %s", content)
            return "invalid argument: xml invalid or type uuid name can not parse", ERROR_CODE_INVALID_ARGUMENT
        md5 = net_lib.Utility.calculate_md5(content)
        profiles = self._dm.get_all_netprofiles()
        for profile in profiles:
            if md5 == profile.md5: # TODO reloaded file lead to md5 change
                logging.debug("new netprofile is same with old profile %s, md5=%s, not deliver flow", profile.name, md5)
                return "ok", ERROR_CODE_OK
            elif profile_name == profile.name and uuid != profile.uuid:
                res = "invalid argument: netprofile %s already exists with uuid %s" % (profile.name, profile.uuid)
                logging.error(res)
                return res, ERROR_CODE_INVALID_ARGUMENT
            elif uuid == profile.uuid and profile_name != profile.name:
                res = "invalid argument: netprofile with same UUID but different name(%s) already exists" % profile.name
                logging.error(res)
                return res, ERROR_CODE_INVALID_ARGUMENT

        new_profile = create_netprofile(profile_type, md5, element)
        if not new_profile:
            res = "invalid argument: netprofile instantiation failed as wrong xml parameter err=%s" % net_lib.Utility.get_lasterr()
            logging.error(res)
            return res, ERROR_CODE_INVALID_ARGUMENT

        if save and not net_lib.Utility.save_file("%s/%s/%s.xml" % (NETPROFILE_DIR, profile_type, new_profile.name), new_profile.format_xml()):
            logging.error("write file failed for %s", new_profile.name)
            return "failed", ERROR_CODE_FAILED
        
        if profile_type == "pvlan_all":
            vswitch_all_pvlans = {}
            vswitch_assign_pvlan = ""
            for rule in new_profile.rules:
                if rule.vswitch:
                    br = rule.vswitch
                    continue
                if br and rule.primary_vlan > 0:
                    vswitch_assign_pvlan = "(" + str(rule.primary_vlan) + ":" + rule.action.isolated_vlan + ":" + rule.action.group_vlan + ")"
                    second_vlans = []
                    before_second_vlans = []
                    primary_br = br + "_P" + str(rule.primary_vlan)
                    if len(primary_br) > 15:
                        br_name = self._dm.get_interface_by_name(br)
                        if br_name is not None:
                            br_mac = br_name.mac
                        else:
                            logging.error("The pvlan policy configuration of vswitch %s may be residual", br)
                            return "vswitch not exist", ERROR_CODE_NOTEXIST
                        primary_br = br_mac.replace(":", "")[-10:] + primary_br[-5:]
                    pvlan_vlans = list(map(int, vswitch_assign_pvlan.strip("()").replace(":", ",").replace(",", " ").split()))
                    pvlan_vlans = [x for x in pvlan_vlans if x != 0]
                    for pvlan_vlan in pvlan_vlans:
                        if pvlan_vlan != rule.primary_vlan:
                            second_vlans.append(pvlan_vlan)
                    if net_lib.Utility.cmd_execute("ovs-vsctl br-exists %s" % primary_br)[0] == 0:
                        pe_primary_br = "pvP" + str(rule.primary_vlan) + "_" + br
                        pe_br_primary = "pv" + br + "_P" + str(rule.primary_vlan)
                        pe_primary_br_ofport = OVSDb.get_ofport_by_iface(pe_primary_br)
                        trunks = ','.join(map(str, pvlan_vlans))
                        net_lib.Utility.cmd_execute("ovs-vsctl set port %s vlan_mode=trunk trunks=%s -- set port %s vlan_mode=trunk trunks=%s" % (pe_br_primary, trunks, pe_primary_br, trunks))
                        net_lib.Utility.cmd_execute('ovs-vsctl set port %s other_config:private_vlan="%s"' % (pe_br_primary, vswitch_assign_pvlan))
                        before_vswitch_assign_pvlan = ""
                        if len(self._dm.get_vswitch_all_pvlans(br)) != 0:
                            before_vswitch_assign_pvlan = self._dm.get_vswitch_all_pvlans(br).get(rule.primary_vlan, "")
                        if before_vswitch_assign_pvlan:
                            before_pvlan_vlans = list(map(int, before_vswitch_assign_pvlan.strip("()").replace(":", ",").replace(",", " ").split()))
                            before_pvlan_vlans.remove(rule.primary_vlan)
                            for before_pvlan_vlan in before_pvlan_vlans:
                                if before_pvlan_vlan > 0:
                                    before_second_vlans.append(before_pvlan_vlan)
                            logging.info("primary_vlan: %d before_second_vlans: %s second_vlans %s", rule.primary_vlan, before_second_vlans, second_vlans)
                            if len(before_second_vlans) > len(second_vlans):
                                for before_second_vlan in before_second_vlans:
                                    if before_second_vlan not in second_vlans:
                                        flow_del = "cookie=0x80/-1,in_port=%d,dl_vlan=%d" % (pe_primary_br_ofport, before_second_vlan)
                                        net_lib.Utility.cmd_execute('ovs-ofctl -O OpenFlow13 del-flows %s %s' % (primary_br, flow_del))
                            elif len(before_second_vlans) < len(second_vlans):
                                for second_vlan in second_vlans:
                                    if second_vlan not in before_second_vlans:
                                        flow_add = "cookie=0x80,priority=1001,table=100,in_port=%d,dl_vlan=%d,actions=mod_vlan_vid:%d,resubmit(,120)" % (pe_primary_br_ofport, second_vlan, rule.primary_vlan)
                                        net_lib.Utility.cmd_execute('ovs-ofctl -O OpenFlow13 add-flow %s %s' % (primary_br, flow_add))
                        else:
                            logging.info("primary_vlan: %d before_second_vlans: %s second_vlans %s", rule.primary_vlan, before_second_vlans, second_vlans)
                            for second_vlan in second_vlans:
                                flow_add = "cookie=0x80,priority=1001,table=100,in_port=%d,dl_vlan=%d,actions=mod_vlan_vid:%d,resubmit(,120)" % (pe_primary_br_ofport, second_vlan, rule.primary_vlan)
                                net_lib.Utility.cmd_execute('ovs-ofctl -O OpenFlow13 add-flow %s %s' % (primary_br, flow_add))
 
                    vswitch_all_pvlans = self._dm.get_vswitch_all_pvlans(br)
                    vswitch_all_pvlans[rule.primary_vlan] = vswitch_assign_pvlan
                    logging.info("br %s assign pvlan---->primary_vlan: %d pvlan: %s", br, rule.primary_vlan, vswitch_assign_pvlan)
            uplink_ports = net_lib.Utility.uplink(br, True)
            uplink_private_vlan = ','.join(vswitch_all_pvlans.values())
            if len(uplink_ports) >= 2:
                vswitch_bond = net_lib.Utility.cmd_execute("ovs-vsctl -t 5 list-bonds %s" % br, True)[1].split()
                if len(vswitch_bond) >= 1:
                    net_lib.Utility.cmd_execute("ovs-vsctl set port %s other_config:private_vlan=%s" %(vswitch_bond[0], uplink_private_vlan))
            elif len(uplink_ports) == 1:
                net_lib.Utility.cmd_execute("ovs-vsctl set port %s other_config:private_vlan=%s" %(uplink_ports[0], uplink_private_vlan))
            self._dm.update_vswitch_all_pvlans(br, vswitch_all_pvlans)
            logging.info("br %s all pvlans---->" % br)
            for key, value in vswitch_all_pvlans.items():
                logging.info("primary_vlan: %d pvlan: %s", key, value)
            self._dm.update_netprofile(new_profile)
            return "ok", ERROR_CODE_OK

        logging.debug("update_netprofile %s deliver flow", new_profile.name)
        is_update_trunkport = False
        old_profile = self._dm.get_netprofile_by_name(new_profile.name)
        old_profiles = [old_profile] if old_profile is not None else []
        if old_profile is not None and old_profile.name == new_profile.name and \
            old_profile.type == 'trunkport' and new_profile.type == 'trunkport':
            is_update_trunkport = True
        ifaces = self._dm.get_ifaces_by_netprofile(new_profile.name)
        for iface in ifaces:
            if not is_update_trunkport:
                bridge = self._dm.get_bridge_by_name(iface.br_name)
                self._flow_manager.unapply_netprofiles(bridge, iface, old_profiles, self._dm)
                if iface.nwfilter:
                    self._flow_manager.del_acl(bridge, iface)
            netprofiles = self._dm.get_netprofiles_by_references(self._dm.get_portprofile_by_name(iface.port_profile).netprofiles)
            if old_profile in netprofiles:
                netprofiles[netprofiles.index(old_profile)] = new_profile
            else:
                rnetprofiles = self._dm.get_portprofile_by_name(iface.port_profile).netprofiles
                index = 0
                for rnetprofile in rnetprofiles:
                    if rnetprofile.name == new_profile.name:
                        netprofiles.insert(index, new_profile)
                        break
                    else:
                        index = index + 1
                else:
                    netprofiles.append(new_profile)
            if not is_update_trunkport:
                self._flow_manager.apply_netprofiles(bridge, iface, netprofiles, self._dm)
            else:
                br = OVSDb.get_bridge_by_iface(iface.name)
                iface_update_trunkport(iface, br, old_profile, new_profile, self._dm)

        self._dm.update_netprofile(new_profile)
        return "ok", ERROR_CODE_OK

    def undefine_netprofile(self, name):
        netprofile = self._dm.get_netprofile_by_name(name)
        if netprofile is None:
            return "file not exist", ERROR_CODE_NOTEXIST

        ifaces = self._dm.get_ifaces_by_netprofile(name)
        if ifaces:
            res = "ifaces is still using %s" % name
            logging.error(res)
            return res, ERROR_CODE_FAILED

        file_name = "%s/%s/%s.xml" % (NETPROFILE_DIR, netprofile.type, name)
        try:
            if os.path.isfile(file_name):
                os.remove(file_name)
            self._dm.delete_netprofile(name)
            if netprofile.type == "pvlan_all":
                for rule in netprofile.rules:
                    if rule.vswitch:
                        br = rule.vswitch
                    if rule.primary_vlan > 0:
                        primary_vlan = rule.primary_vlan
                vswitch_all_pvlans = self._dm.get_vswitch_all_pvlans(br)
                vswitch_all_pvlans.pop(primary_vlan, None)
                uplink_ports = net_lib.Utility.uplink(br, True)
                vswitch_bond = net_lib.Utility.cmd_execute("ovs-vsctl -t 5 list-bonds %s" % br, True)[1].split()
                if len(vswitch_all_pvlans) == 0:
                    if len(uplink_ports) >= 2:
                        if len(vswitch_bond) >= 1:
                            net_lib.Utility.cmd_execute("ovs-vsctl remove port %s other_config private_vlan" % vswitch_bond[0])
                    elif len(uplink_ports) == 1:
                        net_lib.Utility.cmd_execute("ovs-vsctl remove port %s other_config private_vlan" % uplink_ports[0])
                    self._dm.delete_vswitch_all_pvlans(br)
                else:
                    logging.info("br %s all pvlans---->" % br)
                    for key, value in vswitch_all_pvlans.items():
                        logging.info("primary_vlan: %d pvlan: %s", key, value)
                    uplink_private_vlan = ','.join(vswitch_all_pvlans.values())
                    if len(uplink_ports) >= 2:
                        if len(vswitch_bond) >= 1:
                            net_lib.Utility.cmd_execute("ovs-vsctl set port %s other_config:private_vlan=%s" %(vswitch_bond[0], uplink_private_vlan))
                    elif len(uplink_ports) == 1:
                        net_lib.Utility.cmd_execute("ovs-vsctl set port %s other_config:private_vlan=%s" %(uplink_ports[0], uplink_private_vlan))
            return "ok", ERROR_CODE_OK
        except Exception as e:
            logging.exception(e)
            return "exception", ERROR_CODE_FAILED

