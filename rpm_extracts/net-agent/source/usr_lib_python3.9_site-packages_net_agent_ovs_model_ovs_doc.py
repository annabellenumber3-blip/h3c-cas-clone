def getActiveNics():
    global ActiveNics
    if ActiveNics is None:
        ActiveNics = net_lib.Utility.cmd_execute(r"ip -o link show up | sed 's#^[0-9]*: \([^ ]*\): .*#\1#'", shell=True)[1].splitlines()
        ActiveNics = list(filter(lambda nic: os.path.islink("/sys/class/net/%s" % nic) and ("/devices/platform" in os.readlink("/sys/class/net/%s" % nic) or "/devices/pci" in os.readlink("/sys/class/net/%s" % nic)), ActiveNics))
    return ActiveNics


def isActiveNic(nic):
    return nic in getActiveNics()


