@contextlib.contextmanager
def auto_commit_flow(flow_manager):
    try:
        yield
    finally:
        flow_manager.commit()


def _get_nic_speed(eth):
    # Only get the up interfaces
    speed = 0
    try:
        with open("/sys/class/net/%s/speed" % eth, "r") as f:
            speed = int(f.read().strip())
    except:
        logging.exception("failed to get speed for nic %s from sysfs.", eth)
    if speed == 0xffffffff:
        speed = 0
    return speed * 1000000


def _get_tc_speed(eth):
    using_speed = -1
    try:
        using_speed = net_lib.Utility.cmd_execute("tc class show dev %s classid 1:100" % eth, shell=True, log_err=False)[1].split(" ceil ")[1].split("bit ", 1)[0]
        using_speed = using_speed.replace("G","000000000").replace("M", "000000").replace("K", "000")
        return int(using_speed)
    except:
        logging.debug("can't obtain %s speed from tc" % eth)
    return using_speed


def getNicSpeedFromConfig():
    ETH_SPEED_CONFIG_FILE = '/etc/cvk/nic_speed.conf'
    nicToSpeed = {}
    if os.path.exists(ETH_SPEED_CONFIG_FILE):
        file = open(ETH_SPEED_CONFIG_FILE)
        try:
            ethInfos = file.read().strip().split('\n')
            # to check file is ''
            for k in ethInfos:
                ethInfo = k.split()
                if len(ethInfo) == 2 and ethInfo[1].isdigit():
                    nicToSpeed[ethInfo[0]] = int(ethInfo[1])
                else:
                    logging.warn("invalid nic information in config file: '%s'", k)
        finally:
            file.close()
    return nicToSpeed


def _get_config_speed(nic, nicToSpeed):
    if not nicToSpeed:
        nicToSpeed = getNicSpeedFromConfig()
    if nic in nicToSpeed.keys():
        return int(nicToSpeed[nic] * 1000000)

    if os.path.exists("%s/%s" % (LINUX_NETWORK_PATH, nic)):
        uplinks = getActiveUplinkOfBridge(nic)
        if len(uplinks) == 1 and uplinks[0] in nicToSpeed.keys():
            return int(nicToSpeed[uplinks[0]] * 1000000)

    return -1


def _is_tos_referred(bridge):
    ifaces = net_lib.Utility.cmd_output_string(r"ovs-vsctl -t 2 --verbose=db_ctl_base:syslog:off list-ifaces %s" % bridge).split()
    ifaces += net_lib.Utility.cmd_output_string(r"ovs-vsctl --format=table --columns=name --no-heading --data=bare find interface other_config:origin_br=%s" % bridge).split()
    if ifaces:
        vnet_cnt = net_lib.Utility.cmd_execute(r"ovs-vsctl --format=table --columns=name,other_config --no-heading --data=bare list Interface %s | grep 'tos=' | cut -d' ' -f1 | wc -l" % (" ".join(ifaces)), shell=True)[1].strip("\n")
        return int(vnet_cnt) > 0

    return False


