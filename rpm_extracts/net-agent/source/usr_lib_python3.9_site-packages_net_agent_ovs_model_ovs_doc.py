    def _handle_unix_socket_event(self, data):
        ress = []
        err_code = 0
        logging.warn("_handle_unix_socket_event->data: %s", data)
        for argv in data:
            if argv.cmd == net_lib.CTL_CMD_TYPE_NWFILTER:
                res = 'ok'
                if argv.subcmd == net_lib.CTL_CMD_TYPE_UPDATE:
                    res = self.update_acl(argv.nwfilter)
                elif argv.subcmd == net_lib.CTL_CMD_TYPE_DELETE:
                    self.del_acl(argv.nwfilter)
                else:
                    res = 'unsupported update/delete acl commands'
                message = '%s %s %s %s' % (argv.subcmd, argv.cmd, argv.nwfilter, res)
            elif argv.cmd == net_lib.CTL_CMD_TYPE_DUMP:
                message = self.dump()
            elif argv.cmd == net_lib.CTL_CMD_TYPE_BYPASS:
                message = self.drain_bypass(argv.escape)
            elif argv.cmd == net_lib.CTL_CMD_TYPE_REVALIADE:  # TODO: revalidate different thing
                mesgbad = ''
                net_lib.GlobalConfig().reload()
                brnames = argv.bridge if argv.bridge else []
                ifnames = argv.interface if argv.interface else []
                ifaces = [a for a in [self._dm.get_interface_by_name(b) for b in ifnames] if a]
                bridges = [a for a in [self._dm.get_bridge_by_name(b) for b in brnames] if a]
                if not brnames and not ifnames:
                    bridges = self._dm.get_all_bridges()
                if brnames and len(brnames) != len(bridges):
                    mesgbad += 'bridge input error '
                if ifnames and len(ifnames) != len(ifaces):
                    mesgbad += 'iface input error '
                if brnames and [a for a in ifaces if a.br_name not in brnames]:
                    mesgbad += 'iface not in the given bridges'
                message = mesgbad if mesgbad else self._revalidate_flows(bridges, ifaces, argv.include_base, argv.remove_old)
            elif argv.cmd == net_lib.CTL_CMD_TYPE_SELF_CHECK:
                message = self.self_check()
            elif argv.cmd == net_lib.CTL_CMD_TYPE_PORTPROFILE:
                res = ""
                if argv.subcmd == net_lib.CTL_CMD_TYPE_DEFINE:
                    content = argv.content if argv.content else ""
                    res, err_code = self.define_portprofile(content)
                    res = "%s %s %s" % (argv.subcmd, argv.cmd, res)
                elif argv.subcmd == net_lib.CTL_CMD_TYPE_UNDEFINE:
                    res, err_code = self.undefine_portprofile(argv.name)
                    res = "%s %s %s" % (argv.subcmd, argv.cmd, res)
                elif argv.subcmd == net_lib.CTL_CMD_TYPE_DUMP:
                    names = [argv.name] if argv.name else self._dm.get_all_portprofile_names()
                    for name in names:
                        res += self.dump_portprofile(name)
                elif argv.subcmd == net_lib.CTL_CMD_TYPE_LIST:
                    for name in self._dm.get_all_portprofile_names():
                        res += name+"\n"
                else:
                    res = "unsupported define/undefine/dump/list portprofile commands"
                message = res
            elif argv.cmd == net_lib.CTL_CMD_TYPE_NETPROFILE:
                res = ""
                if argv.subcmd == net_lib.CTL_CMD_TYPE_DEFINE:
                    content = argv.content if argv.content else ""
                    res, err_code = self.define_netprofile(content)
                    res = "%s %s %s" % (argv.subcmd, argv.cmd, res)
                elif argv.subcmd == net_lib.CTL_CMD_TYPE_UNDEFINE:
                    res, err_code = self.undefine_netprofile(argv.name)
                    res = "%s %s %s" % (argv.subcmd, argv.cmd, res)
                elif argv.subcmd == net_lib.CTL_CMD_TYPE_DUMP:
                    names = [argv.name] if argv.name else self._dm.get_all_netprofile_name()
                    for name in names:
                        res += self.dump_netprofile(name)
                elif argv.subcmd == net_lib.CTL_CMD_TYPE_LIST:
                    for name in self._dm.get_all_netprofile_name():
                        res += name+"\n"
                else:
                    res = "unsupported define/undefine/dump/list netprofile commands"
                message = res
            else:
                message = 'unknown cmd %s' % argv.cmd
            ress.append(message)
            ress.append(str(err_code))
        return ress

    def handle_ovsdb_interface_update(self, ovsdb_events, net_agent):
        old_data = None
        with auto_commit_flow(self._flow_manager):
            for event in ovsdb_events:
                logging.debug("receive event => %s", event)
                if event.action == "initial" or event.action == "insert":
                    self.handle_interface_add(event)
                    net_agent.handle_wait("set", event)
                elif event.action == "delete":
                    self.handle_interface_del(event)
                    net_agent.handle_wait("remove", event)
                elif event.action == "old":
                    old_data = event
                elif event.action == "new":
                    event.uuid = event.uuid or old_data.uuid
                    old_data.name = event.name or old_data.name
                    logging.debug("old: %s", old_data)
                    logging.debug("new: %s", event)
                    self.handle_interface_change(old_data, event)
                    net_agent.handle_wait("change", event)
                    old_data = None
                else:
                    logging.error("unhandle: %s", event)

    def _reconfigure_ovs_isolate(self):
        def ovs_pid_get():
            pid_list = []
            OVS_VSWITCHD_PID_PATH = "/run/openvswitch/ovs-vswitchd.pid"
            OVSDB_SERVER_PID_PATH = "/run/openvswitch/ovsdb-server.pid"
            OVS_FAILOVER_PID_PATH = "/run/openvswitch/ovs-failover.pid"

            with open(OVS_VSWITCHD_PID_PATH, 'r') as f:
                pid = f.read().strip()
                pid_list.append(pid)

            with open(OVSDB_SERVER_PID_PATH, 'r') as f:
                pid = f.read().strip()
                pid_list.append(pid)

            # for s1020v
            if os.path.exists(OVS_FAILOVER_PID_PATH):
                with open(OVS_FAILOVER_PID_PATH, 'r') as f:
                    pid = f.read().strip('\n')
                    pid_list.append(pid)

            return pid_list

        cpuset_ovs_procs_file = "/sys/fs/cgroup/cpuset/openvswitch/cgroup.procs"
        memory_ovs_procs_file = "/sys/fs/cgroup/memory/openvswitch/cgroup.procs"

        ovs_pid_list = ovs_pid_get()

        cpuset_ovs_procs = []
        memory_ovs_procs = []

        if not os.path.exists(cpuset_ovs_procs_file) and not os.path.exists(memory_ovs_procs_file):
            logging.info("ovs cpu and memory isolate not open, do nothing")
            return

        if os.path.exists(cpuset_ovs_procs_file):
            with open(cpuset_ovs_procs_file, 'r') as f:
                for line in f.readlines():
                    if line.strip().isdigit():
                        cpuset_ovs_procs.append(line.strip())

        if os.path.exists(memory_ovs_procs_file):
            with open(memory_ovs_procs_file, 'r') as f:
                for line in f.readlines():
                    if line.strip().isdigit():
                        memory_ovs_procs.append(line.strip())

        #if not cpuset_ovs_procs and not memory_ovs_procs:
        #    logging.info("ovs cpu and memory isolate not open, do nothing")
        #    return

        if set(ovs_pid_list) == set(cpuset_ovs_procs) and set(ovs_pid_list) == set(memory_ovs_procs):
            logging.info("ovs pid(%s) have been in %s and %s" % (ovs_pid_list, cpuset_ovs_procs_file, memory_ovs_procs_file))
            return
        else:
            logging.info("ovs pid(%s) not match procs in %s(%s) or %s(%s), start reconfigure ovs isolate ..." %
                                (ovs_pid_list, cpuset_ovs_procs_file, cpuset_ovs_procs, memory_ovs_procs_file, memory_ovs_procs))
            ret = net_lib.Utility.cmd_call("/opt/bin/isolate_cpuset_mem_config.sh.x openvswitch")
            if ret != 0:
                logging.error('reconfigure ovs isolate failed.')
            else:
                logging.info('reconfigure ovs isolate done.')

    def handle_daemon_change(self, daemon, running):
        if running and daemon == "ovs-vswitchd.pid":
            while not net_lib.Utility.wait_util(self.get_vswitchd_running, 60, 1):
                logging.warn("the ovs-vswitchd is not ready")

            self._reconfigure_ovs_isolate()
            self._reconfigure_flow_manager()
            self._init_drain_bypass()
            with auto_commit_flow(self._flow_manager):
                self._revalidate_flows(self._dm.get_all_bridges(), [], include_base=True, remove_old=True)
        elif daemon == "ovsdb-server.pid":
            if running and self._verify_ovsdb():
                self.load_bridges()

