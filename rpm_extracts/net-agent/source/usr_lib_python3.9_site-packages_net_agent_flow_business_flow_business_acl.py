#!/usr/bin/python3
import logging
from . flow_business_base import FlowBusinessBase


class FlowBusinessAcl(FlowBusinessBase):

    def __init__(self, dm):
        super(FlowBusinessAcl, self).__init__()
        self._dm = dm

    def _has_netprofile_acl(self, iface):
        port_profile = self._dm.get_portprofile_by_name(iface.port_profile)
        return any([(np.type == "acl") for np in port_profile.netprofiles]) if port_profile else False

    def teardown_flows(self, flow_manager, iface, bridge):
        if self._has_netprofile_acl(iface):
            logging.debug("ignore teardown acl flows for %s as has represent by netprofile yet", iface)
            return
        flow_manager.del_acl(bridge, iface)
        logging.debug("teardown acl flows for: %s.", iface)

    def setup_flows(self, flow_manager, iface, bridge):
        if self._has_netprofile_acl(iface):
            logging.debug("ignore setup acl flows for %s as has represent by netprofile yet", iface)
            return
        if iface.nwfilter:
            acl = self._dm.get_nwfilter_by_name(iface.nwfilter)
            if acl is None:
                logging.warn("can't find acl %s of iface %s, maybe nwfilter is not loaded yet", iface.nwfilter, iface.name)
            else:
                flow_manager.apply_acl(bridge, iface, acl.aclrules, 0, False, True)
                logging.debug("setup acl flows for %s has been done.", iface)

    def handle_interface_add_begin(self, flow_manager, iface, bridge, event):
        is_change = iface is None or iface.nwfilter != event.nwfilter
        if is_change:
            self.teardown_flows(flow_manager, iface, bridge)
        return is_change

    def handle_interface_add_end(self, flow_manager, iface, bridge, is_change):
        if is_change:
            self.setup_flows(flow_manager, iface, bridge)
        return is_change

    def handle_interface_change_begin(self, flow_manager, iface, bridge, old_event, new_event):
        is_change = old_event.other_config is not None and old_event.nwfilter != new_event.nwfilter
        if is_change:
            self.teardown_flows(flow_manager, iface, bridge)
        return is_change

    # is_change is passed from handle_interface_change_begin
    def handle_interface_change_end(self, flow_manager, iface, bridge, is_change):
        if is_change and iface.nwfilter:
            self.setup_flows(flow_manager, iface, bridge)
