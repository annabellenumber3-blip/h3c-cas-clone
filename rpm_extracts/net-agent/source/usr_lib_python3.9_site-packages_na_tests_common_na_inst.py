import utility


class NaInst(object):

    def __init__(self, startdir, extracode):
        self._na_started = False
        self._na_testdir = startdir
        self._extra_code = extracode

    def start(self):
        utility.Utility.cmd_execute('''
NA_TEST_MODE=1 NA_EXTRACODE='{extracode}' python3 -c 'from net_agent import net_agentd; net_agentd.main()'
'''.format(**{'extracode': self._extra_code}), True)
        self._na_started = True
        return True

    def cleanup(self):
        if not self._na_started:
            return

        utility.Utility.cmd_execute('''
kill `cat {na_testdir}/net-agentd.pid`
'''.format(**{'na_testdir': self._na_testdir}), True)
