#!/usr/bin/python3

import unittest
import xml.etree.ElementTree as ET
from net_agent.ovs_model import ovs_object
from na_tests.mock import mock


class Mock_ACL(object):

    def __init__(self,
                 name="",
                 aclrule=None):
        super(Mock_ACL, self).__init__()
        self.name = name
        self.aclrules = aclrule


class TestOVSObject(unittest.TestCase):

    # test OVSDb class
    @mock.patch('logging.error')
    def test_ovsdb_class(self, _):
        ovsdb = ovs_object.OVSDb()
        # self.assertEqual(ovsdb.get_bridge_by_iface('eth0'), 'vswitch0')  #  cause error: '%' != 'vswitch0'
        ovsdb.get_bridges()

    # test Bridge class
    def test_birdge_class(self):
        bridge = ovs_object.Bridge('vswitch0', 0, True)
        self.assertEqual(str(bridge).strip(' '), "vswitch0: mode=0, exist=True".strip(' '))

    # test Interface class
    def test_interface_class(self):
        interface = ovs_object.Interface("123321", "vnet0", 4, 100, None, None, "vswitch0")
        self.assertEqual(str(interface), "vnet0: br=vswitch0,ofport=4,tag=100,other_cfg={},ex_id={},uuid=123321")

        interface1 = ovs_object.Interface("124321", "vnet1", 5, 101, None, {'bind-ip':'172.20.23.5'}, "vswitch0")
        self.assertEqual(interface1.bind_ip, "172.20.23.5")
        interface2 = ovs_object.Interface("125321", "vnet2", 6, 102, {'port-profile':'test1'}, None , "vswitch0")
        self.assertEqual(interface2.port_profile, 'test1')

    # test FlowMatchField class
    def test_flowmatchfield_class(self):
        matchfield = ovs_object.FlowMatchField()
        matchfield.set_fields(nw_src='172.20.23.5',nw_proto='tcp')
        self.assertEqual(matchfield.nw_src, '172.20.23.5')
        self.assertEqual(matchfield.nw_proto, 'tcp')
        matchfield.reverse()
        self.assertEqual(matchfield.nw_dst, '172.20.23.5')
        self.assertEqual(matchfield.nw_src, None)
        flow = ET.fromstring("<flow direction='in' dl_type='ipv6' ipv6_dst='2001:2::32/64' nw_proto='icmp6' priority='13'></flow>")
        matchfield.format_xml(flow)

    # test Flow class
    def test_flow_class(self):
        flow = ovs_object.Flow(bridge='vswich0', cookie='0x30', table=10, priority=1000, matchfield=None, actions='drop')
        self.assertEqual(str(flow), "priority=1000,cookie=0x30,table=10,actions=drop")
        flow_clone = flow.clone()
        self.assertNotEqual(flow, flow_clone)
        self.assertEqual(str(flow), str(flow_clone))

    # test OVSAction class
    def test_ovsaction_class(self):
        ovs_action = ovs_object.OVSAction()
        ovs_action_tmp = ovs_object.OVSAction("accept")  # openflow doen't has accept action

        self.assertEqual(str(ovs_action), 'drop')
        ovs_action.add_normal()
        self.assertEqual(str(ovs_action), 'normal')

        ovs_action.add_output_outport()
        self.assertEqual(str(ovs_action), 'normal,output:NXM_NX_REG1[0..15]')

        ovs_action.add_set_meter(6)
        self.assertEqual(str(ovs_action), 'normal,output:NXM_NX_REG1[0..15],meter:6')

        ovs_action.add_xnormal()
        self.assertEqual(str(ovs_action), 'normal,output:NXM_NX_REG1[0..15],meter:6,load:1->NXM_NX_REG0[],normal,load:0->NXM_NX_REG0[]')

        ovs_action.add_output_reg(0)
        self.assertEqual(str(ovs_action), 'normal,output:NXM_NX_REG1[0..15],meter:6,load:1->NXM_NX_REG0[],normal,load:0->NXM_NX_REG0[],output:NXM_NX_REG0[]')

        ovs_action.add_goto_table(0)
        self.assertEqual(str(ovs_action), 'normal,output:NXM_NX_REG1[0..15],meter:6,load:1->NXM_NX_REG0[],normal,load:0->NXM_NX_REG0[],output:NXM_NX_REG0[],goto_table:0')

        ovs_action.add_set_reg(1, 0)
        self.assertEqual(str(ovs_action), 'normal,output:NXM_NX_REG1[0..15],meter:6,load:1->NXM_NX_REG0[],normal,load:0->NXM_NX_REG0[],output:NXM_NX_REG0[],goto_table:0,load:1->NXM_NX_REG0[]')

        ovs_action.add_actions(ovs_action_tmp)
        self.assertEqual(str(ovs_action), 'normal,output:NXM_NX_REG1[0..15],meter:6,load:1->NXM_NX_REG0[],normal,load:0->NXM_NX_REG0[],output:NXM_NX_REG0[],goto_table:0,load:1->NXM_NX_REG0[],accept')

    # test ACLRule class is an auxiliary class of the ACL class. lt only needs to test the ACL class
    @mock.patch('logging.error')
    def test_acl_class(self, _):
        xml = "<filter name='tdpdk' chain='root'><uuid>08d0ac68-c6d4-4984-823c-cf4a68022d6a</uuid><rule action='accept' direction='in' priority='4' statematch='false'><icmp srcipaddr='6.6.51.51' srcipmask='32' dstipaddr='6.6.52.52' dstipmask='32'/></rule><rule action='accept' direction='out' priority='5' statematch='false'><all/></rule></filter>"
        acl = ovs_object.ACL(xml)
        self.assertEqual(str(acl.aclrules[0]), 'action=accept, dir=out, priority=5, statematch=false, match: dl_type=0x0800,')
        self.assertEqual(str(acl.aclrules[1]), 'action=accept, dir=in, priority=4, statematch=false, match: dl_type=0x0800,nw_src=6.6.51.51/32,nw_dst=6.6.52.52/32,nw_proto=1,')
        # actionss is error field
        xml_error = "<filter name='tdpdk' chain='root'><uuid>08d0ac68-c6d4-4984-823c-cf4a68022d6a</uuid><rule actionss='accept' direction='in' priority='4' statematch='false'><icmp srcipaddr='6.6.51.51' srcipmask='32' dstipaddr='6.6.52.52' dstipmask='32'/></rule><rule action='accept' direction='out' priority='5' statematch='false'><all/></rule></filter>"
        acl_error = ovs_object.ACL(xml_error)
        self.assertEqual(str(acl_error.aclrules[0]), 'action=accept, dir=out, priority=5, statematch=false, match: dl_type=0x0800,')
        self.assertEqual(str(acl_error.aclrules[1]), 'action=drop, dir=in, priority=4, statematch=false, match: dl_type=0x0800,nw_src=6.6.51.51/32,nw_dst=6.6.52.52/32,nw_proto=1,')


if __name__ == '__main__':
    unittest.main()