import logging
from .. base import net_lib
from . flow_business_base import FlowBusinessBase


class FlowBusinessPortProfile(FlowBusinessBase):

    def __init__(self, dm):
        super(FlowBusinessPortProfile, self).__init__()
        self._dm = dm

    def teardown_flows(self, flow_manager, iface, bridge):
        if iface.port_profile:
            port_profile = self._dm.get_portprofile_by_name(iface.port_profile)
            if port_profile is None:
                logging.warn("can't find portprofile %s of iface %s,  maybe portprofile is not loaded yet", iface.port_profile, iface.name)
                return
            else:
                netprofiles = self._dm.get_netprofiles_by_references(port_profile.netprofiles)
                flow_manager.unapply_netprofiles(bridge, iface, netprofiles, self._dm)
        logging.debug("teardown portprofile flow for: %s.", iface)

    ''' set the interface flow by portprofile'''
    def setup_flows(self, flow_manager, iface, bridge):
        if iface.port_profile:
            port_profile = self._dm.get_portprofile_by_name(iface.port_profile)
            if port_profile is None:
                logging.warn("can't find portprofile %s of iface %s, maybe portprofile is not loaded yet", iface.port_profile, iface.name)
                return
            else:
                netprofiles = self._dm.get_netprofiles_by_references(port_profile.netprofiles)
                flow_manager.apply_netprofiles(bridge, iface, netprofiles, self._dm)
                logging.debug("setup portprofile %s flow for %s has been done.", port_profile, iface)

    def handle_interface_add_begin(self, flow_manager, iface, bridge, event):
        is_change = iface is None or iface.port_profile != event.port_profile
        if iface and not is_change:
            # first check for type vm_ports drain.
            # This means port tag change event(tag = origin_vlan) when add port, no need to reapply drain flows..
            if iface.origin_vlan and iface.origin_br and (iface.drain_vlan and str(event.tag) == iface.origin_vlan):
                is_change = False
            else:
                port_profile = self._dm.get_portprofile_by_name(iface.port_profile)
                if port_profile:
                    is_change = any([(np.type == "drain") for np in port_profile.netprofiles])
        if event.other_config.get("origin_vlan") and "primary_vlan" not in event.other_config:
            is_change = is_change or int(event.other_config.get("origin_vlan")) > 0
        if is_change:
            self.teardown_flows(flow_manager, iface, bridge)
        return is_change

    def handle_interface_add_end(self, flow_manager, iface, bridge, is_change):
        if is_change:
            self.setup_flows(flow_manager, iface, bridge)
        return is_change

    def handle_interface_change_begin(self, flow_manager, iface, bridge, old_event, new_event):
        is_change = old_event.external_ids is not None and old_event.port_profile != new_event.port_profile
        if iface and not is_change:
            if iface.origin_vlan and iface.origin_br and not iface.primary_vlan: # first check for type vm_ports drain.
                old_event_origin_vlan = old_event.other_config.get("origin_vlan")
                new_event_origin_vlan = new_event.other_config.get("origin_vlan")
                if old_event_origin_vlan != new_event_origin_vlan:
                    # This means change origin_vlan for port.
                    logging.info("port %s: change origin_vlan %s to %s." % (iface.name, old_event_origin_vlan, new_event_origin_vlan))
                    is_change = True
                elif iface.drain_vlan and new_event.tag != old_event.tag:
                    old_event_drain_vlan = old_event.other_config.get("drain_vlan")
                    new_event_drain_vlan = new_event.other_config.get("drain_vlan")
                    if str(old_event.tag) == old_event_origin_vlan and str(new_event.tag) == old_event_drain_vlan and old_event_drain_vlan == new_event_drain_vlan:
                        # This means recover drain flows after bypass.
                        logging.info("port %s: recover drain from %s to %s after bypass." % (iface.name, old_event.tag, new_event.tag))
                        is_change = True

            elif new_event.tag != old_event.tag: # second check for type uplink drain.
                port_profile = self._dm.get_portprofile_by_name(iface.port_profile)
                if port_profile:
                    is_change = any([(np.type == "drain") for np in port_profile.netprofiles])
        is_change = is_change or (str(old_event.other_config.get("origin_vlan")) != str(new_event.other_config.get("origin_vlan")) and "primary_vlan" not in new_event.other_config) or str(old_event.other_config.get("bypass_revalidate_flow")) != str(new_event.other_config.get("bypass_revalidate_flow"))
        if str(old_event.other_config.get("origin_vlan")) != str(new_event.other_config.get("origin_vlan")) and "primary_vlan" in new_event.other_config:
            if "isolated_vlan" in new_event.other_config:
                pvlan_vlan = int(new_event.other_config.get("isolated_vlan"))
            elif "group_vlan" in new_event.other_config:
                pvlan_vlan = int(new_event.other_config.get("group_vlan"))
            else:
                pvlan_vlan = int(new_event.other_config.get("primary_vlan"))
            net_lib.Utility.cmd_execute("ovs-vsctl set port %s tag=%d" % (iface.name, pvlan_vlan))
            if "isolated_vlan" in new_event.other_config:
                net_lib.Utility.cmd_execute("ovs-appctl fdb/add %s %s %d %s" % (bridge.name, iface.name, pvlan_vlan, iface.mac))
        if is_change:
            self.teardown_flows(flow_manager, iface, bridge)
        return is_change

    # is_change is passed from handle_interface_change_begin
    def handle_interface_change_end(self, flow_manager, iface, bridge, is_change):
        if is_change and iface.port_profile:
            self.setup_flows(flow_manager, iface, bridge)
