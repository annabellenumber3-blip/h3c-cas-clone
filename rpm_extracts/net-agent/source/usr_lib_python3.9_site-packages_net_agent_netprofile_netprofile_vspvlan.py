#!/usr/bin/python3
import logging
import xml.etree.ElementTree as ET
import uuid
from . netprofile import NetProfile
from . netprofile import Rule
from .. base import net_lib

class VsPvlanRule(Rule):
    """ vspvlan netprofile rule """
    def __init__(self):
        super(VsPvlanRule, self).__init__()
        self.vswitch = ""
        self.primary_vlan = 0

    def __str__(self):
        if self.vswitch:
            return "vswitch=%s" % self.vswitch
        else:
            return "primary_vlan=%s, action:%s" % (self.primary_vlan, self.action)

class VsPvlanAction(object):
    """ define VsPvlanAction used as action of flow which describe vspvlan parameters """
    def __init__(self):
        super(VsPvlanAction, self).__init__()
        self.isolated_vlan = ""
        self.group_vlan = ""

    def __str__(self):
        return "isolated_vlan=%s group_vlan=%s" % (self.isolated_vlan, self.group_vlan)

class NetProfileVsPvlan(NetProfile):
    def __init__(self, md5=""):
        super(NetProfileVsPvlan, self).__init__(md5)

    def parse(self, root):
        if root is None:
            logging.error("root is None")
            return False

        self.type = root.attrib.get("type", "")
        self.name = root.attrib.get("name", "")
        self.uuid = root.attrib.get("uuid", str(uuid.uuid3(uuid.NAMESPACE_DNS, self.name)))
        self.version = root.attrib.get("version", "")

        if self.name == "":
            net_lib.Utility.set_log_lasterr("name is None")
            return False

        for child in root:
            if child.tag != "flow":
                continue

            if "primary_vlan" not in child.attrib.keys() and "vswitch" not in child.attrib.keys():
                net_lib.Utility.set_log_lasterr("incomplete flow for vspvlan: %s" % child.attrib)
                return False

            rule = VsPvlanRule()
            try:
                if "vswitch" in child.attrib.keys():
                    rule.vswitch = child.attrib.get("vswitch", "")
                    if not rule.vswitch:
                        raise ValueError("vspvlan vswitch is None")
                if "primary_vlan" in child.attrib.keys():
                    rule.primary_vlan = int(child.attrib.get("primary_vlan", ""))
                    if rule.primary_vlan > 4094 or rule.primary_vlan < 2:
                        raise ValueError("vspvlan primary_vlan is not in [2,4094]")
            except Exception as e:
                net_lib.Utility.set_log_lasterr("invalid vspvlan primary_vlan or vswitch, check your xml(%s)" % str(e))
                return False

            for item in child:
                if item.tag != "action":
                    continue
                rule.action = self.parse_extra_action(item)
                if rule.action is None:
                    net_lib.Utility.set_log_lasterr("failed to parse pvlan action: %s" % item)
                    return False
            self.rules.append(rule)

        return True

    def format_xml(self):
        xml = "\n"
        try:
            root = ET.Element("netprofile")
            root.attrib["type"] = self.type
            root.attrib["name"] = self.name
            root.attrib["uuid"] = self.uuid
            root.attrib["version"] = self.version
            for rule in self.rules:
                flow = ET.SubElement(root, "flow")
                if rule.vswitch:
                    flow.attrib["vswitch"] = rule.vswitch
                    continue
                flow.attrib["primary_vlan"] = str(rule.primary_vlan)
                action = ET.SubElement(flow, "action")
                self.format_extra_action(rule.action, action)
            xml = ET.tostring(root).decode().replace("><", ">\n<")
        except:
            logging.exception("catch exception")

        return xml

    def parse_extra_action(self, item):
        if 'isolated_vlan' not in item.attrib.keys() or 'group_vlan' not in item.attrib.keys():
            net_lib.Utility.set_log_lasterr("invalid action for vspvlan: %s" % item.attrib)
            return None

        act = VsPvlanAction()
        try:
            for pvlan_type in ["isolated_vlan", "group_vlan"]:
                if pvlan_type == "isolated_vlan":
                    act.isolated_vlan  = item.attrib.get(pvlan_type, "")
                    vlans = list(map(int, act.isolated_vlan.split(',')))
                else:
                    act.group_vlan = item.attrib.get(pvlan_type, "")
                    vlans = list(map(int, act.group_vlan.split(',')))
                for vlan in vlans:
                    if not isinstance(vlan, int) or vlan > 4094 or vlan < 0 or vlan == 1:
                        raise ValueError("vspvlan vlan is not int or not in [2,4094] or 0")
        except Exception as e:
            net_lib.Utility.set_log_lasterr("invalid action pvlan vlan for vspvlan, check your xml(%s)" % str(e))
            return None

        return act

    def format_extra_action(self, action, element):
        element.attrib["isolated_vlan"] = action.isolated_vlan
        element.attrib["group_vlan"] = action.group_vlan
