import fcntl

LOCK_FILE = '/var/run/net-agent.lock'

def create_lock(file_path):
    lock_file = open(file_path, 'w')
    return lock_file

def acquire_lock(lock_file):
    fcntl.flock(lock_file.fileno(), fcntl.LOCK_EX)

def release_lock(lock_file):
    fcntl.flock(lock_file.fileno(), fcntl.LOCK_UN)

def get_cmdline_parser():
    top_parser = argparse.ArgumentParser(description='Process ovs configuration.')
    top_parser.add_argument(
        '--loglevel',
        choices=["error", "warn", "info", "debug"],
        help='log level')
    # timeout: per connect operation timeout
    top_parser.add_argument('-t', '--timeout', type=int, help='timeout')
    # retry: count of retry connect, -1: always retry, [0, maxint] the count to retry connect
    top_parser.add_argument('-r', '--retry', type=int, help='count of retry connect')

    sub_parsers = top_parser.add_subparsers(dest="cmd", help="netflow supported operations")
    sub_parsers.add_parser(net_lib.CTL_CMD_TYPE_EXIT)

    # Dump
    sub_parsers.add_parser(net_lib.CTL_CMD_TYPE_DUMP)

    # Bypass for drain when vSSM error.
    bypass_parsers = sub_parsers.add_parser(net_lib.CTL_CMD_TYPE_BYPASS)
    bypass_parsers.add_argument('--escape', action='store_true', help='enable bypass')

    # Revalidate
    revalidate_parser = sub_parsers.add_parser(net_lib.CTL_CMD_TYPE_REVALIADE)
    revalidate_parser.add_argument('-i', '--interface', nargs='+', help='default all')
    revalidate_parser.add_argument('--remove-old', action='store_true', help='')
    revalidate_parser.add_argument('--include-base', action='store_true', help='')
    revalidate_parser.add_argument('--bridge', nargs='+', help='default all')

    # update/delete nwfilter rules on all interfaces that refer the nwfilter
