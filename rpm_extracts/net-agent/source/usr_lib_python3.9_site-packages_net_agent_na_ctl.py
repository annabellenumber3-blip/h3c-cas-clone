def parser_individual_args(argv):
    def split_to_individual_cmd(argv):
        argvs = [[]]
        for v in argv:
            if v != '--':
                argvs[-1].append(v)
            elif argvs[-1]:
                argvs.append([])
        if len(argvs) >= 2 and argvs[-1] == []:
            argvs = argvs[:-1]
        return argvs

    p = get_cmdline_parser()
    argvs = split_to_individual_cmd(argv)
    for a in argvs:
        logging.debug("len:%d, %s", len(a), str(a))
    return [p.parse_args(a) for a in argvs]


def get_time_out(argvs):
    timeout = -1
    for argv in argvs:
        if isinstance(argv.timeout, int) and argv.timeout > timeout:
            timeout = argv.timeout
    return timeout if timeout >= 0 else 5


def get_retry(argvs):
    for argv in argvs:
        if isinstance(argv.retry, int):
            return argv.retry
    return 0


def ctl_do(argvs):
    data, err_code = "", ERROR_CODE_FAILED
    client = socket.socket(socket.AF_UNIX, socket.SOCK_STREAM)
    t = get_time_out(argvs)
    client.settimeout(t)
    retry_count = get_retry(argvs)
    lock = create_lock(LOCK_FILE)
    acquire_lock(lock)
    while True:
        err_code = client.connect_ex(net_lib.Network.NET_AGENT_UNIX_PATH)
        if err_code == ERROR_CODE_OK:
            break
        elif retry_count > 0:
            retry_count = retry_count - 1
            time.sleep(0.4)  # seems consume much cpu yet
        elif retry_count == 0:
            data = "connect failed"
            err_code = net_lib.ERROR_CODE_CONNECT_FAILED
            break

    if err_code == ERROR_CODE_OK:
        try:
            net_lib.Network.socket_send_message(
                client, net_lib.Network.NETWORK_MESSAGE_TYPE_CMDLINE,
                net_lib.Network.data_serialize(argvs))
            logging.debug("network message send")
        except:
            logging.exception("handled send exception:")
            release_lock(lock)
        else:
            try:
                type_, message = net_lib.Network.socket_recv_message(client)
            except socket.timeout:
                logging.exception("handled recv timeout:")
                err_code = net_lib.ERROR_CODE_NACTL_COMMAND_TIMEOUT
                release_lock(lock)
            except:
                logging.exception("handled recv exception:")
                release_lock(lock)
            else:
                if type_ == net_lib.Network.NETWORK_MESSAGE_TYPE_NONE:
                    logging.error("socket is closed by client.")
                elif type_ == net_lib.Network.NETWORK_MESSAGE_TYPE_CMDLINE_REPLY:
                    recv = net_lib.Network.data_unserialize(message)
                    if len(recv) == 2:
                        data, err_code = recv[0], int(recv[1]) if recv[1].isdigit() else ERROR_CODE_FAILED
                    else:
                        logging.warn("wrong recv len: %d, message: %s", len(recv), recv)
    logging.debug("close socket")
    client.close()
    release_lock(lock)
    return data, err_code


def pre_ctl_do(argvs):
    err_code = ERROR_CODE_OK
    for argv in argvs:
        if (argv.cmd == net_lib.CTL_CMD_TYPE_NETPROFILE or argv.cmd == net_lib.CTL_CMD_TYPE_PORTPROFILE) and argv.subcmd == net_lib.CTL_CMD_TYPE_DEFINE:
            if argv.file:
                # noinspection PyBroadException
                try:
                    argv.content = net_lib.Utility.read_file(argv.file)
                    argv.file = None
                except:
                    logging.exception("convert file %s to content failed", argv.file)
                    err_code = ERROR_CODE_FAILED
                    break
    return err_code


def _main(argv):
    argvs = parser_individual_args(argv)
    err_code = pre_ctl_do(argvs)
    if err_code == ERROR_CODE_OK:
        result, err_code = ctl_do(argvs)
        print(result)
        return err_code
    else:
        print("pre_ctl_do error")
        return err_code


def main():
    res = ERROR_CODE_OK
    log_file = os.getenv("NA_LOGDIR", "/var/log/net-agent") + "/cas_na_ctl.log"
    logging.basicConfig(
        level=logging.DEBUG,
        format='%(asctime)s %(levelname)7s %(funcName)s:%(lineno)d %(message)s',
        datefmt='%m-%d %H:%M:%S',
        filename=log_file)
    try:
        logging.warn("args: &&%s&&", str(sys.argv))
        res = _main(sys.argv[1:])
    except BaseException as e:
        res = e.errorcode if hasattr(e, "errorcode") else 1
        logging.exception("")
    else:
        if res != ERROR_CODE_OK:
            logging.error("error code: %d\n\n", res)
        else:
            logging.info("execute end: error code %d\n\n", res)
    sys.exit(res)


if __name__ == '__main__':
    main()
