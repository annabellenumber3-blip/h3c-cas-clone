    acl_subparser = acl_parser.add_subparsers(dest="subcmd", help="nwfilter supported commands")
    update_nwfilter = acl_subparser.add_parser(net_lib.CTL_CMD_TYPE_UPDATE, help="update the nwfilter")
    update_nwfilter.add_argument('nwfilter', help='')
    delete_nwfilter = acl_subparser.add_parser(net_lib.CTL_CMD_TYPE_DELETE, help="delete the nwfilter")
    delete_nwfilter.add_argument('nwfilter', help='')

    # define/undefine/dump/list portprofile
    portprofile_parser = sub_parsers.add_parser(net_lib.CTL_CMD_TYPE_PORTPROFILE)
    portprofile_subparser = portprofile_parser.add_subparsers(dest="subcmd", help="portprofile supported commands")
    define_portprofile = portprofile_subparser.add_parser(net_lib.CTL_CMD_TYPE_DEFINE, help="define or update a portprofile")
    define_portprofile.add_argument('--content', help="XML format content of portprofile")
    define_portprofile.add_argument('file', nargs='?', help="portprofile XML file")
    undefine_portprofile = portprofile_subparser.add_parser(net_lib.CTL_CMD_TYPE_UNDEFINE, help="undefine a portprofile")
    undefine_portprofile.add_argument('name', help="portprofile name")
    dump_portprofile = portprofile_subparser.add_parser(net_lib.CTL_CMD_TYPE_DUMP, help="portprofile information in XML")
    dump_portprofile.add_argument('name', nargs='?', help="portprofile name")
    portprofile_subparser.add_parser(net_lib.CTL_CMD_TYPE_LIST, help="list portprofiles")

    # define/undefine/dump/list netprofile
    netprofile_parser = sub_parsers.add_parser(net_lib.CTL_CMD_TYPE_NETPROFILE)
    netprofile_subparser = netprofile_parser.add_subparsers(dest="subcmd", help="netprofile supported commands")
    define_netprofile = netprofile_subparser.add_parser(net_lib.CTL_CMD_TYPE_DEFINE, help="define or update a netprofile")
    define_netprofile.add_argument('--content', help="XML format content of netprofile")
    define_netprofile.add_argument('file', nargs='?', help="netprofile XML file")
    undefine_netprofile = netprofile_subparser.add_parser(net_lib.CTL_CMD_TYPE_UNDEFINE, help="undefine a netprofile")
    undefine_netprofile.add_argument('name', help="netprofie name")
    dump_netprofile = netprofile_subparser.add_parser(net_lib.CTL_CMD_TYPE_DUMP, help="netprofile information in XML")
    dump_netprofile.add_argument('name', nargs='?', help="netprofie name")
    netprofile_subparser.add_parser(net_lib.CTL_CMD_TYPE_LIST, help="list netprofiles")

