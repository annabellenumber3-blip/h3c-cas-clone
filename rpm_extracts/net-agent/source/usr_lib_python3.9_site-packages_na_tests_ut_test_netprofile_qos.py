#!/usr/bin/python3

import unittest
import xml.etree.ElementTree as ET
from net_agent.netprofile import netprofile_qos
from net_agent.netprofile import netprofile


class TestNetprofileQos(unittest.TestCase):

    def test_parse_extra_action(self):
        netprofile_object = netprofile.NetProfile()
        netprofile_qos_object = netprofile_qos.NetProfileQos(netprofile_object)

        action = '<action burst="10000" domain="interface" id="4" rate="30000" unit="kbps" />'
        item = ET.fromstring(action)
        meter = netprofile_qos_object.parse_extra_action(item)

        self.assertEqual(str(meter), "domain=interface,id=4,unit=kbps,rate=30000,burst=10000")

    def test_format_extra_action(self):
        netprofile_object = netprofile.NetProfile()
        netprofile_qos_object = netprofile_qos.NetProfileQos(netprofile_object)

        action = '<action burst="10000" domain="interface" id="4" rate="30000" unit="kbps" />'
        item = ET.fromstring(action)
        meter = netprofile_qos.Meter()
        meter.burst = '50000'
        meter.rate = '30000'
        netprofile_qos_object.format_extra_action(meter, item)

        self.assertDictEqual(item.attrib, {'domain': 'interface', 'rate': '30000', 'id': '0', 'unit': 'kbps', 'burst': '50000'})


if __name__ == '__main__':
    unittest.main()