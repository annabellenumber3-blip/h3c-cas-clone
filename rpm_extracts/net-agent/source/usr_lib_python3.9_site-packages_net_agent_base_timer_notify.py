#!/usr/bin/python3
import time
import logging


class Timer(object):

    def __init__(self, id_, interval, idle_interval, count):
        super(Timer, self).__init__()
        self.id = id_
        self.interval = interval
        self.idle_interval = idle_interval
        self.count = count
        self.timeout_when = interval + time.time()

    def __str__(self):
        return "[timer %d: interval=%d, idle=%d, count=%d, when=%d]" %\
               (self.id, self.interval, self. idle_interval, self.count, self.timeout_when)


class TimerNotify(object):

    def __init__(self):
        super(TimerNotify, self).__init__()
        self._timers = []
        self._last_active = time.time()  # _last_active: the time do real business (not timer)
        self._first_tick = self._last_active  # _first_tick: the time just before poll call

    def clear(self):
        self._timers = []

    def add_timer(self, timer_id, interval, idle_interval=-1, count=-1):
        assert interval > idle_interval
        if [it for it in self._timers if it.id == timer_id]:
            return False
        self._timers.append(Timer(timer_id, interval, idle_interval, count))
        return True

    def contain_timers(self, timer_ids):
        for timer_id in timer_ids:
            if [it for it in self._timers if it.id == timer_id]:
                continue
            else:
                return False
        return True

    def set_last_active(self):
        self._last_active = time.time()

    def run(self, callback):
        """Produce timer events and also update wait_time for each timer"""
        need_remove = False
        now_time = time.time()
        for it in self._timers:
            if it.timeout_when < now_time:
                idle_interval = now_time - self._last_active
                if idle_interval > it.idle_interval:
                    try:
                        logging.debug("process timer task %s, now %s", it, now_time)
                        callback(it.id)
                    except:
                        logging.exception("handle timer task %s exception", it)
                    if it.count > 0:
                        it.count -= 1
                    it.timeout_when = now_time + it.interval  # don't consider processing time here
                else:
                    logging.info("delay timer task %s because idle interval %d is too short", it, idle_interval)
                    it.timeout_when = now_time + it.idle_interval
                if it.count == 0:
                    need_remove = True

        if need_remove:
            self._timers = [it for it in self._timers if it.count != 0]

    def get_wait_time(self):
        min_ = 0x7fffffffffffffff  # max int in Python
        for it in self._timers:
            if it.timeout_when < min_:
                min_ = it.timeout_when
        min_ -= time.time()
        return min_ if min_ > 0 else 0
