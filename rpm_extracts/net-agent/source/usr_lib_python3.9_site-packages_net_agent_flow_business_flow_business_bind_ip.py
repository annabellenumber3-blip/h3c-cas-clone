#!/usr/bin/python3
import logging
from . flow_business_base import FlowBusinessBase


class FlowBusinessBindIp(FlowBusinessBase):

    def __init__(self):
        super(FlowBusinessBindIp, self).__init__()

    def teardown_flows(self, flow_manager, iface, bridge):
        flow_manager.del_bind_ip(bridge, iface)
        logging.debug("teardown bind-ip flow for iface=%s", iface)

    def setup_flows(self, flow_manager, iface, bridge):
        ipv4 = iface.bind_ip
        ipv6 = iface.bind_ipv6
        if ipv4 or ipv6:
            flow_manager.apply_bind_ip(bridge, iface, ipv4, ipv6)
            logging.debug("setup bind-ip flow for ipv4=%s, ipv6=%s, iface=%s has been done.", ipv4, ipv6, iface)

    def _is_equal_ips(self, old_ip, new_ip):
        return str(sorted(old_ip.strip().split(","))) == str(sorted(new_ip.strip().split(",")))

    def handle_interface_add_begin(self, flow_manager, iface, bridge, event):
        is_change = not (iface and self._is_equal_ips(iface.bind_ip, event.bind_ip) and self._is_equal_ips(iface.bind_ipv6, event.bind_ipv6))
        if is_change:
            self.teardown_flows(flow_manager, iface, bridge)
        return is_change

    def handle_interface_add_end(self, flow_manager, iface, bridge, is_change):
        if is_change and (iface.bind_ip or iface.bind_ipv6):
            self.setup_flows(flow_manager, iface, bridge)

    def handle_interface_change_begin(self, flow_manager, iface, bridge, old_event, new_event):
        is_change = old_event.other_config is not None and not (self._is_equal_ips(old_event.bind_ip, new_event.bind_ip) and self._is_equal_ips(old_event.bind_ipv6, new_event.bind_ipv6))
        if is_change:
            self.teardown_flows(flow_manager, iface, bridge)
        return is_change

    def handle_interface_change_end(self, flow_manager, iface, bridge, is_change):
        if is_change and (iface.bind_ip or iface.bind_ipv6):
            self.setup_flows(flow_manager, iface, bridge)
