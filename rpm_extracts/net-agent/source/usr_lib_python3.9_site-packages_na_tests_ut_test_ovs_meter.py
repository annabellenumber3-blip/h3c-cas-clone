#!/usr/bin/python3

import unittest
from net_agent.ovs_model import ovs_meter
from net_agent.ovs_model import ovs_object


class Mock_Meter(object):

    # Define Meter used as action of flow which describe qos parameter.
    def __init__(self):
        super(Mock_Meter, self).__init__()
        self.id = ['interface', 0]
        self.unit = "kbps"
        self.rate = 0
        self.burst = 0

    def __str__(self):
        return "domain=%s,id=%s,unit=%s,rate=%s,burst=%s" % (self.id[0], self.id[1], self.unit, self.rate, self.burst)


class TestOVSMeter(unittest.TestCase):

    # test OVSMeter class, _meters = [[br_name, iface_name, profile, ovs_meter_id] ... ], profile is the key to alloc ovs_meter_id from IdManager
    # used meter
    # meter: bridge=vswitch0, iface=vnet6, name=Qos_ipv6_vnet6_3, ovs_meter_id=2   (Qos_ipv6 --> netprofile.name)
    # meter: bridge=vswitch0, iface=vnet6, name=Qos_ipv6_vnet6_4, ovs_meter_id=12

    def test_ovsmeter_class(self):
        ovsmeter = ovs_meter.OVSMeter()
        meters = []
        bridge = ovs_object.Bridge('vswitch0', 0, True)
        interface1 = ovs_object.Interface("123321", "vnet0", 4, None, None, "vswitch0")
        interface2 = ovs_object.Interface("432321", "vnet1", 4, None, None, "vswitch0")
        meters.append([bridge, interface1, 'Qos_ipv6_vnet0_3', 2])
        meters.append([bridge, interface2, 'Qos_ipv6_vnet1_4', 12])
        meter = Mock_Meter()
        meter.id[1] = 4
        meter.rate = 10000
        meter.burst = 10000

        ovsmeter.update_meter(bridge, interface1, 'Qos_ipv6_vnet0_3', 2)
        ovsmeter.update_meter(bridge, interface2, 'Qos_ipv6_vnet1_4', 12)
        self.assertEqual(ovsmeter._meters, meters)
        self.assertEqual(ovsmeter.get_meter(bridge, interface1, meter, "Qos_test"), 2)

        ovsmeter.del_meter(bridge, interface2,  'Qos_ipv6_vnet1_4', 12)
        meters.pop(-1)
        self.assertEqual(ovsmeter._meters, meters)


if __name__ == '__main__':
    unittest.main()