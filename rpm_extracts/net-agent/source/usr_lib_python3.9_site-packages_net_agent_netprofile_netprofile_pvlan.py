#!/usr/bin/python3
import logging
import xml.etree.ElementTree as ET
import uuid
from . netprofile import NetProfile
from . netprofile import Rule
from .. base import net_lib

class PvlanRule(Rule):
    """ pvlan netprofile rule """
    def __init__(self):
        super(PvlanRule, self).__init__()
        self.primary_vlan = 0

    def __str__(self):
        return "primary_vlan=%s, action:%s" % (self.primary_vlan, self.action)

class PvlanAction(object):
    """ define PvlanAction used as action of flow which describe pvlan parameters """
    def __init__(self, pvlan_type="primary_vlan"):
        super(PvlanAction, self).__init__()
        self.pvlan_type = pvlan_type
        self.pvlan_vlan = 0

    def __str__(self):
        return "%s=%d" % (self.pvlan_type, self.pvlan_vlan)

class NetProfilePvlan(NetProfile):
    def __init__(self, md5=""):
        super(NetProfilePvlan, self).__init__(md5)

    def parse(self, root):
        if root is None:
            logging.error("root is None")
            return False

        self.type = root.attrib.get("type", "")
        self.name = root.attrib.get("name", "")
        self.uuid = root.attrib.get("uuid", str(uuid.uuid3(uuid.NAMESPACE_DNS, self.name)))
        self.version = root.attrib.get("version", "")

        if self.name == "":
            net_lib.Utility.set_log_lasterr("name is None")
            return False

        for child in root:
            if child.tag != "flow":
                continue

            if 'primary_vlan' not in child.attrib.keys():
                net_lib.Utility.set_log_lasterr("incomplete flow for pvlan: %s" % child.attrib)
                return False

            rule = PvlanRule()
            try:
                rule.primary_vlan = int(child.attrib.get("primary_vlan", ""))
                if rule.primary_vlan > 4094 or rule.primary_vlan < 2:
                    raise ValueError("pvlan primary_vlan is not in [2,4094]")
            except Exception as e:
                net_lib.Utility.set_log_lasterr("invalid pvlan primary_vlan, check your xml(%s)" % str(e))
                return False

            for item in child:
                if item.tag != "action":
                    continue
                rule.action = self.parse_extra_action(item)
                if rule.action is None:
                    net_lib.Utility.set_log_lasterr("failed to parse pvlan action: %s" % item)
                    return False
                self.rules.append(rule)

        return True

    def format_xml(self):
        xml = "\n"
        try:
            root = ET.Element("netprofile")
            root.attrib["type"] = self.type
            root.attrib["name"] = self.name
            root.attrib["uuid"] = self.uuid
            root.attrib["version"] = self.version
            for rule in self.rules:
                flow = ET.SubElement(root, "flow")
                flow.attrib["primary_vlan"] = str(rule.primary_vlan)
                action = ET.SubElement(flow, "action")
                self.format_extra_action(rule.action, action)
            xml = ET.tostring(root).decode().replace("><", ">\n<")
        except:
            logging.exception("catch exception")

        return xml

    def parse_extra_action(self, item):
        if 'primary_vlan' in item.attrib.keys():
            pvlan_type = "primary_vlan"
        elif 'isolated_vlan' in item.attrib.keys():
            pvlan_type = "isolated_vlan"
        elif 'group_vlan' in item.attrib.keys():
            pvlan_type = "group_vlan"
        else:
            net_lib.Utility.set_log_lasterr("invalid action for pvlan: %s" % item.attrib)
            return None

        act = PvlanAction(pvlan_type)
        try:
            act.pvlan_vlan = int(item.attrib.get(pvlan_type, ""))
            if act.pvlan_vlan > 4094 or act.pvlan_vlan < 2:
                raise ValueError("pvlan pvlan_vlan is not in [2,4094]")
        except Exception as e:
            net_lib.Utility.set_log_lasterr("invalid action pvlan vlan for pvlan, check your xml(%s)" % str(e))
            return None

        return act

    def format_extra_action(self, action, element):
        element.attrib[action.pvlan_type] = str(action.pvlan_vlan)
