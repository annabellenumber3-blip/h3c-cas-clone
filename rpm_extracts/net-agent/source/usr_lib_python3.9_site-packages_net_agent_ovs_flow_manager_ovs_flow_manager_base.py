    def _apply_bind_ip(self, cookie, table, base_priority, bridge, iface, ipv4, ipv6):
        mac = None
        if net_lib.GlobalConfig().enable_bind_mac and iface.attached_mac and net_lib.ValidatyCheck.check_mac(iface.attached_mac):
            mac = iface.attached_mac
        else:
            logging.warn("bind mac is disabled on %s, config=%s, mac=%s",
                         iface.name, net_lib.GlobalConfig().enable_bind_mac, iface.attached_mac)

        bind_ip_accept_action = OVSAction().add_set_bitmap(FLOW_REG_BIT_INDEX_BINDIP).add_resubmit(None, table)
        bind_mac_accept_action = OVSAction().add_set_bitmap(FLOW_REG_BIT_INDEX_BINDMAC).add_resubmit(None, table)

        def construct_bind_mac_flow(priority, mac_, action):
            flow = Flow(
                bridge=bridge.name,
                cookie=cookie,
                table=table,
                priority=base_priority + priority,
                actions=action,
                in_port=iface.ofport,
                reg_bind_ip=0,
                is_bind_mac=True)
            flow.matchfield.set_fields(dl_src=mac_)
            return flow

        def construct_bind_ip_accept_flow(priority, src, dl_type):
            flow = Flow(
                bridge=bridge.name,
                cookie=cookie,
                table=table,
                priority=base_priority + priority,
                actions=bind_ip_accept_action,
                in_port=iface.ofport,
                reg_bind_ip=0)
            flow.matchfield.set_fields(dl_src=mac, dl_type=dl_type)
            if dl_type == "ip":
                flow.matchfield.set_fields(nw_src=src)
            elif dl_type == "ipv6":
                flow.matchfield.set_fields(ipv6_src=src)
            return flow

        def construct_accpet_dhcpv4_flow(priority):
            flow = Flow(
                bridge=bridge.name,
                cookie=cookie,
                table=table,
                priority=base_priority + priority,
                actions=bind_ip_accept_action,
                in_port=iface.ofport)
            flow.matchfield.set_fields(dl_type="ip", nw_proto='udp', nw_src='0.0.0.0', tp_src='68', tp_dst='67', dl_src=mac, reg_bind_ip=0)
            return flow

        def construct_bind_arp_accept_flow(priority, nw_src, nw_dst, arp_op):
            flow = Flow(
                bridge=bridge.name,
                cookie=cookie,
                table=table,
                priority=base_priority + priority,
                actions=bind_ip_accept_action)
            flow.matchfield.set_fields(in_port=iface.ofport, dl_type='arp', nw_src=nw_src, nw_dst=nw_dst, arp_op=arp_op, reg_bind_ip=0)
            flow.matchfield.set_fields(dl_src=mac)
            return flow

        def construct_bind_nd_accept_flow(priority, nd_target, icmp_types):
            flow = Flow(
                bridge=bridge.name,
                cookie=cookie,
                table=table,
                priority=base_priority + priority,
                actions=bind_ip_accept_action)
            flow.matchfield.set_fields(in_port=iface.ofport, dl_type='ipv6', nw_proto="icmp6", nd_target=nd_target, icmp_type=icmp_types, reg_bind_ip=0)
            flow.matchfield.set_fields(dl_src=mac)
            return flow

        def construct_bind_ip_drop_flow(priority, dl_type):
            flow = Flow(
                bridge=bridge.name,
                cookie=cookie,
                table=table,
                priority=base_priority + priority,
                actions=OVSACTION_DROP)
            flow.matchfield.set_fields(in_port=iface.ofport, dl_type=dl_type, reg_bind_ip=0)
            return flow

        if not bridge:
            logging.info("bridge of %s doesn't exist.", iface.name)
            return

        if mac:
            flow_accept_mac = construct_bind_mac_flow(5, mac, bind_mac_accept_action)
            flow_drop_mac = construct_bind_mac_flow(2, None, OVSACTION_DROP)
            self._txn.add_flows([flow_accept_mac, flow_drop_mac])

        if ipv4:
            for ip in ipv4.split(","):
                if not net_lib.ValidatyCheck.check_ip(ip):
                    logging.warn("ipv4 address %s is invalid for Interface %s", ip, iface)
                    continue
                flow1_accept = construct_bind_ip_accept_flow(70, ip, "ip")
                flow2_accept = construct_bind_arp_accept_flow(60, ip, None, None)
                flow3_accept = construct_bind_arp_accept_flow(50, '0.0.0.0', ip, ARP_OP_INT_TYPE)
                self._txn.add_flows([flow1_accept, flow2_accept, flow3_accept])

            flow1_drop = construct_bind_ip_drop_flow(10, 'ip')
            flow2_drop = construct_bind_ip_drop_flow(0, 'arp')
            flow_accept_dhcpv4 = construct_accpet_dhcpv4_flow(20)
            self._txn.add_flows([flow1_drop, flow2_drop, flow_accept_dhcpv4])

        if ipv6:
            for ip in ipv6.split(","):
                if not net_lib.ValidatyCheck.check_ipv6(ip):
                    logging.warn("ipv6 address %s is invalid for Interface %s", ip, iface)
                    continue
                flow1_accept_ipv6 = construct_bind_ip_accept_flow(70, ip, "ipv6")
                flow3_accept_ipv6 = construct_bind_nd_accept_flow(60, ip, 135)
                flow5_accept_ipv6 = construct_bind_nd_accept_flow(50, ip, 136)
                self._txn.add_flows([flow1_accept_ipv6, flow3_accept_ipv6, flow5_accept_ipv6])

            if iface.attached_mac and net_lib.ValidatyCheck.check_mac(iface.attached_mac):
                ipv6_linklocal = net_lib.Utility.mac2ipv6_linklocal(iface.attached_mac)
                flow2_accept_ipv6 = construct_bind_ip_accept_flow(70, ipv6_linklocal, "ipv6")
                flow4_accept_ipv6 = construct_bind_nd_accept_flow(60, ipv6_linklocal, 135)
                flow6_accept_ipv6 = construct_bind_nd_accept_flow(50, ipv6_linklocal, 136)
                self._txn.add_flows([flow2_accept_ipv6, flow4_accept_ipv6, flow6_accept_ipv6])

            flow1_drop_ipv6 = construct_bind_ip_drop_flow(10, 'ipv6')
            self._txn.add_flows([flow1_drop_ipv6])

    def _del_bind_ip(self, cookie, bridge, iface):
        flow = Flow(
            bridge=bridge.name,
            cookie="%d/-1" % cookie,
            in_port=iface.ofport)
        self._txn.del_flows(flow)

