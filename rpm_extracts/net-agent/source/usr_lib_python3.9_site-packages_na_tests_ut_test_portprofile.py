#!/usr/bin/python3

import unittest
from net_agent.netprofile import portprofile
from na_tests.mock import mock


class TestPortProfile(unittest.TestCase):

    def test_parse_form_xml(self):
        portprofile_object = portprofile.PortProfile()

        xml = '<portprofile name="test1" type="compose"><netprofile name="qos_test1" type="qos" /></portprofile>'
        portprofile_object.parse_from_xml(xml)

        self.assertEqual(portprofile_object.name, 'test1')
        self.assertEqual(portprofile_object.type, 'compose')
        self.assertEqual(portprofile_object.netprofiles[-1].name, 'qos_test1')
        self.assertEqual(portprofile_object.netprofiles[-1].type, 'qos')

    # mocking out the annoyed message:
    # ERROR:root:acl rule is incomplete: {'actionss': 'accept', 'priority': '4', 'direction': 'in', 'statematch': 'false'}

    @mock.patch('logging.error')
    def test_format_xml(self, _):
        portprofile_object = portprofile.PortProfile()
        xml = '<portprofile name="test1" type="compose"><netprofile name="qos_test1" type="qos" /></portprofile>'
        portprofile_object.parse_from_xml(xml)
        test_xml = '<portprofile name="test1" type="compose">\n<netprofile name="qos_test1" type="qos" />\n</portprofile>'
        self.assertEqual(portprofile_object.format_xml(), test_xml)


if __name__ == '__main__':
    unittest.main()