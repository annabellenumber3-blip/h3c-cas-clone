#!/usr/bin/python3

import unittest
from net_agent import na_ctl
from na_tests.common import testbase
import logging
from na_tests.mock import mock
import argparse


class TestNaCtl(testbase.TestCaseBase):

    def setUp(self):
        super(TestNaCtl, self).setUp()

    def test_parse_argvs_normal(self):
        expect_output = [argparse.Namespace(cmd='dump', loglevel=None, retry=None, timeout=None)]
        self.assertListEqual(na_ctl.parser_individual_args(["dump"]), expect_output)

        expect_output = [argparse.Namespace(cmd='nwfilter', loglevel=None, nwfilter='name', subcmd='delete', retry=None, timeout=None)]
        self.assertListEqual(na_ctl.parser_individual_args(["nwfilter", "delete", "name"]), expect_output)

        expect_output = [argparse.Namespace(cmd='portprofile', loglevel=None, name=None, subcmd='dump', retry=None, timeout=None)]
        self.assertListEqual(na_ctl.parser_individual_args(["portprofile", "dump"]), expect_output)

        expect_output = [argparse.Namespace(cmd='log-level', loglevel='debug', retry=None, timeout=None)]
        self.assertListEqual(na_ctl.parser_individual_args(["log-level", "debug"]), expect_output)

        with open(self._log_file, 'r') as f:
            lines = f.readlines()
            self.assertIn("len:1, ['dump']", lines[0])
            self.assertIn("len:3, ['nwfilter', 'delete', 'name']", lines[1])
            self.assertIn("len:2, ['portprofile', 'dump']", lines[2])
            self.assertIn("len:2, ['log-level', 'debug']", lines[3])

    def mock_err(self, message):
        import sys
        sys.exit(-1)

    @mock.patch('argparse.ArgumentParser.error')
    def test_parse_argvs_miss(self, mock_error):
        def mock_err(self, message):
            import sys
            sys.exit(-1)

        mock_error.side_effect = mock_err
        actual_output = None
        try:
            actual_output = na_ctl.parser_individual_args(["nwfilter", "delete"])
        except BaseException as e:
            if e:
                logging.warn("exception when parser_individual_args")
        self.assertEqual(actual_output, None)

    @mock.patch('argparse.ArgumentParser.error')
    def test_parse_argvs_error(self, _):
        actual_output = None
        try:
            actual_output = na_ctl.parser_individual_args(["dump1"])
        except BaseException as e:
            if e:
                logging.warn("exception when parser_individual_args")
        self.assertEqual(actual_output, None)

        try:
            actual_output = na_ctl.parser_individual_args(["deletee", "name"])
        except BaseException as e:
            if e:
                logging.warn("exception when parser_individual_args")
        self.assertEqual(actual_output, None)

        with open(self._log_file, 'r') as f:
            lines = f.readlines()
            self.assertIn("len:1, ['dump1']", lines[0])
            self.assertIn("len:2, ['deletee', 'name']", lines[2])

    def tearDown(self):
        super(TestNaCtl, self).tearDown()


if __name__ == '__main__':
    unittest.main()
