#!/usr/bin/python3
import logging
from . import net_lib
import os

LOG_FILE = os.getenv("NA_LOGDIR", "/var/log/net-agent") + "/cas_net_agentd.log"


def log_init():
    formatter = logging.Formatter(
        '%(asctime)s|%(levelname)s|%(funcName)s:%(lineno)d|%(message)s',
        '%m-%d %H:%M:%S')

    level = get_log_level("net_agentd")
    logging.basicConfig(level=level, filename=LOG_FILE)
    logging.getLogger().addHandler(logging.StreamHandler())
    files_preserve = []
    for handler in logging.getLogger().handlers:
        handler.setFormatter(formatter)
        files_preserve.append(handler.stream)
    return files_preserve


def get_log_level(module):
    logdict = {'DEBUG': logging.DEBUG,
               'INFO': logging.INFO,
               'WARNING': logging.WARNING,
               'ERROR': logging.ERROR}
    level = logging.DEBUG

    # match string bond is ' ' or '='
    mstr = "Logging_Level_%s" % module
    contents = net_lib.Utility.read_file("/etc/cvk/util_cvk_log.conf").strip()
    for line in contents.splitlines():
        if line.startswith(mstr):
            strlevel = line.split('=')[-1].strip()
            level = logdict.get(strlevel, level)
            break

    return level
