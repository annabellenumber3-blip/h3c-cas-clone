#!/usr/bin/python3
import logging
import xml.etree.ElementTree as ET
from . netprofile import NetProfileReference
from .. base import net_lib


class PortProfile(object):
    """
    Define PortProfile used by iface, define funtions parse and format portprofile.xml.
    The value of name is same with "port-profile" in libvirt xml, a portprofile can include multi-netprofiles.
    """
    def __init__(self, md5=""):
        super(PortProfile, self).__init__()
        self.type = ""
        self.name = ""
        self.md5 = md5
        self.netprofiles = []

    # parse from xml content to instantiate portprofile, return True if parse success.
    def parse_from_xml(self, xml):
        success = False
        if xml:
            try:
                success = self._parse_from_xml(xml)
            except:
                logging.exception("parse %s file exception", xml)
        return success

    def _parse_from_xml(self, xml):
        if xml == "":
            return False

        root = ET.fromstring(xml)
        self.type = root.attrib.get("type", "")
        self.name = root.attrib.get("name", "")

        if self.type != "compose" or self.name == "":
            net_lib.Utility.set_log_lasterr("wrong type %s or name" % self.type)
            return False

        for child in root:
            if child.tag != "netprofile":
                continue
            netprofile = NetProfileReference()
            netprofile.name = child.attrib.get("name", "")
            netprofile.type = child.attrib.get("type", "")
            self.netprofiles.append(netprofile)

        return True

    # return portproflie content as xml format
    def format_xml(self):
        xml = "\n"
        try:
            root = ET.Element("portprofile")
            root.attrib["type"] = str(self.type)
            root.attrib["name"] = str(self.name)
            for netprofile in self.netprofiles:
                child = ET.SubElement(root, "netprofile")
                child.attrib["type"] = str(netprofile.type)
                child.attrib["name"] = str(netprofile.name)
            xml = ET.tostring(root).decode().replace("><", ">\n<")
        except:
            logging.exception("catch exception")

        return xml

    def __str__(self):
        return "name=%s" % self.name
