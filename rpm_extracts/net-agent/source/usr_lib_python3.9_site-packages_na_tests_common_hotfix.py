from net_agent.base import net_lib
import os
import glob
from utility import Utility


class Mock_cmd_output_lines(object):

    def __init__(self, func):
        self._func = func

    def __call__(self, cmd):
        if cmd.rfind('virsh nwfilter-list') != -1:
            rets = '''
 UUID                                  Name
------------------------------------------------------------------
'''
            test_dir = os.environ['NA_TESTDIR']
            for filename in glob.glob(test_dir + "/nwfilter/*.xml"):
                items = os.path.split(filename)[1].split('.', 1)
                rets += ' 825d0f36-3f47-4001-8ec2-cd1f8c967f63  %s\n' % items[0]
            return rets.splitlines()

        return self._func(cmd)


class Mock_uplink(object):

    def __call__(self, br, is_all=False):
        uplinks = []
        ifaces = Utility.cmd_execute("ovs-vsctl -t 5 list-ifaces %s" % br, True)[1].split()
        for iface in ifaces:
            if iface.startswith("phy"):
                uplinks.append(iface.strip())
                if not is_all:
                    break

        return uplinks if is_all else (uplinks[0] if len(uplinks) else None)


def do_mock():
    net_lib.Utility.cmd_output_lines = Mock_cmd_output_lines(net_lib.Utility.cmd_output_lines)
    net_lib.Utility.uplink = Mock_uplink()
    print('do_mock called')


