class TestProcessRestart(testbase.TestCaseBase):
    def myinit(self):
        super(TestProcessRestart, self).myinit()
        self._start_ovs = True
        self._start_netagent = True

    def test_restart_bindip(self):
        ret = Utility.cmd_execute('''
cd {test_dir}
ovs-vsctl add-br br0 -- set bridge br0 fail-mode=standalone -- \
    add-port br0 p1 -- set interface p1 ofport_request=1 type=dummy -- \
    add-port br0 p2 -- set interface p2 ofport_request=2 type=dummy

ovs-ofctl mod-port br0 br0 noforward
ovs-appctl revalidator/purge
na-ctl --retry=-1 add-listen --id=1 --event="set Interface p1 other_config:bind-ip=1.2.3.3"
ovs-vsctl set Interface p1 other_config:bind-ip=1.2.3.3
na-ctl wait --id=1
ovs-appctl netdev-dummy/receive p1 'in_port(1),eth(src=50:54:00:00:00:0c,dst=50:54:00:00:00:05),eth_type(0x0800),ipv4(src=1.2.3.4,dst=1.2.3.5,proto=1,tos=0,ttl=64,frag=no),icmp(type=8,code=0)'
ovs-appctl time/warp 100
ovs-appctl dpif/dump-flows br0 > br0_flows.txt
grep 'actions:drop' br0_flows.txt
if test $? -ne 0; then
    exit 1
fi

pid1=$(cat ovs-vswitchd.pid)
kill $pid1
sleep 1
ovs-vswitchd --enable-dummy=override --disable-system --detach --no-chdir --pidfile --log-file -vvconn -vofproto_dpif -vunixctl
sleep 1
pid2=$(cat ovs-vswitchd.pid)
if test $pid1 -eq $pid2; then
    exit 2
fi

ovs-ofctl mod-port br0 br0 noforward
ovs-appctl revalidator/purge
ovs-appctl time/warp 2000
ovs-appctl netdev-dummy/receive p1 'in_port(1),eth(src=50:54:00:00:00:0c,dst=50:54:00:00:00:05),eth_type(0x0800),ipv4(src=1.2.3.4,dst=1.2.3.5,proto=1,tos=0,ttl=64,frag=no),icmp(type=8,code=0)'
ovs-appctl dpif/dump-flows br0 > br0_flows1.txt
grep 'actions:drop' br0_flows1.txt
if test $? -ne 0; then
    exit 3
fi

ovs-appctl revalidator/purge
ovs-appctl time/warp 2000
ovs-appctl netdev-dummy/receive p1 'in_port(1),eth(src=50:54:00:00:00:0c,dst=50:54:00:00:00:05),eth_type(0x0800),ipv4(src=1.2.3.3,dst=1.2.3.5,proto=1,tos=0,ttl=64,frag=no),icmp(type=8,code=0)'
ovs-appctl dpif/dump-flows br0 > br0_flows2.txt
grep 'actions:2' br0_flows2.txt
if test $? -ne 0; then
    exit 4
fi

na-ctl --retry=-1 add-listen --id=1 --event="remove Interface p1 other_config:bind-ip"
ovs-vsctl remove Interface p1 other_config bind-ip
na-ctl wait --id=1
ovs-appctl revalidator/purge
ovs-appctl time/warp 2000
ovs-appctl netdev-dummy/receive p1 'in_port(1),eth(src=50:54:00:00:00:0c,dst=50:54:00:00:00:05),eth_type(0x0800),ipv4(src=1.2.3.4,dst=1.2.3.5,proto=1,tos=0,ttl=64,frag=no),icmp(type=8,code=0)'
ovs-appctl dpif/dump-flows br0 > br0_flows3.txt
grep 'actions:2' br0_flows3.txt
if test $? -ne 0; then
    exit 5
fi
'''.format(**{'test_dir': self._test_dir}), True)
        self.assertEqual(ret[0], 0, "test restart bind ip failed: shell return code:%d" % ret[0])

