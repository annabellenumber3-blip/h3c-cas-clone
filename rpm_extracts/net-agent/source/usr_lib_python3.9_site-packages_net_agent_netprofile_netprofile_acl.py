#!/usr/bin/python3
import logging
from . netprofile import NetProfile
from .. base import net_lib
from .. ovs_model.ovs_object import FlowMatchField, DIRECTION_IN, ACTION_DROP, ACTION_ACCEPT, OVSACTION_DROP


class AclAction(object):

    # define AclAction used as action of flow which describe acl parameter
    def __init__(self):
        super(AclAction, self).__init__()
        self.target = OVSACTION_DROP

    def __str__(self):
        return self.target


class NetProfileAcl(NetProfile):

    def __init__(self, md5=""):
        super(NetProfileAcl, self).__init__(md5)

    def parse_extra_action(self, item):
        if [k for k in ["target"] if k not in item.attrib.keys()]:
            net_lib.Utility.set_log_lasterr("acl/vfw action is incomplete: %s" % item.attrib)
            return None

        act = AclAction()
        act.target = item.attrib.get("target", ACTION_DROP)
        if act.target not in (ACTION_DROP, ACTION_ACCEPT):
            net_lib.Utility.set_log_lasterr("invalid acl/vfw action, action must be accept/drop")
            return None

        return str(act)

    # return acl action content as xml format
    def format_extra_action(self, action_str, element):
        element.attrib["target"] = action_str
