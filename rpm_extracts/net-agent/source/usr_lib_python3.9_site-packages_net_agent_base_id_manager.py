#!/usr/bin/python3

import logging


class IdManager(object):

    _singleton = None
    _inited = False

    def __new__(cls):
        if not cls._singleton:
            cls._singleton = super(IdManager, cls).__new__(cls)
        return cls._singleton

    def __init__(self):
        if not (self._singleton and self._inited):
            super(IdManager, self).__init__()
            self._dict = {}  # [cat] = {name->id}
            self._inited = True

    '''alloc id by name'''
    def alloc_id(self, cat, name, start=1, step=1):
        if name is None:
            return None
        sub_dict = self._dict.setdefault(cat, {})
        s = set(sub_dict.values())
        val = start
        while val in s:
            val += step
        return sub_dict.setdefault(name, val)

    '''find id by name'''
    def find_id(self, cat, name):
        if name is None:
            return None
        return self._dict.get(cat, {}).get(name, None)

    '''del id by name'''
    def free_id(self, cat, name):
        if name is None:
            return None
        if self._dict.get(cat, {}).pop(name, None) is None:
            logging.error("free_id: free id %s which is not in the map[%s]", name, cat)
