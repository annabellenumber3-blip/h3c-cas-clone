import os
import unittest
import ovs_inst
import na_inst
import shutil
import logging
import hotfix
import re
from na_tests.common.utility import Utility


# NA_TEST_ROOT is the tests run's root directory, not include concrete test
# na_testrootdir = os.path.abspath(os.getenv("NA_TEST_ROOT", os.path.split(os.path.realpath(__file__))[0] + "/../testsuite.dir"))
# change na_testrootdir to /tmp as AF_UNIX PATH is limited 108
na_testrootdir = "/tmp/na_tests.dir"

class ExTestResult(unittest.TestResult):

    def stop(self):
        super(ExTestResult, self).stop()
        if self._testcase:
            self._testcase.on_stop()


class TestCaseBase(unittest.TestCase):

    ovs_version = None

    def myinit(self):
        self._test_dir = na_testrootdir + '/%s.%s' % (self.__class__.__name__, self._testMethodName) + '/'
        self._log_file = self._test_dir + 'na_test.log'
        self._start_ovs = False
        self._ovs_inst = None
        self._start_netagent = False
        self._na_inst = None
        unittest.installHandler()
        self._test_result = ExTestResult()
        self._test_result._testcase = self
        unittest.registerResult(self._test_result)

    def on_stop(self):
        self.tearDown()

    def setUp(self):
        self.myinit()

        self.prepare_environment()
        if self._start_ovs:
            self._ovs_inst = ovs_inst.OvsInst(self._test_dir)
            if not self._ovs_inst.start():
                self.skipTest('ovs can not be launched')

        if self._start_netagent:
            extra_code = self.replace_code()
            self._na_inst = na_inst.NaInst(self._test_dir, extra_code)
            if not self._na_inst.start():
                self.skipTest('net agent can not be launched')

    def tearDown(self):
        if self._ovs_inst:
            self._ovs_inst.cleanup()
        if self._na_inst:
            self._na_inst.cleanup()

        self.cleanup_environment()
        unittest.removeResult(self._test_result)
        # unittest.removeHandler()  # uncomment the statement will cause next test not react signal, so don't UNcomment

    def replace_code(self):
        return hotfix.replace_code() if (self._start_ovs and self._start_netagent) else None

    @property
    def nwfilter_dir(self):
        return self._test_dir + '/nwfilter'

    @property
    def netprofile_qos_dir(self):
        return self._test_dir + '/netprofile/qos'

    @property
    def netprofile_acl_dir(self):
        return self._test_dir + '/netprofile/acl'

    @property
    def netprofile_drain_dir(self):
        return self._test_dir + '/netprofile/drain'

    @property
    def portprofile_dir(self):
        return self._test_dir + '/portprofile'

    def prepare_environment(self):
        if os.path.exists(self._test_dir):
            shutil.rmtree(self._test_dir)  # make a clean directory before test
        for path_ in [self._test_dir, self.nwfilter_dir, self.netprofile_qos_dir, self.netprofile_acl_dir, self.netprofile_drain_dir, self.portprofile_dir]:
            if not os.path.exists(path_):
                os.makedirs(path_)

        run_in_dist = False
        thispath = os.path.split(os.path.realpath(__file__))[0]
        if not os.path.exists(thispath + "/../../scripts/na-test"):  # we are running under distpackage dir
            run_in_dist = True
        if not run_in_dist:
            os.environ['PATH'] = thispath + "/../../scripts:" + os.environ['PATH']
            os.environ['PYTHONPATH'] = (thispath + "/../../:" + os.environ['PYTHONPATH']) if 'PYTHONPATH' in os.environ else (thispath + "/../../")

        os.environ['NA_RUNDIR'] = self._test_dir
        os.environ['NA_CONFDIR'] = self._test_dir
        os.environ['NA_LOGDIR'] = self._test_dir
        os.environ['NA_TESTDIR'] = self._test_dir
        os.environ['OVS_DBDIR'] = self._test_dir
        os.environ['OVS_RUNDIR'] = self._test_dir
        os.environ['OVS_LOGDIR'] = self._test_dir

        # os.environ['OVS_CONFDIR'] = self._test_dir
        # os.environ['OVS_SYSCONFDIR'] = self._test_dir
        for handle in logging.root.handlers[:]:
            logging.root.removeHandler(handle)
        logging.basicConfig(
            level=logging.DEBUG,
            format='%(asctime)s %(levelname)7s %(funcName)s:%(lineno)d %(message)s',
            datefmt='%m-%d %H:%M:%S',
            filename=self._log_file)

    def _should_rm_test_dir(self):
        result = self._resultForDoCleanups
        if result.shouldStop:
            return False
        if len(result.errors) > 0 and result.errors[-1][0].id() == self.id():
            return False
        if len(result.failures) > 0 and result.failures[-1][0].id() == self.id():
            return False
        return True

    def cleanup_environment(self):
        if self._should_rm_test_dir():
            try:
                shutil.rmtree(self._test_dir)  # no need to remove nwfilter_dir etc
            except BaseException as e:
                if e:
                    logging.warn("exception when rmtree %s" % self._test_dir)

        for handle in logging.root.handlers[:]:
            logging.root.removeHandler(handle)

    @staticmethod
    def get_ovs_version():
        if TestCaseBase.ovs_version is None:
            ret = Utility.cmd_execute("ovs-vswitchd --version")
            if ret[0] == 0:
                m = re.search("(\d+).(\d+).(\d+)$", ret[1].splitlines()[0])
                if m:
                    TestCaseBase.ovs_version = (int(m.group(1)), int(m.group(2)), int(m.group(3)))
        return TestCaseBase.ovs_version

    def check_meter_test(self):
        version = TestCaseBase.get_ovs_version()
        if version < (2, 9, 0):
            self.skipTest("Not support meter or meter support is not mature on ovs %d.%d.%d" % (version[0], version[1], version[2]))
