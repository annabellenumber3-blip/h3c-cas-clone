class ValidatyCheck(object):

    def __init__(self):
        pass

    @staticmethod
    def check_int(v, start, end):
        return True if (isinstance(v, int) or v.isdigit()) and (start <= int(v) <= end) else False

    @staticmethod
    def check_mac(mac):
        return True if mac is None or re.match("([0-9a-f]{2}:){6}$", mac.lower() + ":") else False

    @staticmethod
    def check_ip(v, is_mask=False):
        if v is None:
            return True
        if is_mask and (isinstance(v, int) or v.isdigit()):
            return True if 0 <= int(v) <= 32 else False
        try:
            # raise if invalid format
            re.match("([0-9]{1,3}.){4}$", v + ".").group()
            socket.inet_aton(v)
        except:
            return False
        else:
            return True

    @staticmethod
    def check_ipv6(v, is_mask=False):
        if v is None:
            return True
        if is_mask and (isinstance(v, int) or v.isdigit()):
            return True if 0 <= int(v) <= 128 else False
        try:
            socket.inet_pton(socket.AF_INET6, v)
        except socket.error:
            return False
        return True

    @staticmethod
    def check_nw_proto(v):
        return True if v is None else ValidatyCheck.check_int(v, 0, 255)

    @staticmethod
    def check_icmp(v, is_mask=False):
        end = 8 if is_mask else 255
        return True if v is None else ValidatyCheck.check_int(v, 0, end)

    @staticmethod
    def check_tp_port(v, is_mask=False):
        end = 16 if is_mask else 65535
        return True if v is None else ValidatyCheck.check_int(v, 0, end)

    @staticmethod
    def check_matchfield(argv):
        if not ValidatyCheck.check_mac(argv.dl_src):
            return False
        if not ValidatyCheck.check_mac(argv.dl_src_mask):
            return False
        if not ValidatyCheck.check_mac(argv.dl_dst):
            return False
        if not ValidatyCheck.check_mac(argv.dl_dst_mask):
            return False

        if not ValidatyCheck.check_ip(argv.nw_src, is_mask=False):
            return False
        if not ValidatyCheck.check_ip(argv.nw_src_mask, is_mask=True):
            return False
        if not ValidatyCheck.check_ip(argv.nw_dst, is_mask=False):
            return False
        if not ValidatyCheck.check_ip(argv.nw_dst_mask, is_mask=True):
            return False

        if not ValidatyCheck.check_ipv6(argv.ipv6_src, is_mask=False):
            return False
        if not ValidatyCheck.check_ipv6(argv.ipv6_src_mask, is_mask=True):
            return False
        if not ValidatyCheck.check_ipv6(argv.ipv6_dst, is_mask=False):
            return False
        if not ValidatyCheck.check_ipv6(argv.ipv6_dst_mask, is_mask=True):
            return False

        if not ValidatyCheck.check_icmp(argv.icmp_type, is_mask=False):
            return False
        if not ValidatyCheck.check_icmp(argv.icmp_type_mask, is_mask=True):
            return False
        if not ValidatyCheck.check_icmp(argv.icmp_code, is_mask=False):
            return False
        if not ValidatyCheck.check_icmp(argv.icmp_code_mask, is_mask=True):
            return False

        if not ValidatyCheck.check_tp_port(argv.tp_src, is_mask=False):
            return False
        if not ValidatyCheck.check_tp_port(argv.tp_src_mask, is_mask=False):  # in parse tp_src_mask is not mask, is_mask set to False
            return False
        if not ValidatyCheck.check_tp_port(argv.tp_dst, is_mask=False):
            return False
        if not ValidatyCheck.check_tp_port(argv.tp_dst_mask, is_mask=False):
            return False

        return True

    @staticmethod
    def check_flow(argv):
        return False if not ValidatyCheck.check_int(argv.priority, 0, 1000) else ValidatyCheck.check_matchfield(argv.matchfield)


class Network(object):

    NETWORK_MESSAGE_TYPE_NONE = 0
    NETWORK_MESSAGE_TYPE_CMDLINE = 1
    NETWORK_MESSAGE_TYPE_CMDLINE_REPLY = 2

    NET_AGENT_UNIX_PATH = os.path.join(NET_AGENT_RUN_DIR, 'net-agent.sock')

    MESSAGE_LENGTH_STRUCT_FORMAT = '!ii'
    MESSAGE_LENGTH_STRUCT_CALCSIZE = struct.calcsize(MESSAGE_LENGTH_STRUCT_FORMAT)

    def __init__(self):
        pass

    @staticmethod
    def socket_send_message(socket_, type_, str_):
        assert isinstance(type_, int)
        message = struct.pack(Network.MESSAGE_LENGTH_STRUCT_FORMAT, len(str_), type_) + str_
        socket_.sendall(message)

    @staticmethod
    def socket_recv_message(socket_):
        def recv_expected_size(size):
            data = b''
            while len(data) < size:
                d = socket_.recv(size - len(data))
                if d:
                    data += d
                else:
                    return b''
            return data

        type_, str_ = Network.NETWORK_MESSAGE_TYPE_NONE, b''
        header = recv_expected_size(Network.MESSAGE_LENGTH_STRUCT_CALCSIZE)
        if header:
            str_len, type_ = struct.unpack(Network.MESSAGE_LENGTH_STRUCT_FORMAT, header)
            str_ = recv_expected_size(str_len)
        return type_, str_

    @staticmethod
    def data_serialize(data):
        return pickle.dumps(data)

    @staticmethod
    def data_unserialize(data):
        return pickle.loads(data)


class Utility(object):

    DEVNULL = None

    def __init__(self):
        pass

    @staticmethod
    def _dev_null_fd():
        if not Utility.DEVNULL:
            Utility.DEVNULL = open(os.devnull, "wb")
        return Utility.DEVNULL

    @staticmethod
    def cmd_execute(cmd, shell=False, input_=None, log_err=True, log_dbg=True):
        cmd2 = cmd
        if not shell and not isinstance(cmd, list):
            cmd2 = cmd.split()

        input_ = input_ if isinstance(input_, bytes) or input_ is None else input_.encode()
        p = subprocess.Popen(cmd2, shell=shell, stdin=subprocess.PIPE,
                             close_fds=True, stderr=subprocess.PIPE, stdout=subprocess.PIPE)
        stdout, stderr = p.communicate(input_)
        stdout, stderr = stdout.decode(), stderr.decode()
        if p.returncode != 0 and log_err:
            logging.error("Failed to execute cmd: %s :%s", cmd, stderr.strip())
        else:
            in_ = " < %s " % input_ if input_ else ""
            if log_dbg:
                logging.debug("Successful to execute cmd: %s %s:%s", cmd, in_, stdout.strip())
        return (p.returncode, stdout, stderr)

    @staticmethod
    def cmd_output_string(cmd):
        return Utility.cmd_execute(cmd)[1]

    @staticmethod
    def cmd_output_lines(cmd):
        return Utility.cmd_output_string(cmd).strip().splitlines()

    @staticmethod
    def cmd_call(cmd):
        cmd = cmd if isinstance(cmd, list) else cmd.split()
        return subprocess.call(cmd, close_fds=True, stdout=Utility._dev_null_fd(), stderr=Utility._dev_null_fd())

    @staticmethod
    def uplink(br, is_all=False):
        uplinks = []
        ifaces = Utility.cmd_execute("ovs-vsctl -t 5 list-ifaces %s" % br, True)[1].split()
        for iface in ifaces:
            if os.path.exists("/sys/class/net/" + iface) and \
                (os.readlink("/sys/class/net/" + iface).startswith("../../devices/platform") \
                 or os.readlink("/sys/class/net/" + iface).startswith("../../devices/pci")) \
                or iface.startswith("pci_"):
                uplinks.append(iface.strip())
                if not is_all:
                    break

        return uplinks if is_all else (uplinks[0] if len(uplinks) else None)

    @staticmethod
    def iface_ports(br):
        virtual_ports = []
        ifaces = Utility.cmd_execute("ovs-vsctl -t 5 list-ifaces %s" % br, True)[1].split()
        for iface in ifaces:
            if os.path.exists("/sys/class/net/" + iface) and (os.readlink("/sys/class/net/" + iface).startswith("../../devices/virtual")):
                virtual_ports.append(iface.strip())

        return virtual_ports
    
    @staticmethod
    def get_info_from_ovsdb(iface_name, attri):
        if attri == 'mtu':
            cmd = "ovs-vsctl get interface %s mtu" % iface_name
            mtu = Utility.cmd_output_string(cmd).strip()
            return mtu if mtu else '1500'
        if attri == 'tag':
            cmd = "ovs-vsctl get port %s tag" % iface_name
            tag = Utility.cmd_output_string(cmd).strip()
            return tag if tag else '[]'
        if attri == 'path':
            cmd = "ovs-vsctl get interface %s options:vhost-server-path" % iface_name
            path = Utility.cmd_output_string(cmd).strip()
            return path if path else ""

    @staticmethod
    def get_vnet_info_from_xml(iface):
        xml_str = Utility.cmd_execute("virsh dumpxml %s" % iface.external_ids['vm-id'], log_dbg=False)[1]
        try:
            root = ET.fromstring(xml_str)
            if root.tag == "domstatus":
                root = root.find("domain")
            for interface in root.find("devices").findall("interface"):
                itype = interface.get("type","")
                if itype == "bridge" or itype == "vhostuser":
                    mac = interface.find("mac").get("address", "")
                    if mac != iface.external_ids['attached-mac']:
                        continue
                    info = dict()
                    vlan_tag = interface.find('vlan')
                    if vlan_tag is not None:
                        info['tag'] = vlan_tag.find('tag').get("id", "1")
                    else:
                        logging.error("can't get vlan tag from vm %s, get from ovsdb", iface.external_ids['vm-id'])
                        info['tag'] = Utility.get_info_from_ovsdb(iface.name, 'tag')

                    mtu_tag = interface.find("mtu")
                    if mtu_tag is not None:
                        info['mtu'] = mtu_tag.get("size", "1500")
                    else:
                        logging.error("can't get mtu from vm %s, get from ovsdb", iface.external_ids['vm-id'])
                        info['mtu'] = Utility.get_info_from_ovsdb(iface.name, 'mtu')

                    info['br_dp_type'] = 'system'  # just dpdk nic use netdev
                    if itype == "vhostuser": # dpdk vnet
                        info['type'] = 'dpdkvhostuserclient'
                        info['br_dp_type'] = 'netdev'
                        path = interface.find("source").get('path', "")
                        if not path:
                            path = Utility.get_info_from_ovsdb(iface.name, 'path')
                        info['path'] = path

                    bandwidth_tag = interface.find('bandwidth')
                    if bandwidth_tag is not None:
                        info['bandwidth'] = dict()
                        inbound = bandwidth_tag.find('inbound')
                        if inbound is not None:
                            inbound_dict = dict()
                            inbound_dict['average'] = inbound.get('average','0')
                            inbound_dict['peak'] = inbound.get('peak','0')
                            inbound_dict['burst'] = inbound.get('burst','0')
                            info['bandwidth']['inbound'] = inbound_dict
                        outbound = bandwidth_tag.find('outbound')
                        if outbound is not None:
                            outbound_dict = dict()
                            outbound_dict['average'] = str(int(outbound.get('average','0')) * 8)
                            outbound_dict['peak'] = str(int(outbound.get('peak','0')) * 8)
                            outbound_dict['burst'] = str(int(outbound.get('burst','0')) * 8)
                            info['bandwidth']['outbound'] = outbound_dict

                    logging.debug("get vnet info: %s" % info)
                    return info

        except Exception as e:
            logging.error("get vnet info from vm %s failed(%s)", iface.external_ids['vm-id'], str(e))
        return None

    @staticmethod
    def get_vnet_info_with_try(iface, cnt=20):
        for n in range(cnt):
            logging.debug("trying %s time to get %s vnet info", str(n+1), iface.name)
            info = Utility.get_vnet_info_from_xml(iface)
            if info is not None:
                return info
            else:
                time.sleep(0.02)

        return None

    @staticmethod
    def read_file(filename, exception=False):
        content = ""
        try:
            with open(filename, 'r') as f:
                content = f.read()
        except Exception as e:
            if e:
                logging.warn("exception when read file1: %s", filename)
            if exception:
                raise e

        return content

    @staticmethod
    def write_file(filename, content=""):
        success = False
        # noinspection PyBroadException
        try:
            with open(filename, 'w') as f:
                f.write(content)
            success = True
        except:
            logging.warn("exception when write file: %s", filename)
        return success

    @staticmethod
    def wait_util(func, count, interval):
        for _ in range(count):
            if func():
                return True
            if interval > 0:
                time.sleep(interval)
        return False

    @staticmethod
    def save_file(filename, content):
        success = False
        # noinspection PyBroadException
        try:
            write_file_tmp = "%s.tmp" % filename
            if Utility.wait_util(lambda: Utility.write_file(write_file_tmp, content), 3, 0.5):
                os.renames(write_file_tmp, filename)
                success = True
            else:
                logging.warn("write file %s failed", filename)
                if os.path.exists(write_file_tmp):
                    os.remove(write_file_tmp)
        except Exception as e:
            logging.warn("exception(%s) when write file: %s", str(e), filename)
        return success

    @staticmethod
    def mac2ipv6_linklocal(mac):
        # convert mac addr to ipv6 link local (rfc 4862)
        # only accept MACs separated by a colon
        parts = mac.split(":")

        # modify parts to match IPv6 value
        parts.insert(3, "ff")
        parts.insert(4, "fe")
        parts[0] = "%x" % (int(parts[0], 16) ^ 2)
        # format output
        ipv6Parts = []
        for i in range(0, len(parts), 2):
            ipv6Parts.append("".join(parts[i:i + 2]))
        return "fe80::%s" % (":".join(ipv6Parts))

    @staticmethod
    def calculate_md5(content):
        h = hashlib.md5()
        h.update(content.encode('utf-8'))
        return h.hexdigest()

