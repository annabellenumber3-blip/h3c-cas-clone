class GlobalConfig(object):

    _singleton = None

    def __new__(self):
        if not self._singleton:
            self._singleton = super(GlobalConfig, self).__new__(self)
        return self._singleton

    def __init__(self):
        super(GlobalConfig, self).__init__()

    def reload(self):
        self._default_dict = {
            "enable_state": "1",
            "enable_xnormal": "1",
            "enable_l2_acl": "1",
            "enable_bind_mac": "1",
            "enable_meter": "1",
        }
        self._dict = dict(self._default_dict)
        self._flows = {}  # bridge ==> flow str list

        self._reload_conf()
        self._reload_flows()

    def _reload_conf(self):
        if os.path.isfile(NET_AGENT_CONF_PATH):
            content = Utility.read_file(NET_AGENT_CONF_PATH)
            for line in content.splitlines():
                line = line.strip()
                if line.startswith("#"):
                    continue
                items = line.split("=", 1)
                if len(items) == 2:
                    self._dict[items[0]] = items[1]

    def _get(self, key, validate):
        v = self._dict[key]
        v = v if validate(v) else self._default_dict[key]
        return v

    def get_flow(self, br_name):
        res = {}
        for fun, fun_flows in self._flows.items():
            if not fun_flows:
                continue
            res.setdefault(fun, []).extend(fun_flows.get(br_name, []))
            res.setdefault(fun, []).extend(fun_flows.get("all", []))
        return res

    @property
    def enable_state(self):
        return int(self._get("enable_state", lambda v: v.isdigit))

    @property
    def enable_xnormal(self):
        return int(self._get("enable_xnormal", lambda v: v.isdigit))

    @property
    def enable_l2_acl(self):
        return int(self._get("enable_l2_acl", lambda v: v.isdigit))

    @property
    def enable_bind_mac(self):
        return int(self._get("enable_bind_mac", lambda v: v.isdigit))

    @property
    def enable_meter(self):
        return int(self._get("enable_meter", lambda v: v.isdigit))

    def __str__(self):
        return str(self._dict)

    def _reload_flows(self):
        for filename in glob.glob(NET_AGENT_FLOW_FILE_PATTERN):
            if not os.path.isfile(filename):
                continue
            raw_flow = Utility.read_file(filename).strip()
            if not raw_flow:
                continue
            fun_name = filename.split('/')[-2]
            fun_flow = self._flows.setdefault(fun_name, {})
            items = filename.split(".")
            flows = fun_flow.setdefault(items[1], [])
            flows.append(raw_flow)
