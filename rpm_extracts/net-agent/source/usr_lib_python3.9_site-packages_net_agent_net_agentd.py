TIMER_CHECK_OBJECT = (1, 3600, 55)  # (id, interval, idle_interval=-1, count=-1)
TIMER_RELOAD_LIBVIRT = (2, 2, 1.5, 1)
TIMER_RELOAD_CONFIG = (3, 2, 1.5, 1)
TIMER_RELOAD_RAW_FLOWS = (4, 2, 1.5, 1)
TIMER_VALIDATE_NIC_SPEED = (5, 10, 1.5, 1)  # timer validating physical nic's speed config -- first time
TIMER_VALIDATE_NIC_SPEED2 = (6, 90, 1.5, 2)  # timer validating physical nic's speed config -- second, third


class Event(object):

    def __init__(self):
        super(Event, self).__init__()
        self._event_id = None
        self._event_str = []  # splitted event strings
        self._completed = False


class WaitItem(object):

    def __init__(self):
        super(WaitItem, self).__init__()
        self._event_id = None
        self._conn = None


class Agent(object):

    def __init__(self):
        super(Agent, self).__init__()

        self._message_queue = None

        self._file_monitor = None
        self._ovsdb_monitor = None
        self._pollobj = None
        self._timer_notify = None

        self._ovs_doc = None
        self._exit = True

        self._unix_socket = None
        self._is_open = False

        self._old_signal_handlers = {}
        self._listen_events = {}
        self._waiters = {}

        if not os.path.isdir(net_lib.NET_AGENT_RUN_DIR):
            os.mkdir(net_lib.NET_AGENT_RUN_DIR)

    def _register_signal(self, signal_number):
        if signal_number not in self._old_signal_handlers:
            self._old_signal_handlers[signal_number] = signal.signal(signal_number, self._signal_handle)

    def _unregister_signals(self):
        for k, v in self._old_signal_handlers.items():
            signal.signal(k, v)
        self._old_signal_handlers = {}

    def open(self):
        if not self._is_open:
            self._message_queue = MessageQueue(1000)
            self._timer_notify = None
            self._file_monitor = FileMonitor(self.file_monitor_event_handler)
            self._ovsdb_monitor = None
            self._pollobj = select.epoll()

            self._ovs_doc = ovs_doc.OVSDocument()
            self._is_open = True

            self._register_signal(signal.SIGTERM)

    def close(self):
        logging.info("close: %s", self._is_open)
        if self._is_open:
            self._is_open = False
            self._exit = True
            if self._timer_notify:
                self._timer_notify.clear()
                self._timer_notify = None
            self._ovsdb_monitor.stop()
            self._ovsdb_monitor = None
            self._file_monitor.stop()
            self._file_monitor = None
            self._message_queue.close()
            self._message_queue = None
            self._unregister_signals()
            self._ovs_doc.close()
            self._ovs_doc = None
            if self._unix_socket:
                self._unix_socket.close()
                self._unix_socket = None

    def _restart_unix_socket(self):
        if self._unix_socket is not None:
            self._pollobj.unregister(self._unix_socket)
            self._unix_socket = None

        self._unix_socket = socket.socket(socket.AF_UNIX, socket.SOCK_STREAM)
        self._unix_socket.setsockopt(socket.SOL_SOCKET, socket.SO_REUSEADDR, 1)
        try:
            os.remove(net_lib.Network.NET_AGENT_UNIX_PATH)
        except:
            pass
        self._unix_socket.bind(net_lib.Network.NET_AGENT_UNIX_PATH)
        self._unix_socket.listen(1)
        self._pollobj.register(self._unix_socket, select.EPOLLIN)

    def _restart_timer_notify(self):
        if self._timer_notify:
            self._timer_notify.clear()
        self._timer_notify = TimerNotify()
        self._timer_notify.add_timer(*TIMER_RELOAD_RAW_FLOWS)
        self._timer_notify.add_timer(*TIMER_RELOAD_CONFIG)
        self._timer_notify.add_timer(*TIMER_RELOAD_LIBVIRT)
        self._timer_notify.add_timer(*TIMER_CHECK_OBJECT)
        self._timer_notify.add_timer(*TIMER_VALIDATE_NIC_SPEED)

    def file_monitor_event_handler(self, event):
        if event.name.endswith(".pid"):
            message = (event.maskname, event.name)
            self._message_queue.message_enqueue(message)

    def _restart_ovsdb_monitor(self, ovsdb_running):
        if self._ovsdb_monitor is not None:
            old_fd = self._ovsdb_monitor.getfd()
            if old_fd is not None:
                self._pollobj.unregister(old_fd)
            self._ovsdb_monitor.stop()
        #self._ovsdb_monitor = OVSDBMonitor('?Interface:name,ofport,external_ids,other_config?Port:name,tag')
        self._ovsdb_monitor = OVSDBMonitor('?Interface:name,ofport,external_ids,other_config')

        if ovsdb_running:
            self._ovsdb_monitor.start()
            monitor_fd = self._ovsdb_monitor.getfd()
            self._pollobj.register(monitor_fd, select.EPOLLIN)

    def wait_ovs_running(self):
        running = True
        if not net_lib.Utility.wait_util(self._ovs_doc.get_ovsdb_running, 300, 1):
            logging.warn("the ovsdb-server is not running")
            running = False
        if not net_lib.Utility.wait_util(self._ovs_doc.get_vswitchd_running, 300, 1):
            logging.warn("the ovs-vswitchd is not running")
            running = False
        return running

    def run(self):
        while True:
            net_lib.GlobalConfig().reload()
            logging.info("agentd starts into big run.")
            exit_run = self._run()
            if exit_run:
                break
            time.sleep(2)
        logging.info("agentd exits from big run.")

    def _run(self):
        self.open()

        wackup_fd = self._message_queue.get_wackup_fd()
        try:
            self._pollobj.register(wackup_fd, select.EPOLLIN)
        except select.error as msg:
            logging.exception(msg)

        try:
            self._restart_unix_socket()
            self._restart_timer_notify()
            self._file_monitor.start()
            self._restart_ovsdb_monitor(True)
            self._exit = not self.wait_ovs_running()
            if not net_lib.Utility.wait_util(self._ovs_doc.verify_ovs_version, 3, 0.5):
                net_lib.OFCTL_DISABLED = True
                logging.warn("Running ovs-vswitchd version unmatched with its client, disable flow deliver")
            else:
                net_lib.OFCTL_DISABLED = False
                # Delete out acl table, so out acl c code logic in openvswitch (if any) is virtually disabled
                net_lib.Utility.cmd_execute("ovs-vsctl -t 5 set Open_vSwitch . acls=[]")
                logging.warn("Deleting out acl table in ovs, as it is not needed when net-agent takes over")
            fail_count = 0
            if net_lib.Utility.is_log_flow_drop():
                net_lib.Utility.cmd_execute("python3 /opt/bin/ovs_drop_record.pyc record -d")  # log-flow-drop=true indicate run 'record' process

            while not self._exit:
                try:
                    poll_timeout = self._timer_notify.get_wait_time()
                    epoll_list = self._pollobj.poll(poll_timeout)
                    if epoll_list:
                        monitor_fd = self._ovsdb_monitor.getfd()
                        unix_fd = self._unix_socket
                        for fd, _ in epoll_list:
                            if fd == unix_fd.fileno():
                                self.process_network_messages(unix_fd)
                            elif fd == wackup_fd:
                                self._message_queue.flush_wackup()
                                for message in self._message_queue.message_dequeue():
                                    if self._exit:
                                        break
                                    self.process_queue_messages(message)
                            elif monitor_fd is not None and fd == monitor_fd:  # TODO: fileno
                                for r in self._ovsdb_monitor.dequeue():
                                    self.process_ovsdb_message(r)
                                    if self._exit:
                                        break
                            else:
                                raise Exception("this should not happens. unkown fd: %s" % fd)
                            self._timer_notify.set_last_active()
                    self._timer_notify.run(self.process_timer_message)
                    will_remove = []
                    for e_id, e in self._listen_events.items():
                        if e._completed and e_id in self._waiters:
                            will_remove.append(e_id)
                            self._waiters[e_id]._conn.close()
                    for e_id in will_remove:
                        del self._waiters[e_id]
                        del self._listen_events[e_id]
                except SystemExit:
                    logging.info("System Exit")
                    self._exit = True
                except KeyboardInterrupt:
                    logging.exception("KeyboardInterrupt")
                    self._exit = True
                except:
                    fail_count += 1
                    if fail_count >= 10:
                        raise
                    logging.exception("fail count %d", fail_count)
        except:
            logging.exception("")
            raise
        finally:
            self.close()

        return self._exit

    def _signal_handle(self, signal_number, stack_frame):
        logging.warn("handle signal: %d, %s", signal_number, stack_frame)
        if signal_number == signal.SIGTERM:
            self._exit = True

        old_handler = self._old_signal_handlers.get(signal_number, None)
        if old_handler:
            old_handler(signal_number, stack_frame)

    def _meet_field(self, event, e_kv, e_act):
        if e_kv.startswith('other_config:'):
            if e_act == 'set':
                kv = e_kv[13:].split('=')
                if len(kv) < 2:
                    return False
                if kv[0] in event.other_config and event.other_config[kv[0]] == kv[1]:
                    return True
            elif e_act == 'remove':
                if e_kv[13:] not in event.other_config:
                    return True
        elif e_kv.startswith('external_ids:'):
            if e_act == 'set':
                kv = e_kv[13:].split('=')
                if len(kv) < 2:
                    return False
                if kv[0] in event.external_ids and event.external_ids[kv[0]] == kv[1]:
                    return True
            elif e_act == 'remove':
                if e_kv[13:] not in event.external_ids:
                    return True
        return False

    def reply_wait(self, waiter):
        res = ['ok', str(net_lib.ERROR_CODE_OK)]
        net_lib.Network.socket_send_message(
            waiter._conn,
            net_lib.Network.NETWORK_MESSAGE_TYPE_CMDLINE_REPLY,
            net_lib.Network.data_serialize(res))
        logging.debug("network message reply: %s", res)
        waiter._conn.close()
        logging.debug("conn close")

    def handle_wait(self, action, event):
        for e_id, e in self._listen_events.items():
            e_str = e._event_str
            if len(e_str) < 4:
                continue
            # e_str has form: action type obj k v
            e_act, e_obj, e_kv = e_str[0], e_str[2], e_str[3]
            if (action == e_act or action == 'change') and e_obj == event.name:
                if self._meet_field(event, e_kv, e_act):
                    e._completed = True

    def _process_listen_message(self, data):
        res = ['ok', str(net_lib.ERROR_CODE_OK)]
        if data[0].event != None:
            event_ = data[0].event.split()
            if len(event_) != 4:
                res = ['event args should be of form: eventid action type obj k v', str(net_lib.ERROR_CODE_FAILED)]
                return res
        else:
            event_ = []

        e = Event()
        e._event_id = data[0].id
        e._event_str = event_
        self._listen_events[e._event_id] = e
        return res

    def _process_wait_message(self, data, conn):
        # 1) if find an event via event id, and the event is marked as completed, then: remove the event from listen
        #    queue
        # 2) if the event is not completed, then: add an wait item to the wait queue, the wait item is removed later
        # 3) if can't find the event via event id, then operation failed
        wait, res = True, ['ok', str(net_lib.ERROR_CODE_OK)]
        event_id = data[0].id
        if event_id in self._listen_events:
            w = WaitItem()
            w._event_id = event_id
            w._conn = conn
            self._waiters[event_id] = w
        else:
            wait, res = False, ['wait operation failed, no listen event is registered', str(net_lib.ERROR_CODE_FAILED)]
        return wait, res

    def _process_messages(self, data):
        ress = []
        unhandle_data = []
        for argv in data:
            res = 'ok'
            message = 'unset'
            if argv.cmd == net_lib.CTL_CMD_TYPE_EXIT:
                self._exit = True
                message = '%s %s' % (argv.cmd, "ok")
            elif argv.cmd == net_lib.CTL_CMD_TYPE_LOGLEVEL:
                level = {
                    "error": logging.ERROR,
                    "warn": logging.WARN,
                    "info": logging.INFO,
                    "debug": logging.DEBUG
                }.get(argv.loglevel, "")
                if level:
                    logging.getLogger().setLevel(level)
                else:
                    res = 'invalid paramenter'
                message = '%s %s' % (argv.cmd, res)
            else:
                message = ''
                unhandle_data.append(argv)
            if message:
                ress.append(message)
        res2 = self._ovs_doc.handle_unix_socket_event(unhandle_data)
        ress.extend(res2)
        return ress

    def process_network_messages(self, fd):
        conn, addr = fd.accept()

        logging.debug("accept new message %s, %s", conn, addr)
        conn.settimeout(1)
        wait = False

        try:
            type_, message = net_lib.Network.socket_recv_message(conn)
        except socket.timeout:
            logging.error("handle client recv timeout:")
        else:
            res = ''
            if type_ == net_lib.Network.NETWORK_MESSAGE_TYPE_NONE:
                logging.error("socket is closed by client.")
            elif type_ == net_lib.Network.NETWORK_MESSAGE_TYPE_CMDLINE:
                try:
                    data = net_lib.Network.data_unserialize(message)
                    if data[0].cmd == net_lib.CTL_CMD_TYPE_ADD_LISTEN:
                        res = self._process_listen_message(data)
                    elif data[0].cmd == net_lib.CTL_CMD_TYPE_WAIT:
                        wait, res = self._process_wait_message(data, conn)
                    else:
                        res = self._process_messages(data)
                except:
                    logging.exception("doc excetpion:")
                    res = 'ovs doc exception'
            else:
                res = 'unkown request type'
            if res and not wait:
                try:
                    net_lib.Network.socket_send_message(
                        conn,
                        net_lib.Network.NETWORK_MESSAGE_TYPE_CMDLINE_REPLY,
                        net_lib.Network.data_serialize(res))
                    logging.debug("network message reply: %s", res)
                except:
                    logging.exception("handled send exception:")
        finally:
            if not wait:
                conn.close()
                logging.debug("conn close")

    def process_queue_messages(self, message):
        running = True
        if message[0] == "IN_DELETE":
            running = False
        elif message[0] == "IN_MOVED_TO":
            running = True
        else:
            return

        daemon = message[1]
        if daemon == "ovsdb-server.pid":
            self._restart_ovsdb_monitor(running)

        self._ovs_doc.handle_daemon_change(daemon, running)

    def process_ovsdb_message(self, message):
        ovsdb_events = []
        for i in range(0, len(message)):
            t = message[i]
            e = ovs_doc.OVSDbEvent(t.action, t.uuid, t.name, t.ofport, t.tag, t.external_ids, t.other_config)
            ovsdb_events.append(e)
        self._ovs_doc.handle_ovsdb_interface_update(ovsdb_events, self)

    def process_timer_message(self, timer_id):
        try:
            if timer_id == TIMER_CHECK_OBJECT[0]:
                self._ovs_doc.handle_timer_check_object()
            elif timer_id == TIMER_RELOAD_LIBVIRT[0]:
                if not net_lib.Utility.wait_util(self._ovs_doc.reload_libvirt_nwfilters, 1, 1):
                    self._timer_notify.add_timer(*TIMER_RELOAD_LIBVIRT)
            elif timer_id == TIMER_RELOAD_CONFIG[0]:
                if not net_lib.Utility.wait_util(self._ovs_doc.reload_config, 1, 1):
                    logging.warning("reload config failed")
            elif timer_id == TIMER_RELOAD_RAW_FLOWS[0]:
                if not net_lib.Utility.wait_util(self._ovs_doc.setup_bridge_raw_flows, 6, 1):
                    self._timer_notify.add_timer(*TIMER_RELOAD_RAW_FLOWS)
            elif timer_id == TIMER_VALIDATE_NIC_SPEED[0]:
                self._ovs_doc.validate_nic_speed(True)
                self._timer_notify.add_timer(*TIMER_VALIDATE_NIC_SPEED2)
            elif timer_id == TIMER_VALIDATE_NIC_SPEED2[0]:
                self._ovs_doc.validate_nic_speed(False)
        except:
            logging.exception("")


class NetAgentDaemonRunnerApp(object):

    def __init__(self):
        super(NetAgentDaemonRunnerApp, self).__init__()
        self.stdin_path = '/dev/null'
        self.stdout_path = '/dev/null'
        self.stderr_path = '/dev/null'
        self.pidfile_path = os.path.join(net_lib.NET_AGENT_RUN_DIR, "net-agentd.pid")
        self.pidfile_timeout = 5

    def run(self):
        agent = Agent()
        agent.run()


def daemonrunner_init(run):
    if os.getppid() != 1 and not the_test_mode:
        run.daemon_context.stdout.close()
        run.daemon_context.stderr.close()
        run.daemon_context.stdout = sys.stdout
        run.daemon_context.stderr = sys.stderr
        run.daemon_context.stdin = sys.stdin
        run.stdout_path = os.ttyname(sys.stderr.fileno())


the_test_mode = os.getenv("NA_TEST_MODE")
# if environment NA_EXTRACODE exists, execute the inject code
extra_code = os.getenv("NA_EXTRACODE")
if extra_code:
    exec(extra_code)


def main():
    if len(sys.argv) <= 1:
        sys.argv = [sys.argv[0], "start"]

    agent_runner = runner.DaemonRunner(NetAgentDaemonRunnerApp())
    agent_runner.daemon_context.detach_process = bool(the_test_mode)
    daemonrunner_init(agent_runner)
    agent_runner.daemon_context.files_preserve = log_init()
    agent_runner.do_action()


if __name__ == "__main__":
    main()
