def correct_port(name, ofport):
    if ofport and type(ofport) is list:
        ofport = ofport[0] if len(ofport) else None
    if ofport == None:
        return None
    if type(ofport) is int:
        return ofport
    i = 0
    ofport = None
    max_obtain = 8
    while i < max_obtain:
        ofport = ovs_object.OVSDb.get_ofport_by_iface(name)
        if ofport != None:
            break
        i = i + 1
        time.sleep(0.4)

    if ofport == None:
        logging.warn("ofport get failed, may be cause failed to issue flow table!")
    else:
        logging.info("ofport obtain numbers:%s/%s",i+1,max_obtain)
    return ofport


class sobject(object):

    def __init__(self):
        pass


class OVSDBMonitor(object):

    def __init__(self, tabcols):
        self._db_sock = 'unix:%s/db.sock' % net_lib.OPENVSWITCH_RUN_DIR
        self._stop = True
        self._stop_pipe = os.pipe()
        self._tabcols = tabcols
        self._msg_queue = MessageQueue()
        self._thread = None

    def __del__(self):
        if self._stop_pipe:
            os.close(self._stop_pipe[0])
            os.close(self._stop_pipe[1])
        self._stop_pipe = None

    def start(self):
        if not self._thread:
            self._thread = threading.Thread(target=self._do_idl)
        self._stop = False
        self._thread.start()

    def stop(self):
        if not self._stop:
            self._stop = True
            os.write(self._stop_pipe[1], "1".encode('utf-8'))
            self._thread.join()  # TODO
            self._thread = None

    def getfd(self):
        return self._msg_queue.get_wackup_fd() if not self._stop else None

    def dequeue(self):
        '''Intentionally only deque an element, this gives more priority to other events'''
        try:
            it = next(self._msg_queue.message_dequeue())
            if it is not None:
                yield it
        except StopIteration:
            self._msg_queue.flush_wackup()

    def _emit(self, r):
        self._msg_queue.message_enqueue(r)
        self._msg_queue.notify_wackup()

    def _get_schema(self):
        while True:
            error, strm = Stream.open_block(Stream.open(self._db_sock))
            if error:
                logging.warn("Unable to connect to %s" % self._db_sock)
                time.sleep(0.5)
            else:
                break
        rpc = jsonrpc.Connection(strm)
        req = jsonrpc.Message.create_request('get_schema', ['Open_vSwitch'])
        error, resp = rpc.transact_block(req)
        rpc.close()

        if error or resp.error:
            raise Exception('Unable to retrieve schema.')
        return idl.SchemaHelper(None, resp.result)

    def get_table(self, table_name):
        return self._idl_conn.tables[table_name]

    def _find_row(self, table_name, find):
        return next(
            (row for row in self.get_table(table_name).rows.values()
             if find(row)), None)

    def _find_row_by_name(self, table_name, value):
        return self._find_row(table_name, lambda row: row.name == value)

    def _wait_for_db_change(self):
        seq = self._idl_conn.change_seqno
        while self._idl_conn.change_seqno == seq and not self._idl_conn.run() and not self._stop:
            poller = Poller()
            self._idl_conn.wait(poller)
            poller.fd_wait(self._stop_pipe[0], POLLIN)
            poller.block()

    def _init(self):
        schema_helper = self._get_schema()  # schema helper
        commands = self._tabcols
        if commands and commands.startswith("?"):
            readonly = {}
            for x in commands[1:].split("?"):
                readonly = []
                table, columns = x.split(":")
                columns = columns.split(",")
                for index, column in enumerate(columns):
                    if column[-1] == '!':
                        columns[index] = columns[index][:-1]
                        readonly.append(columns[index])
                schema_helper.register_columns(table, columns, readonly)
        else:
            schema_helper.register_all()
        self._idl_conn = idl.Idl(self._db_sock, schema_helper)
        error, _ = Stream.open_block(Stream.open(self._db_sock))
        if error:
            logging.info("failed to connect to \"%s\"", self._db_sock)
            return

    def _do_idl(self):
        self._init()

        def trans_action(act, initial):
            if initial:
                return 'initial'
            elif act == 'create':
                return 'insert'
            return act

        def normalize(ev):
            if ev.ofport and type(ev.ofport) is list:
                ev.ofport = ev.ofport[0] if len(ev.ofport) else None
            ev.tag = ev.tag[0] if len(ev.tag) else 0
            return ev

        # the watch event occurs
        # event can be: 'create', 'delete', 'update'
        # both row_ and updates are Row objects. updates contains old information of changed columns
        def event_notify(event, row_, updates=None):
            logging.debug("event_notify enter")
            initial = False
            if updates is None:
                caller_frame = inspect.currentframe().f_back
                args = inspect.getargvalues(caller_frame)
                if "row_update" in args and "initial" in args.locals["row_update"]:
                    if row_._table.name == 'Port':  # if row_ is Port and initial event don't generate event
                        logging.info("skip event: don't generate event on row_ if Port and initial event")
                        return
                    initial = True
                if "delete" in args.locals["event"]:
                    if row_._table.name == 'Port' :  # if row_ is Port and delete event don't generate event
                        logging.info("skip event: don't generate event on row_ if Port and delete event")
                        return
            if not hasattr(row_, 'name'):  # TODO
                logging.info("skip event: row_ uuid:%s, updates:%s. because row_ has no name", row_.uuid, updates)
                return

            if row_._table.name == 'Interface':
                row = row_
                row_ = None
                #row_ = self._find_row_by_name('Port', row_.name)  # find port row
            else:
                row = self._find_row_by_name('Interface', row_.name)  # find interface row
            if row is None or row.name is None:
                logging.info("event_notify row is None or row.name is None, event_notify leave")
                return

            ne = sobject()  # new event
            ofport = correct_port(row.name, row.ofport)
            setattr(ne, 'uuid', row.uuid)
            setattr(ne, 'name', row.name)
            setattr(ne, 'ofport', ofport)
            setattr(ne, 'external_ids', row.external_ids)
            setattr(ne, 'other_config', row.other_config)
            setattr(ne, 'tag', row_.tag if row_ is not None else [])
            iface_type = net_lib.Utility.cmd_execute("ovs-vsctl --verbose=db_ctl_base:syslog:off -t 5 get Interface %s type" % row.name, shell=True, log_err=False)[1].strip()
            if (ofport == None or ofport < 0) and iface_type != "dpdkvhostuserclient":
                setattr(ne, 'action', 'delete')
                self._emit(tuple([normalize(ne)]))
            elif updates:  # update
                oe = sobject()
                for k, v in ne.__dict__.items():
                    setattr(oe, k, getattr(updates, k) if hasattr(updates, k) else v)
                setattr(oe, 'ofport', None)  # This is tricky. see handle_interface_change is_ofport_change why this is needed
                setattr(oe, 'action', 'old')
                setattr(ne, 'action', 'new')
                self._emit(tuple([normalize(oe), normalize(ne)]))  # Note: msg should not be list type, see deque message expanding
            else:
                setattr(ne, 'action', trans_action(event, initial))
                self._emit(tuple([normalize(ne)]))
            logging.info("event_notify leave")

        def event_notify_wrap(event, row_, updates=None):
            try:
                event_notify(event, row_, updates)
            except:
                logging.info("event_notify get exception, please report to developers.\n%s", traceback.format_exc())

        self._idl_conn.notify = event_notify_wrap

        while not self._stop:
            self._wait_for_db_change()
