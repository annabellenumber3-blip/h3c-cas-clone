#!/usr/bin/python3
import logging
from . flow_business_base import FlowBusinessBase


class FlowBusinessUsedByMirror(FlowBusinessBase):

    def __init__(self):
        super(FlowBusinessUsedByMirror, self).__init__()

    def teardown_flows(self, flow_manager, iface, bridge):
        if iface.used_by_mirror == "true" and iface.name == bridge.name:
            flow_manager.del_used_by_mirror(bridge, iface)
            logging.debug("teardown used_by_mirror flow for: %s.", iface)

    def setup_flows(self, flow_manager, iface, bridge):
        if iface.used_by_mirror == "" and iface.name == bridge.name:
            flow_manager.apply_used_by_mirror(bridge, iface)
            logging.debug("setup used_by_mirror flow for %s.", iface)

    def handle_interface_change_begin(self, flow_manager, iface, bridge, old_event, new_event):
        is_change = old_event.other_config is not None and old_event.used_by_mirror == "true" and new_event.used_by_mirror == ""
        if is_change:
            self.teardown_flows(flow_manager, iface, bridge)
        return is_change

    def handle_interface_change_end(self, flow_manager, iface, bridge, is_change):
        if is_change:
            self.setup_flows(flow_manager, iface, bridge)
