#!/usr/bin/python3
"""
Test port profile basic behaviour
"""

import unittest
from na_tests.common.utility import Utility
from na_tests.common import testbase


class TestPortProfileData(testbase.TestCaseBase):

    def myinit(self):
        super(TestPortProfileData, self).myinit()
        self._start_ovs = True
        self._start_netagent = True

    def test_add_portprofile(self):
        ret = Utility.cmd_execute('''
cd {test_dir}
PRTNAME="6cecd756-a718-4ed6-9679-a19c38461d42_1"
NETNAME="qos_test"
get_portprofile() {{
    cat <<EOF
<portprofile name="$PRTNAME" type="compose">
  <netprofile name="$NETNAME" type="qos" />
</portprofile>
EOF
}}

na-ctl --retry=-1 portprofile define --content "$(get_portprofile)"
if test $? -ne 0; then
    exit 1
fi

ls portprofile > tmp.txt  # TODO: bad
grep $PRTNAME.xml tmp.txt
ret=$?
if test $ret -ne 0; then
    exit 2
fi

PRTNAME="errored_portprofile_test"
get_errored_portprofile() {{  # intentionally to construct is an error netprofile
    cat <<EOF
<portprofile name="$PRTNAME" type="errorcompose">
  <netprofile name="$NETNAME" type="errortype" />
</portprofile>
EOF
}}
na-ctl portprofile define --content "$(get_errored_portprofile)"
if test $? -eq 0; then  # expected failed, but if return status code is OK, then exit 
    exit 2
fi
'''.format(**{'test_dir': self._test_dir}), True)
        self.assertEqual(ret[0], 0, "test add portprofile failed: shell return code:%d" % ret[0])

    def test_del_portprofile(self):
        self.test_add_portprofile()
        ret = Utility.cmd_execute('''
cd {test_dir}
PRTNAME="qos_test"
na-ctl portprofile undefine "$PRTNAME"
ls portprofile > tmp.txt  # TODO: bad
grep $PRTNAME.xml tmp.txt
ret=$?
if test $ret -eq 0; then
    exit 1
fi
NETFILENAME="qos_test_fake"
if na-ctl portprofile undefine "$NETFILENAME"; then  # this is a faked netprofile, undefine should failed
    exit 2  
fi
'''.format(**{'test_dir': self._test_dir}), True)
        self.assertEqual(ret[0], 0, "test del portprofile failed: shell return code:%d" % ret[0])

    def test_mod_portprofile(self):
        ret = Utility.cmd_execute('''
cd {test_dir}
PRTNAME="6cecd756-a718-4ed6-9679-a19c38461d42_1"
NETNAME="qos_test"
get_portprofile() {{
    cat <<EOF
<portprofile name="$PRTNAME" type="compose">
  <netprofile name="$NETNAME" type="qos" />
</portprofile>
EOF
}}

na-ctl --retry=-1 portprofile define --content "$(get_portprofile)"
if test $? -ne 0; then
    exit 1
fi

# modified netprofile name in portprofile
NETNAME="qos_test_mod"
get_portfile_mod() {{
    cat <<EOF
<portprofile name="$PRTNAME" type="compose">
  <netprofile name="$NETNAME" type="qos" />
</portprofile>
EOF
}}
na-ctl portprofile define --content "$(get_portfile_mod)"
if test $? -ne 0; then
    exit 1
fi

get_portfile_mod > portfile_mod.xml
if test diff --ignore-all-space portfile_mod.xml $PRTNAME.xml; then  # expected these two files are different
    exit 2
fi

PRTNAME="errored_portfile"
get_errored_portfile() {{  # intentionally to construct is an error netprofile
    cat <<EOF
<portprofile name="$PRTNAME" type="compose">
  <netprofile name="$NETNAME" type="errorqos" />
</portprofile>
EOF
}}
na-ctl portprofile define --content "$(get_errored_portfile)"
if test $? -ne 0; then  # expected failed, but if return status code is OK, then exit 
    exit 3
fi
'''.format(**{'test_dir': self._test_dir}), True)
        self.assertEqual(ret[0], 0, "test mod portprofile failed: shell return code:%d" % ret[0])

    def test_view_portprofile(self):
        ret = Utility.cmd_execute('''
cd {test_dir}
PRTNAME="6cecd756-a718-4ed6-9679-a19c38461d42_1"
NETNAME="qos_test"
ADDR1=1.0.0.1
get_portfile() {{
    cat <<EOF
<portprofile name="$PRTNAME" type="compose">
  <netprofile name="$NETNAME" type="errorqos" />
</portprofile>
EOF
}}

na-ctl --retry=-1 portprofile define --content "$(get_portfile)"
if test $? -ne 0; then
    exit 1
fi
na-ctl portprofile dump $NETFILENAME >portprofile_dumped.xml
if test $? -ne 0; then
    exit 7
fi
diff --ignore-all-space --ignore-blank-lines portprofile_dumped.xml "{test_dir}/portprofile/$PRTNAME.xml"
if test $? -ne 0; then
    exit 8
fi
# check the dump content
listrt=$(na-ctl portprofile list)
if test $? -ne 0; then
    exit 2
fi
if X"$listrt" != X$PRTNAME; then
    exit 3
fi
'''.format(**{'test_dir': self._test_dir}), True)
        self.assertEqual(ret[0], 0, "test view portprofile: shell return code:%d" % ret[0])


if __name__ == '__main__':
    unittest.main()
