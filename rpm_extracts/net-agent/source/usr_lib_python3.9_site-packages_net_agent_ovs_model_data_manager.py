

class AgentDataManager(object):

    def __init__(self):
        super(AgentDataManager, self).__init__()
        self._bridges = {}  # name -> bridge
        self._deleted_bridges = {}  # name -> bridge
        self._interfaces = {}  # name -> interface
        self._nwfilters = {}  # name -> nwfilter
        self._portprofiles = {}  # name -> portprofile
        self._netprofiles = {}  # name -> netprofile
        self._iface_to_drain = {}  # iface object -> drain netprofile
        self._drain_bypass = {} # bypass_info for vm_port drain.
        self._tp_iface_info = {} # iface info for trunkport iface
        self._iface_to_pvlan = {}  # iface object -> pvlan netprofile
        self._vswitch_all_pvlans = {} # vswitch name -> all pvlans example: {vswitch0:{100:"(100:101:102,103)", 200:"(200:201,202:203)"}} 

    def close(self):
        self._bridges = {}
        self._deleted_bridges = {}
        self._interfaces = {}
        self._nwfilters = {}
        self._portprofiles = {}
        self._netprofiles = {}
        self._drain_bypass = {}
        self._tp_iface_info = {}
        self._iface_to_pvlan = {}
        self._vswitch_all_pvlans = {}

    def add_active_bridge(self, bridge):
        self._bridges[bridge.name] = bridge
        self._deleted_bridges.pop(bridge.name, None)

    def reset_active_bridge(self, bridges):
        self._bridges = {}
        for bridge in bridges:
            self.add_active_bridge(bridge)

    def get_all_bridges(self):
        return self._bridges.values()

    def has_bridge(self, brname):
        return brname in self._bridges

    def get_bridge_by_name(self, name):
        bridge = self._bridges.get(name, None)
        if not bridge:
            bridge = self._deleted_bridges.get(bridge, None)
        return bridge

    def mark_deleted_bridge(self, name):
        bridge = self._bridges.pop(name, None)
        if bridge:
            bridge.exist = False
            self._deleted_bridges[name] = bridge
        return bridge

    def clear_deleted_bridge(self):
        old = self._deleted_bridges.keys()
        self._deleted_bridges.clear()
        return old

    def get_all_interfaces(self):
        return self._interfaces.values()

    def get_interface_by_name(self, name):
        return self._interfaces.get(name, None)

    def update_interface(self, iface):
        self._interfaces[iface.name] = iface

    def delete_interface(self, name):
        self._interfaces.pop(name, None)

    def get_all_nwfilters(self):
        return self._nwfilters.values()

    def get_all_nwfilters_name(self):
        return self._nwfilters.keys()

    def get_nwfilter_by_name(self, name):
        return self._nwfilters.get(name, None)

    def update_nwfilter(self, acl):
        self._nwfilters[acl.name] = acl

    def delete_nwfilter(self, name):
        self._nwfilters.pop(name, None)

    # TODO rename
    def get_all_netprofiles(self):
        return self._netprofiles.values()

    def get_all_netprofile_name(self):
        return self._netprofiles.keys()

    def get_netprofile_by_name(self, name_):
        return self._netprofiles.get(name_, None)

    def update_netprofile(self, netprofile):
        self._netprofiles[netprofile.name] = netprofile

    def delete_netprofile(self, name_):
        self._netprofiles.pop(name_, None)

    def get_ifaces_by_netprofile(self, name_):
        ifaces = []
        for portprofile in self._portprofiles.values():
            for netprofile in portprofile.netprofiles:
                if netprofile.name == name_:
                    for iface in self._interfaces.values():
                        if iface.port_profile == portprofile.name:
                            ifaces.append(iface)
        return ifaces

    def get_netprofiles_by_references(self, references):
        netprofiles = []
        for reference in references:
            netprofile = self.get_netprofile_by_name(reference.name)
            if netprofile is not None:
                netprofiles.append(netprofile)
        return netprofiles

    def get_all_portprofiles(self):
        return self._portprofiles.values()

    def get_all_portprofile_names(self):
        return self._portprofiles.keys()

    def get_portprofile_by_name(self, name):
        return self._portprofiles.get(name, None)

    def update_portprofile(self, portprofile):
        self._portprofiles[portprofile.name] = portprofile

    def delete_portprofile(self, name):
        self._portprofiles.pop(name, None)

    def update_iface_drain_netprofile(self, iface, netprofile):
        self._iface_to_drain[iface] = netprofile

    def delete_iface_drain_netprofile(self, iface):
        self._iface_to_drain.pop(iface, None)

    def get_all_iface_drain_netprofiles(self):
        return self._iface_to_drain

    def get_bridge_by_iface(self, iface):
        return self.get_bridge_by_name(iface.br_name)

    def get_unused_drain_vlan(self, vlan):
        used_vlans = []
        for iface in self._interfaces.values():
            if iface.drain_vlan:
                used_vlans.append(int(iface.drain_vlan))

        select_vlan = 2
        used_vlans.sort()
        for used_vlan in used_vlans:
            if select_vlan != used_vlan and select_vlan != vlan:
                break
            select_vlan = select_vlan + 1

        return str(select_vlan)

    def get_all_drian_vlan_by_origin_vlan(self, vlan, br_name):
        drain_vlans = []
        same_vlan_ports = []
        for iface in self._interfaces.values():
            if iface.origin_br == br_name and iface.origin_vlan == vlan and iface.drain_vlan:
                drain_vlans.append(iface.drain_vlan)
                same_vlan_ports.append([iface.mac, iface.drain_vlan])

        return drain_vlans, same_vlan_ports

    def update_drain_bypass_state(self, state):
        self._drain_bypass["state"] = state

    def update_drain_bypass_connected(self, drain, pair):
        if pair:
            self._drain_bypass["connected"][drain] = pair
        else:
            self._drain_bypass["connected"].pop(drain, None)

    def reset_drain_bypass_info(self):
        self._drain_bypass["state"] = False
        self._drain_bypass["connected"] = {}

    def get_drain_bypass_state(self):
        return self._drain_bypass["state"]

    def get_drain_bypass_connected(self, name):
        if name in self._drain_bypass["connected"].keys():
            return self._drain_bypass["connected"][name]
        else:
            return {}

    def get_drain_bypass_all_connected_keys(self):
        return self._drain_bypass["connected"].keys()

    def get_drain_bypass_all_connected(self):
        return self._drain_bypass["connected"]

    def get_tp_iface_info(self, iface):
        return self._tp_iface_info.get(iface.name, None)

    def update_tp_iface_info(self, iface, info):
        self._tp_iface_info[iface.name] = info

    def delete_tp_iface_info(self, iface):
        self._tp_iface_info.pop(iface.name, None)

    def get_pvlan_iface_netprofile(self, iface):
        return self._iface_to_pvlan.get(iface, None)

    def update_iface_pvlan_netprofile(self, iface, netprofile):
        self._iface_to_pvlan[iface] = netprofile

    def delete_iface_pvlan_netprofile(self, iface):
        self._iface_to_pvlan.pop(iface, None)

    def get_vswitch_all_pvlans(self, br_name):
        return self._vswitch_all_pvlans.get(br_name, {})

    def update_vswitch_all_pvlans(self, br_name, private_vlans):
        self._vswitch_all_pvlans[br_name] = private_vlans

    def delete_vswitch_all_pvlans(self, br_name):
        self._vswitch_all_pvlans.pop(br_name, {})

    def dump(self):
        f = ""
        f += "  deleted bridges:\n"
        for bridge in self._deleted_bridges.values():
            f += "       %-15s\n" % bridge
        f += "  running bridges:\n"
        for bridge in self._bridges.values():
            f += "       %-15s\n" % bridge
        f += "  all nwfilters:\n"
        nwfilters_no_use = []
        for nwfilter in self._nwfilters.values():
            for iface in self._interfaces.values():
                if iface.nwfilter == nwfilter.name:
                    f += "       nwfilter: name=%s\n" % nwfilter.name
                    if nwfilter.aclrules:
                        for acl in nwfilter.aclrules:
                            f += "                 rule: %s\n" % acl
                    else:
                        f += "                 %s is used but no rules\n" % nwfilter.name
                    break
            else:
                nwfilters_no_use.append(nwfilter.name)

        f += "       nwfilter not used: %s\n" % nwfilters_no_use
        f += "\n"

        f += "  all portprofiles:\n"
        for portprofile in self._portprofiles.values():
            f += "      portprofile %s\n" % portprofile
            if portprofile.netprofiles:
                for netprofile in portprofile.netprofiles:
                    f += "                 netprofile: %s\n" % netprofile
        f += "\n"

        f += "  all netprofiles:\n"
        for netprofile in self._netprofiles.values():
            f += "      netprofile  %s\n" % netprofile
            if netprofile.rules:
                for rule in netprofile.rules:
                    f += "                 rule: %s\n" % rule
        f += "\n"

        f += "  all interfaces:\n"
        for iface in self._interfaces.values():
            f += "       interface: %s\n" % iface

        f += "\n"

        f += "  drain_bypass state:\n"
        f += "       %s\n" % self._drain_bypass
        
        f += "\n"

        f += "  all pvlans:\n"
        for bridge in self._bridges.values():
            pvlan_ifaces = {}
            primary_vlans = []
            ifaces = self._interfaces.values()
            for iface in ifaces:
                if iface.origin_br == bridge.name and iface.primary_vlan:
                    if iface.primary_vlan not in primary_vlans:
                        primary_vlans.append(iface.primary_vlan)
            vswitch_all_pvlans = self.get_vswitch_all_pvlans(bridge.name)
            vswitch_all_pvlans_value = ""
            for _, value in vswitch_all_pvlans.items():
                vswitch_all_pvlans_value = vswitch_all_pvlans_value + "%s," % value
            if primary_vlans or len(vswitch_all_pvlans) != 0:
                f += "       %s all pvlans: %s\n" % (bridge.name, vswitch_all_pvlans_value.rstrip(','))
            for primary_vlan in primary_vlans:
                primary_ifaces = ""
                isolated_ifaces = ""
                group_ifaces = ""
                pvlan_key = ""
                for iface in ifaces:
                    if iface.origin_br == bridge.name and iface.primary_vlan == primary_vlan:
                        if iface.isolated_vlan:
                            pvlan_key = "S(%s, %s)" % (iface.primary_vlan, iface.isolated_vlan)
                            isolated_ifaces += iface.name + ","
                            pvlan_ifaces[pvlan_key] = isolated_ifaces.rstrip(',')
                        elif iface.group_vlan:
                            pvlan_key = "G(%s, %s)" % (iface.primary_vlan, iface.group_vlan)
                            group_ifaces += iface.name + ","
                            pvlan_ifaces[pvlan_key] = group_ifaces.rstrip(',')
                        else:
                            pvlan_key = "P(%s, %s)" % (iface.primary_vlan, iface.primary_vlan)
                            primary_ifaces += iface.name + ","
                            pvlan_ifaces[pvlan_key] = primary_ifaces.rstrip(',')
            for key, value in pvlan_ifaces.items():
                f += "       %s: %s\n" % (key, value)
        return f


