#!/usr/bin/python3
import logging
from . flow_business_base import FlowBusinessBase


class FlowBusinessBypass(FlowBusinessBase):

    def __init__(self):
        super(FlowBusinessBypass, self).__init__()

    def teardown_flows(self, flow_manager, iface, bridge):
        flow_manager.del_bypass(bridge, iface)
        logging.debug("teardown bypass flow for: %s.", iface)

    def setup_flows(self, flow_manager, iface, bridge):
        if iface.bypass == "true":
            flow_manager.apply_bypass(bridge, iface)
            logging.debug("setup bypass flow for %s.", iface)

    def handle_interface_change_begin(self, flow_manager, iface, bridge, old_event, new_event):
        is_change = old_event.bypass != new_event.bypass
        if is_change:
            self.teardown_flows(flow_manager, iface, bridge)
        return is_change

    def handle_interface_change_end(self, flow_manager, iface, bridge, is_change):
        if is_change:
            self.setup_flows(flow_manager, iface, bridge)
