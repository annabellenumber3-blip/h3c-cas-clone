import logging
import subprocess
from .. base import net_lib
from .. base.id_manager import IdManager
from .. netprofile.netprofile_qos import METER_DOMAIN_BRIDGE, METER_DOMAIN_IFACE

OVS_METER_BRIDGE_INDEX = 0
OVS_METER_IFACE_INDEX = 1
OVS_METER_PROFILE_INDEX = 2
OVS_METER_ID_INDEX = 3
OPENFLOW_VERSION = "OpenFlow13"


class OVSMeter(object):

    def __init__(self):
        super(OVSMeter, self).__init__()
        self._meters = []  # [[br_name, iface_name, profile, ovs_meter_id] ... ], profile is the key to alloc ovs_meter_id from IdManager

    @staticmethod
    def _set_meter(br, meter_cmd, meter_str):
        if net_lib.OFCTL_DISABLED:
            logging.debug("_set_meter to %s is ignored for %s, meter: %s", meter_cmd, br, meter_str)
            return 0
        cmd = ["ovs-ofctl", "--timeout=5", "-O", OPENFLOW_VERSION, meter_cmd, br]
        if meter_str:
            cmd.append(meter_str)
        p = subprocess.Popen(cmd, stdin=subprocess.PIPE, close_fds=True, stderr=subprocess.PIPE, stdout=subprocess.PIPE)
        _, stderr = p.communicate()
        if p.returncode != 0:
            logging.error("FAILED to %s for %s, error: '%s', meter: %s\n.", meter_cmd, br, stderr.rstrip(), meter_str)
        else:
            logging.debug("Finish to %s for %s, meter: %s\n.", meter_cmd, br, meter_str)
        return p.returncode

    def update_meter(self, bridge, iface, name, ovs_meter_id):
        meter_list = [bridge, iface, name, ovs_meter_id]
        if meter_list not in self._meters:
            self._meters.append(meter_list)

    def del_meter(self, bridge, iface, name, ovs_meter_id):
        meter_list = [bridge, iface, name, ovs_meter_id]
        if meter_list in self._meters:
            self._meters.remove(meter_list)

    def is_meter_id_used(self, ovs_meter_id):
        for meter in self._meters:
            if meter[OVS_METER_ID_INDEX] == ovs_meter_id:
                return True
        return False

    # clear meters in memory and ovs related to iface, openvswitch will del meter if del bridge
    def clear_iface_meters(self, iface):
        is_bridge = True if iface.is_local() else False
        clear_index = OVS_METER_BRIDGE_INDEX if is_bridge else OVS_METER_IFACE_INDEX
        del_meter_list = []
        for meter in self._meters:
            # if del bridge, meter on all bridge ifaces in memory shoud be clean
            if iface.name == meter[clear_index]:
                del_meter_list.append(meter)

        for meter in del_meter_list:
            self._meters.remove(meter)
            # if del iface, meter in ovs and id_manager should both be clean
            if not is_bridge and not self.is_meter_id_used(meter[OVS_METER_ID_INDEX]):
                meter_str = "meter=%s" % meter[OVS_METER_ID_INDEX]
                # clear meter in meter_manager and id_manager
                IdManager().free_id("meter", meter[OVS_METER_PROFILE_INDEX])
                # ovs del-meter may fails if del iface happen after del br, err in this cas can be ignored
                if OVSMeter._set_meter(meter[OVS_METER_BRIDGE_INDEX], "del-meter", meter_str):
                    continue
            elif is_bridge:
                IdManager().free_id("meter", meter[OVS_METER_PROFILE_INDEX])

        logging.debug("del all meters related to iface: %s", iface.name)

    @staticmethod
    def alloc_meter_by_domain(domain, profile_name):
        if domain == METER_DOMAIN_BRIDGE:
            return IdManager().alloc_id("meter", profile_name, 1, 10)
        elif domain == METER_DOMAIN_IFACE:
            return IdManager().alloc_id("meter", profile_name, 2, 10)
        else:
            return None

    def clean_meter(self, bridge, iface, meter, profile_name):
        # name store in meter data manage: qosprofile name + bridge or iface name(depends on domain in qosprofile) + meter id in qosprofile
        name = "%s_%s_%s" % (profile_name, bridge.name if meter.id[0] == METER_DOMAIN_BRIDGE else iface.name, meter.id[1])
        ovs_meter_id = IdManager().find_id("meter", name)
        if ovs_meter_id is None:
            logging.debug("meter:%s is already deleted", name)
            return
        else:
            self.del_meter(bridge.name, iface.name, name, ovs_meter_id)
            if not self.is_meter_id_used(ovs_meter_id):
                meter_str = "meter=%s" % ovs_meter_id
                if OVSMeter._set_meter(bridge.name, "del-meter", meter_str):
                    return
                IdManager().free_id("meter", name)

    def get_meter(self, bridge, iface, meter, profile_name):
        # name store in meter data manage: qosprofile name + bridge or iface name(depends on domain in qosprofile) + meter id in qosprofile
        name = "%s_%s_%s" % (profile_name, bridge.name if meter.id[0] == METER_DOMAIN_BRIDGE else iface.name, meter.id[1])
        ovs_meter_id = OVSMeter.alloc_meter_by_domain(meter.id[0], name)
        if ovs_meter_id and not self.is_meter_id_used(ovs_meter_id):
            meter_str = "meter=%s,%s,burst,band=type=drop,rate=%s,burst_size=%s" % \
                        (ovs_meter_id, meter.unit, meter.rate, meter.burst)
            if OVSMeter._set_meter(bridge.name, "add-meter", meter_str):
                IdManager().free_id("meter", name)
                return None
            self.update_meter(bridge.name, iface.name, name, ovs_meter_id)
        return ovs_meter_id

    @staticmethod
    def clear_bridge_meters(bridge):
        OVSMeter._set_meter(bridge.name, "del-meters", "")

    def dump(self):
        f = ""
        f += "used meters:\n"
        for meter in self._meters:
            f += "     meter: bridge=%s, iface=%s, name=%s, ovs_meter_id=%s\n" \
                 % (meter[0], meter[1], meter[2], meter[3])
        f += "\n"
        return f
