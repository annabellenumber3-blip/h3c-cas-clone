import os
import utility


class OvsInst(object):

    def __init__(self, startdir):
        self._ovs_started = False
        self._na_testdir = startdir

    def start(self):
        if not self._check_ovsbin():
            return False

        ovs_bindir = os.getenv("OVS_BINDIR", "/usr/bin")
        ovs_sbindir = os.getenv("OVS_SBINDIR", "/usr/sbin")
        ovs_logdir = os.getenv("OVS_LOGDIR", "/var/log/openvswitch")
        ovs_dbdir = os.getenv("OVS_DBDIR", "/etc/openvswitch")
        ovs_syscfgdir = os.getenv("OVS_SYSCONFDIR", "/etc/openvswitch")  #
        ovs_cfgdir = os.getenv("OVS_CONFDIR", "/etc/openvswitch")  #
        ovs_rundir = os.getenv("OVS_RUNDIR", "/run/openvswitch")
        ovs_pkgdatadir = os.getenv("OVS_PKGDATADIR", "/usr/share/openvswitch")

        utility.Utility.cmd_execute('''
export OVS_DBDIR={ovs_dbdir}
export OVS_SYSCONFDIR={ovs_syscfgdir}
export OVS_CONFDIR={ovs_cfgdir}
export OVS_RUNDIR={ovs_rundir}

cd {na_testdir}
if ! test -f conf.db; then
    touch .conf.db.~lock~
    {ovs_bindir}/ovsdb-tool create conf.db {ovs_pkgdatadir}/vswitch.ovsschema
fi
{ovs_sbindir}/ovsdb-server --detach --no-chdir --pidfile --log-file --remote=punix:$OVS_RUNDIR/db.sock

{ovs_bindir}/ovs-vsctl --no-wait init
{ovs_sbindir}/ovs-vswitchd --enable-dummy=override --disable-system --detach --no-chdir --pidfile --log-file -vvconn -vofproto_dpif -vunixctl
'''.format(**{'ovs_bindir': ovs_bindir, 'ovs_sbindir': ovs_sbindir, 'na_testdir': self._na_testdir,
              'ovs_cfgdir': ovs_cfgdir, 'ovs_rundir': ovs_rundir, 'ovs_dbdir': ovs_dbdir,
              'ovs_syscfgdir': ovs_syscfgdir, 'ovs_pkgdatadir': ovs_pkgdatadir}), True)
        
        self._ovs_started = True
        return True

    def cleanup(self):
        if not self._ovs_started:
            return

        ovs_bindir = os.getenv("OVS_BINDIR", "/usr/bin")
        ovs_sbindir = os.getenv("OVS_SBINDIR", "/usr/sbin")
        ovs_logdir = os.getenv("OVS_SBINDIR", "/var/log/openvswitch")
        ovs_dbdir = os.getenv("OVS_DBDIR", "/etc/openvswitch")
        ovs_syscfgdir = os.getenv("OVS_SYSCONFDIR", "/etc/openvswitch")  #
        ovs_cfgdir = os.getenv("OVS_CONFDIR", "/etc/openvswitch")  #
        ovs_rundir = os.getenv("OVS_RUNDIR", "/run/openvswitch")

        utility.Utility.cmd_execute('''
kill `cat {ovs_rundir}/ovsdb-server.pid`
kill `cat {ovs_rundir}/ovs-vswitchd.pid`
'''.format(**{'ovs_bindir': ovs_bindir, 'ovs_sbindir': ovs_sbindir, 'na_testdir': self._na_testdir,
              'ovs_cfgdir': ovs_cfgdir, 'ovs_rundir': ovs_rundir}), True)

    def _check_ovsbin(self):
        ovs_bindir = os.getenv("OVS_BINDIR", "/usr/bin")
        ovs_sbindir = os.getenv("OVS_SBINDIR", "/usr/sbin")
        return os.path.exists(ovs_bindir + '/ovs-vsctl') and os.path.exists(ovs_sbindir + '/ovs-vswitchd') \
               and os.path.exists(ovs_sbindir + '/ovsdb-server') and os.path.exists(ovs_bindir + '/ovs-ofctl')
