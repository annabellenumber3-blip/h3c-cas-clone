#!/usr/bin/python3

import unittest
from net_agent.base import id_manager


class TestIdManager(unittest.TestCase):

    def test_alloc_free(self):
        id_man = id_manager.IdManager()

        for i in range(1, 51):
            self.assertTrue(id_man.alloc_id('gid', str(i)) == i)

        for i in range(11, 16):
            id_man.free_id('gid', str(i))

        for i in range(21, 26):
            id_man.free_id('gid', str(i))

        for i in range(1, 6):
            id_man.free_id('gid', str(i))

        for i in range(1, 6):
            self.assertTrue(id_man.alloc_id('gid', str(i)) == i)

        for i in range(11, 16):
            self.assertTrue(id_man.alloc_id('gid', str(i)) == i)

        for i in range(21, 26):
            self.assertTrue(id_man.alloc_id('gid', str(i)) == i)

        for i in range(51, 101):
            self.assertTrue(id_man.alloc_id('gid', str(i)) == i)

        for i in range(1, 101):
            id_man.free_id('gid', str(i))

        # self.assertEqual(len(id_man._dict), 0, 'broken: all elements are removed, but dic is not empty')
        self.assertEqual(len(id_man._dict['gid']), 0, 'broken: all elements are removed, but dict[cat] is not empty')


if __name__ == '__main__':
    unittest.main()
