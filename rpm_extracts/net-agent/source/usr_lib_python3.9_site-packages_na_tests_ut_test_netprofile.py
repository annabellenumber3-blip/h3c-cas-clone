#!/usr/bin/python3

import unittest
import xml.etree.ElementTree as ET
from net_agent.netprofile import netprofile
from net_agent.netprofile import netprofile_qos
from na_tests.mock import mock

# __all__ = ['PortProfile', 'parse_netprofile_element', 'create_netprofile']
__netprofiles = {"qos": netprofile_qos.NetProfileQos}


def mock_parse_netprofile_element(xml):
    type_, name_, uuid, root = "", "", "", None
    try:
        root = ET.fromstring(xml)
        type_ = root.attrib.get("type", "")
        name_ = root.attrib.get("name", "")
        uuid = root.attrib.get("uuid", "")
    except:
        pass
    return type_, name_, uuid, root


def mock_create_netprofile(type_, md5, element):
    _net_profile_class = __netprofiles.get(type_, None)
    if _net_profile_class is None:
        return None

    netprofile = _net_profile_class(md5)
    return netprofile if netprofile.parse(element) else None


class TestNetProfile(unittest.TestCase):

    @mock.patch('logging.error')
    def test_parse_and_format_xml(self, _):
        netprofile_object = netprofile.NetProfile()

        xml = '<netprofile name="qos_test1" type="qos" uuid="245222" version="1"><flow direction="out" dl_type="ip" nw_src="172.20.23.0/24" priority="15"><action burst="10000" domain="interface" id="4" rate="30000" unit="kbps" /></flow><flow direction="in" dl_type="ip" nw_dst="172.20.23.0/24" priority="15"><action burst="10000" domain="interface" id="4" rate="10000" unit="kbps" /></flow></netprofile>'
        root = ET.fromstring(xml)

        netprofile_object.parse(root)
        self.assertEqual(netprofile_object.format_xml(), '<netprofile name="qos_test1" type="qos" uuid="245222" version="1" />')

    def test_netprofile_init(self):
        xml = '<netprofile name="qos_test1" type="qos" uuid="245222" version="1"><flow direction="out" dl_type="ip" nw_src="172.20.23.0/24" priority="15"><action burst="10000" domain="interface" id="4" rate="30000" unit="kbps" /></flow><flow direction="in" dl_type="ip" nw_dst="172.20.23.0/24" priority="15"><action burst="10000" domain="interface" id="4" rate="10000" unit="kbps" /></flow></netprofile>'

        type, name, uuid, root = mock_parse_netprofile_element(xml)
        self.assertEqual(type, 'qos')
        self.assertEqual(name, 'qos_test1')
        self.assertEqual(uuid, '245222')

        netprofile_object = mock_create_netprofile(type, " ", root)
        self.assertEqual(str(netprofile_object), 'type=qos,name=qos_test1,uuid=245222,version=1')


if __name__ == '__main__':
    unittest.main()