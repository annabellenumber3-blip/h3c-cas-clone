class TestDataManager(unittest.TestCase):

    # test _bridges and _delete_bridges, _bridge={name:bridge}, example: {vswitch0: vswitch0: mode=0,exist=True}
    # name --> br_name, bridge --> Bridge object
    def test_birdges_data(self):
        adm = data_manager.AgentDataManager()
        br_name = 'vswitch0'
        bridges = []
        bridges.append(Mock_Bridge(name=br_name, br_mode=0, exist=True))
        test_dict = {'vswitch0':bridges[-1]}
        adm.add_active_bridge(bridges[-1])
        adm.get_bridge_by_name(br_name)

        self.assertEqual(adm.get_bridge_by_name(br_name), test_dict[br_name])
        self.assertEqual(adm.get_all_bridges(), test_dict.values())

        bridges.append(Mock_Bridge(name='br0', br_mode=0, exist=True))
        adm.reset_active_bridge(bridges)
        self.assertEqual(adm.get_all_bridges(), bridges)
        if adm.has_bridge(bridges[-1].name):
            adm.mark_deleted_bridge(bridges[-1].name)
            bridges.pop(-1)
        self.assertEqual(adm.get_all_bridges(), bridges)

    # test _interfaces, _interface={name:interface}, example: {eth0: eth0: br=vswitch0,ofport=1,other_cfg={},ex_id={},uuid=70a9e2bf-0473-42ee-8813-15bfac0de3d2}
    # name --> interface.name, interface --> Interface object
    def test_interfaces_data(self):
        adm = data_manager.AgentDataManager()
        interfaces = []

        interfaces.append(ovs_object.Interface(uuid='70a9e2bf-0473-42ee-8813-15bfac0de3e1', name='vnet0', ofport=5, external_ids={},other_config={}, br_name='vswitch0'))
        interfaces.append(ovs_object.Interface(uuid='70a9e2bf-0473-42ee-8813-15bfac0de3d2', name='eth0', ofport=4, external_ids={},other_config={}, br_name='vswitch0'))
        adm.update_interface(interfaces[0])
        adm.update_interface(interfaces[-1])
        self.assertEqual(adm.get_interface_by_name(interfaces[-1].name), interfaces[-1])
        adm_ifaces = adm.get_all_interfaces()
        adm_ifaces.sort()
        interfaces.sort()
        self.assertEqual(adm_ifaces, interfaces)

        adm.delete_interface(interfaces[-1].name)
        interfaces.pop(-1)
        self.assertEqual(adm.get_all_interfaces(), interfaces)

    # test _nwfilters, _nwfilters={name:nwfilter}, example:  test_acl : name=test_acl   rule: action=accept, dir=in, priority=10, statematch=False, match: dl_type=0x0800,nw_src=3.2.0.0/16,
    # name  --> nwfilter.name, nwfilter --> nwfilter object
    def test_nwfilters_data(self):
        adm = data_manager.AgentDataManager()

        nwfilters = []
        aclrule1 = ovs_object.ACLRule()
        aclrule1.action, aclrule1.direction, aclrule1.priority, aclrule1.statematch = 'normal', 'out', 11, False
        aclrule2 = ovs_object.ACLRule()
        aclrule2.action, aclrule1.direction, aclrule1.priority, aclrule1.statematch = 'normal', 'in', 12, False
        nwfilter1 = ovs_object.ACL()
        nwfilter1.name, nwfilter1.aclrules = 'test_acl1', [aclrule1]
        nwfilter2 = ovs_object.ACL()
        nwfilter2.name, nwfilter1.aclrules = 'test_acl2', aclrule2
        nwfilters.append(nwfilter1)
        nwfilters.append(nwfilter2)

        adm.update_nwfilter(nwfilter1)
        adm.update_nwfilter(nwfilter2)

        self.assertEqual(adm.get_nwfilter_by_name(nwfilter1.name).name, 'test_acl1')
        self.assertEqual(adm.get_nwfilter_by_name(nwfilter2.name).name, 'test_acl2')

        admnwfilters = adm.get_all_nwfilters()
        admnwfilters.sort()
        nwfilters.sort()

        i = 0
        for item in nwfilters:
            self.assertEqual(item.name, admnwfilters[i].name)
            i = i + 1

        adm.delete_nwfilter(nwfilters[-1].name)
        nwfilters.pop(-1)
        admnwfilters = adm.get_all_nwfilters()
        i = 0
        for item in nwfilters:
            self.assertEqual(item.name, admnwfilters[i].name)
            i = i + 1

    # test _portprofiles,  _portprofiles={name:portprofile}, example: b6d45eb8-531d-49f9-b06a-c152a32d96bb_0: name=b6d45eb8-531d-49f9-b06a-c152a32d96bb_0 netprofile: type=qos, name=Qos_zhm
