#!/usr/bin/python3
import logging
from .. ovs_model.ovs_object import OVSAction, Flow, OVSACTION_DROP, ACTION_DROP, DIRECTION_IN, DIRECTION_INOUT
from . ovs_flow_manager_base import OVSFlowBaseManager, BR_MODE_VEB
from .. base import net_lib

COOKIE_BASIC = 0x0010

COOKIE_BIND_IP = 0x0020
COOKIE_IN_ACL = 0x0030

TABLE_ID_BIND_IP = 0
TABLE_ID_IN_ACL = 10

PRIORITY_OFFSET_BASIC = 1
PRIORITY_OFFSET_BIND_IP = 20000
PRIORITY_OFFSET_IN_ACL = 30000


class OVSFlowManagerOpen(OVSFlowBaseManager):

    def __init__(self):
        super(OVSFlowManagerOpen, self).__init__()
        self._defalut_actions = {
            TABLE_ID_BIND_IP: OVSAction().add_resubmit(None, TABLE_ID_IN_ACL),
            TABLE_ID_IN_ACL: OVSAction().add_normal(),
        }

    @staticmethod
    def get_type_version():
        return "open", "0.1"

    def __is_support_bridge(self, bridge):
        return bridge and bridge.br_mode == BR_MODE_VEB

    def __get_default_action(self, table_id):
        return self._defalut_actions.get(table_id, OVSACTION_DROP)

    def install_base_flows(self, bridge):
        logging.debug("Enter install_base_flows bridge: %s", bridge.name)
        if not self.__is_support_bridge(bridge):
            return
        logging.debug("Inner install_base_flows bridge: %s", bridge.name)

        def get_default_flow(table_id):
            return Flow(
                bridge=bridge.name,
                table=table_id,
                cookie=COOKIE_BASIC,
                priority=PRIORITY_OFFSET_BASIC,
                actions=self.__get_default_action(table_id))

        # install flow in a reverse order
        flows = [
            get_default_flow(TABLE_ID_IN_ACL),
            get_default_flow(TABLE_ID_BIND_IP)
        ]
        self._txn.add_flows(flows)

    def install_raw_flows(self, bridge):
        pass

    def uninstall_base_flows(self, bridge):
        if not self.__is_support_bridge(bridge):
            return
        flow = Flow(bridge=bridge.name, cookie="%d/-1" % COOKIE_BASIC)
        self._txn.del_flows(flow)

    def clear_iface(self, bridge, iface):
        if not self.__is_support_bridge(bridge):
            return
        return self._clear_iface(bridge, iface)

    def apply_tcp_syn_forbid(self, bridge, iface):
        return

    def del_tcp_syn_forbid(self, bridge, iface):
        return

    def apply_bind_ip(self, bridge, iface, ipv4, ipv6):
        if not self.__is_support_bridge(bridge):
            return
        self._apply_bind_ip(COOKIE_BIND_IP, TABLE_ID_BIND_IP, PRIORITY_OFFSET_BIND_IP, bridge, iface, ipv4, ipv6)

    def del_bind_ip(self, bridge, iface):
        if not self.__is_support_bridge(bridge):
            return
        self._del_bind_ip(COOKIE_BIND_IP, bridge, iface)

    def apply_acl(self, bridge, iface, aclrules):
        if not self.__is_support_bridge(bridge):
            return

        allflows = []
        for aclrule in aclrules:
            allflows.extend(
                self.__instance_one_aclrule(bridge, iface, aclrule))
        self._txn.add_flows(allflows)

    def del_acl(self, bridge, iface):
        if not self.__is_support_bridge(bridge):
            return

        flow_in = Flow(
            bridge=bridge.name,
            cookie="%d/-1" % COOKIE_IN_ACL,
            table=TABLE_ID_IN_ACL,
            in_port=iface.ofport)

        self._txn.del_flows([flow_in])

    def __instance_one_aclrule(self, bridge, iface, aclrule):
        def construct_in_acl_flow(mf):
            actions = OVSACTION_DROP if aclrule.action == ACTION_DROP else self.__get_default_action(TABLE_ID_IN_ACL)
            mf.in_port = iface.ofport
            flow = Flow(
                bridge=bridge.name,
                cookie=COOKIE_IN_ACL,
                table=TABLE_ID_IN_ACL,
                priority=PRIORITY_OFFSET_IN_ACL + aclrule.priority,
                matchfield=mf,
                actions=actions)
            return flow

        flows = []
        if aclrule.direction == DIRECTION_IN or aclrule.direction == DIRECTION_INOUT:
            mf = aclrule.matchfield.clone()
            flows.append(construct_in_acl_flow(mf))
        return flows

    def clear_all(self, bridge):
        if not self.__is_support_bridge(bridge):
            return
        self._txn.del_flows(Flow(bridge=bridge.name))
        self._txn.add_flows(Flow(bridge=bridge.name, table=0, priority=0, actions=OVSAction().add_normal()))

    def apply_netprofiles(self, bridge, iface, netprofiles, dm):
        return

    def unapply_netprofiles(self, bridge, iface, netprofiles, dm):
        return

    def clear_bridge_meters(self, br):
        return

    def clear_iface_meters(self, iface):
        return

    def clear_iface_drain(self, iface, dm):
        return
    
    def apply_used_by_mirror(self, bridge, iface):
        return

    def del_used_by_mirror(self, bridge, iface):
        return

    def dump(self):
        return "\n"
