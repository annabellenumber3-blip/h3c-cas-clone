#!/usr/bin/python3

import unittest
from net_agent.base import message_queue

class TestMessageQueue(unittest.TestCase):

    # test MessageQueue class
    def test_messagequeue_class(self):
        messagequeue = message_queue.MessageQueue(1000)
        messagequeue.open(1000)
        messagequeue.message_enqueue("AAA")
        messagequeue.message_dequeue()
        self.assertEqual(messagequeue.queue.get(), 'AAA')


if __name__ == '__main__':
    unittest.main()