#!/usr/bin/python3
import logging
from . netprofile import NetProfile
from .. base import net_lib


class DrainAction(object):

    # define DrainAction used as action of flow which describe drain parameters
    def __init__(self):
        super(DrainAction, self).__init__()
        self.bridge = ""
        self.vlan = 0

    def __str__(self):
        if self.vlan == 0:
            return "bridge=%s" % (self.bridge)
        else:
            return "bridge=%s vlan=%d" % (self.bridge, self.vlan)


class NetProfileDrain(NetProfile):

    def __init__(self, md5=""):
        super(NetProfileDrain, self).__init__(md5)

    def parse_extra_action(self, item):
        if [k for k in ["bridge"] if k not in item.attrib.keys()]:
            net_lib.Utility.set_log_lasterr("drain action incomplete: %s, missing bridge or vlan" % item.attrib)
            return None

        act = DrainAction()
        act.bridge = item.attrib.get("bridge", "")
        try:
            if "vlan" in item.attrib.keys():
                act.vlan = int(item.attrib.get("vlan", ""))
                if act.vlan >= 4096 or act.vlan <= 1:
                    raise ValueError("vlan is not in [1,4096)")
        except:
            net_lib.Utility.set_log_lasterr("invalid drain vlan, check your xml")
            return None

        return act

    # return drain action content as xml format
    def format_extra_action(self, action, element):
        element.attrib["bridge"] = str(action.bridge)
        if action.vlan != 0:
            element.attrib["vlan"] = str(action.vlan)

