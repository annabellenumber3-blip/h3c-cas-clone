#!/usr/bin/python3
"""
Test info config synchronization
"""
import unittest
from na_tests.common.utility import Utility
from na_tests.common import testbase


class TestInfoConfigSync(testbase.TestCaseBase):

    def myinit(self):
        super(TestInfoConfigSync, self).myinit()
        self._start_ovs = True
        self._start_netagent = True

    def test_add_bridge(self):
        ret = Utility.cmd_execute('''
cd {test_dir}
ovs-vsctl add-br br0 -- set bridge br0 fail-mode=standalone
ovs-ofctl mod-port br0 br0 noforward
na-ctl add-listen --id=1 --event="set Interface br0 other_config:fake=fake"
ovs-vsctl set Interface br0 other_config:fake=fake
na-ctl wait --id=1
na-ctl dump > add_br_dump.txt
grep 'interface: br0: br=br0,ofport=65534,tag=0,other_cfg' add_br_dump.txt
if test $? -ne 0; then
    exit 2
fi
'''.format(**{'test_dir': self._test_dir}), True)
        self.assertEqual(ret[0], 0, "test add bridge failed: shell return code:%d" % ret[0])

    def test_del_bridge(self):
        ret = Utility.cmd_execute('''
cd {test_dir}
ovs-vsctl add-br br0 -- set bridge br0 fail-mode=standalone -- \
    add-port br0 p1 -- set interface p1 ofport_request=1 type=dummy 

ovs-ofctl mod-port br0 br0 noforward
ovs-vsctl del-br br0
na-ctl dump > del_port_dump.txt  # TODO: bad
grep 'interface: p1: br=br0,ofport=1,tag=0,other_cfg=' del_port_dump.txt
ret=$?
# can't grepped wanted text here
if test $ret -eq 0; then 
    exit 2
fi
'''.format(**{'test_dir': self._test_dir}), True)
        self.assertEqual(ret[0], 0, "test del bridge failed: shell return code:%d" % ret[0])

