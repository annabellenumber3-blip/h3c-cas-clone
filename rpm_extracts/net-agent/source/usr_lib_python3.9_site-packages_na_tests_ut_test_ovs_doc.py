#!/usr/bin/python3

# import mock  # require mock package
import unittest
from net_agent.ovs_model import ovs_doc


class TestOvsDoc(unittest.TestCase):

    # test OVSDbEvent class
    def test_ovsdbevent_class(self):
        ovsdb_event = ovs_doc.OVSDbEvent("normal", "124321", "vnet1", 5, 100, {'fake-bind-ip': '172.20.23.5'}, None)
        self.assertEqual(str(ovsdb_event).strip(), "normal: uuid=124321,name=vnet1,ofport=5,tag=100,ex_id={'fake-bind-ip': '172.20.23.5'},other_cfg:None")
        ovsdb_event = ovs_doc.OVSDbEvent("drop", "125321", "vnet2", 6, 120, {'port-profile':'test1'}, None)
        self.assertEqual(str(ovsdb_event).strip(), "drop: uuid=125321,name=vnet2,ofport=6,tag=120,ex_id={'port-profile': 'test1'},other_cfg:None")


if __name__ == '__main__':
    unittest.main()
