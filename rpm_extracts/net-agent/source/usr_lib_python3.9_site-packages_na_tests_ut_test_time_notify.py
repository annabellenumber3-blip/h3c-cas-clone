#!/usr/bin/python3

import unittest
from net_agent.base import timer_notify
from na_tests.common import testbase


class TestTimeNotify(testbase.TestCaseBase):

    def setUp(self):
        super(TestTimeNotify, self).setUp()
        self.output = []

    def _process_timer_message(self, timer_id):
        self.output.append(timer_id)

    def test_add_clear_timer(self):
        timer_note = timer_notify.TimerNotify()
        timer_to_add1 = (1, 3600, 55)
        timer_to_add2 = (2, 2, 1.5, 1)
        timer_to_add3 = (2, 3, 1.5, 1)

        timer_note.add_timer(*timer_to_add1)
        self.assertIn("[timer 1: interval=3600, idle=55, count=-1, when=", str(timer_note._timers[-1]))
        timer_note.add_timer(*timer_to_add2)
        self.assertIn("[timer 2: interval=2, idle=1, count=1, when=", str(timer_note._timers[-1]))
        timer_note.add_timer(*timer_to_add3)
        self.assertNotIn("[timer 2: interval=3, idle=1, count=1, when=", str(timer_note._timers[-1]))
        timer_note.clear()
        self.assertEqual(len(timer_note._timers), 0)

    def test_run(self):
        timer_note = timer_notify.TimerNotify()
        timer_to_add1 = (1, 0.015, 0.011, 1)
        timer_to_add2 = (2, 0.005, 0, 1)
        timer_to_add3 = (3, 0.015, 0, 1)

        timer_note.add_timer(*timer_to_add1)
        timer_note.add_timer(*timer_to_add2)
        timer_note.add_timer(*timer_to_add3)

        while True:
            timer_length_before = len(timer_note._timers)
            timer_note.run(self._process_timer_message)
            timer_length_after = len(timer_note._timers)
            if timer_length_after == 0:
                break
            if timer_length_after < timer_length_before:
                timer_note.set_last_active()

        self.assertListEqual(self.output, [2, 3, 1])
        timer_note.clear()


if __name__ == '__main__':
    unittest.main()



