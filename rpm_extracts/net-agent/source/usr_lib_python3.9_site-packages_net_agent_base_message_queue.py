#!/usr/bin/python3
import os
try:
    import Queue
except ImportError:
    import queue as Queue
import fcntl


class MessageQueue(object):

    def __init__(self, size=1000):
        super(MessageQueue, self).__init__()
        self.queue = None
        self._monitor_thread_pipe = None
        self.open(size)

    def get_wackup_fd(self):
        return self._monitor_thread_pipe[0]

    def open(self, size):
        self.queue = Queue.Queue(size)
        self._monitor_thread_pipe = os.pipe()
        flag = fcntl.fcntl(self.get_wackup_fd(), fcntl.F_GETFL)
        fcntl.fcntl(self.get_wackup_fd(), fcntl.F_SETFL, flag | os.O_NONBLOCK)

    def close(self):
        if self._monitor_thread_pipe:
            os.close(self._monitor_thread_pipe[0])
            os.close(self._monitor_thread_pipe[1])
        self._monitor_thread_pipe = None
        self.queue = None

    def message_enqueue(self, messages):
        if type(messages) is not list:
            messages = [messages]
        for message in messages:
            self.queue.put(message)
        self.notify_wackup()

    def message_dequeue(self):
        while 1:
            try:
                item = self.queue.get_nowait()
                yield item
            except Queue.Empty:
                break

    def notify_wackup(self):
        os.write(self._monitor_thread_pipe[1], b"1")

    def flush_wackup(self):
        while 1:
            try:
                os.read(self.get_wackup_fd(), 1000)
            except OSError:
                break
