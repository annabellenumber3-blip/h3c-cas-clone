#!/usr/bin/python3
import logging
from . netprofile import NetProfile
from .. base import net_lib

METER_DOMAIN_BRIDGE = "bridge"
METER_DOMAIN_IFACE = "interface"


class Meter(object):

    # Define Meter used as action of flow which describe qos parameter.
    def __init__(self):
        super(Meter, self).__init__()
        self.id = [METER_DOMAIN_IFACE, 0]
        self.unit = "kbps"
        self.rate = 0
        self.burst = 0

    def __str__(self):
        return "domain=%s,id=%s,unit=%s,rate=%s,burst=%s" % (
            self.id[0], self.id[1], self.unit, self.rate, self.burst)


class NetProfileQos(NetProfile):

    def __init__(self, md5=""):
        super(NetProfileQos, self).__init__(md5)

    def parse_extra_action(self, item):
        if [k for k in ["domain", "id", "rate"] if k not in item.attrib.keys()]:
            net_lib.Utility.set_log_lasterr("qos action is incomplete: %s" % item.attrib)
            return None

        domain = item.attrib.get("domain", METER_DOMAIN_IFACE)
        rate = item.attrib.get("rate", "")
        burst = item.attrib.get("burst", "")
        meter = Meter()
        meter.id[0] = domain if domain == METER_DOMAIN_BRIDGE or domain == METER_DOMAIN_IFACE else METER_DOMAIN_IFACE
        meter.id[1] = item.attrib.get("id", "")
        meter.unit = item.attrib.get("unit", "kbps")
        meter.rate = rate if net_lib.ValidatyCheck.check_int(rate, 1, 0xffffffff) else ""
        # set burst same as rate if no burst defined in xml
        if burst == "":
            meter.burst = meter.rate
        else:
            meter.burst = burst if net_lib.ValidatyCheck.check_int(burst, 1, 0xffffffff) else ""
        if meter.id[1] == "" or meter.rate == "" or meter.burst == "":
            net_lib.Utility.set_log_lasterr("invalid meter id or rate/burst: %s/%s" % (rate, burst))
            return None

        return meter

    # return meter action content as xml format
    def format_extra_action(self, action, element):
        meter = action
        element.attrib["domain"] = str(meter.id[0])
        element.attrib["id"] = str(meter.id[1])
        element.attrib["unit"] = str(meter.unit)
        element.attrib["rate"] = str(meter.rate)
        element.attrib["burst"] = str(meter.burst)
