#!/usr/bin/python3
import logging
from . flow_business_base import FlowBusinessBase


class FlowBusinessTCPSynForbid(FlowBusinessBase):

    def __init__(self):
        super(FlowBusinessTCPSynForbid, self).__init__()

    def teardown_flows(self, flow_manager, iface, bridge):
        flow_manager.del_tcp_syn_forbid(bridge, iface)
        logging.debug("teardown tcp-syn-forbidden flow for: %s.", iface)

    def setup_flows(self, flow_manager, iface, bridge):
        tcp_forbid = iface.tcp_syn_forbid
        if tcp_forbid == 'true':
            flow_manager.apply_tcp_syn_forbid(bridge, iface)
            logging.debug("setup tcp-syn-forbidden flow for %s.", iface)

    def handle_interface_add_begin(self, flow_manager, iface, bridge, event):
        is_change = iface is None or iface.tcp_syn_forbid != event.tcp_syn_forbid
        if is_change:
            self.teardown_flows(flow_manager, iface, bridge)

        return is_change

    def handle_interface_add_end(self, flow_manager, iface, bridge, is_change):
        if is_change and iface.tcp_syn_forbid:
            self.setup_flows(flow_manager, iface, bridge)

    def handle_interface_change_begin(self, flow_manager, iface, bridge, old_event, new_event):
        is_change = old_event.other_config is not None and old_event.tcp_syn_forbid != new_event.tcp_syn_forbid
        if is_change:
            self.teardown_flows(flow_manager, iface, bridge)
        return is_change

    def handle_interface_change_end(self, flow_manager, iface, bridge, is_change):
        if is_change and iface.tcp_syn_forbid:
            self.setup_flows(flow_manager, iface, bridge)
