#!/usr/bin/python3
import logging
import xml.etree.ElementTree as ET
import uuid
from .. ovs_model.ovs_object import FlowMatchField, DIRECTION_IN, ACTION_DROP, OVSACTION_DROP
from .. base import net_lib


class NetProfileReference(object):
    """
    NetProfileReference class,  define netprofile refered by portprofile, stor type and name as key for real netprofile.
    """
    def __init__(self):
        super(NetProfileReference, self).__init__()
        self.name = ""
        self.type = ""

    def __str__(self):
        return "type=%s, name=%s " % (self.type, self.name)


class Rule(object):
    """
    Base Class Rule, can be inherited by other netprofile rule.
    """
    def __init__(self):
        super(Rule, self).__init__()
        self.action = OVSACTION_DROP
        self.direction = DIRECTION_IN
        self.priority = 0
        self.statematch = None  # added for acl/vfw rule
        self.matchfield = FlowMatchField()

    def __str__(self):
        statematch = ", stateful=%s" % str(self.statematch).lower() if self.statematch != None else ""
        return "dir=%s, priority=%d%s match:%s action:%s" \
               % (self.direction, self.priority, statematch, self.matchfield, self.action)


class NetProfile(object):
    """
    Base Class NetProfile, provide general form of netprofile rule set.
    """
    def __init__(self, md5=""):
        super(NetProfile, self).__init__()
        self.type = ""
        self.name = ""
        self.uuid = ""
        self.version = ""
        self.md5 = md5
        self.rules = []

    # parse action can be overloaded by subclass
    def parse_extra_action(self, element):
        return None

    def format_extra_action(self, action, element):
        return

    def parse(self, root):
        if root is None:
            logging.error("root is None")
            return False

        self.type = root.attrib.get("type", "")
        self.name = root.attrib.get("name", "")
        self.uuid = root.attrib.get("uuid", str(uuid.uuid3(uuid.NAMESPACE_DNS, self.name)))
        self.version = root.attrib.get("version", "")

        if self.name == "":
            net_lib.Utility.set_log_lasterr("name is None")
            return False

        for child in root:
            if child.tag != "flow":
                continue

            ipaddr_type = ""
            ipaddr_list = []
            if None != child.attrib.get("nw_src", None):
                ipaddr_type = "nw_src"
            elif None != child.attrib.get("nw_dst", None):
                ipaddr_type = "nw_dst"
            elif None != child.attrib.get("ipv6_src", None):
                ipaddr_type = "ipv6_src"
            elif None != child.attrib.get("ipv6_dst", None):
                ipaddr_type = "ipv6_dst"

            if ipaddr_type == "":
                ipaddr_num = 0
            else:
                ipaddr_list = child.attrib[ipaddr_type].split(";")
                ipaddr_num = len(ipaddr_list)

            while True:
                rule = Rule()
                if len(ipaddr_list) != 0:
                    child.attrib[ipaddr_type] = ipaddr_list[ipaddr_num-1]
                if [k for k in ["direction", "priority"] if k not in child.attrib.keys()]:
                    net_lib.Utility.set_log_lasterr("incomplete flow: %s" % child.attrib)
                    return False

                rule.direction = child.attrib.get("direction", DIRECTION_IN)
                priority = child.attrib.get("priority", "0")
                if None != child.attrib.get("stateful", None):
                    rule.statematch = "false" != child.attrib.get("stateful", None)
                rule.priority = int(priority) if priority.isdigit() else 0

                self._parse_flow_from_xml(child.attrib, rule.matchfield)
                # netprofile acl has L2 support, which may not present dl_type; however must have one those fields at least
                if rule.matchfield.dl_type is None and rule.matchfield.dl_src is None and rule.matchfield.dl_dst is None:
                    net_lib.Utility.set_log_lasterr("dl_type, dl_src, dl_dst in flow are none")
                    return False

                for item in child:
                    if item.tag == "action":
                        rule.action = self.parse_extra_action(item)
                        if rule.action is None:
                            net_lib.Utility.set_log_lasterr("failed to pares action: %s" % item)
                            return False
                if not net_lib.ValidatyCheck.check_flow(rule):
                    net_lib.Utility.set_log_lasterr("invalid flow %s" % rule)
                    return False

                self.rules.append(rule)
                if ipaddr_num == 0 or ipaddr_num == 1:
                    break
                else:
                   ipaddr_num -= 1
        self.rules.sort(key=lambda r: -r.priority)

        return True

    # return netprofile content as xml format
    def format_xml(self):
        xml = "\n"
        try:
            root = ET.Element("netprofile")
            root.attrib["type"] = self.type
            root.attrib["name"] = self.name
            root.attrib["uuid"] = self.uuid
            root.attrib["version"] = self.version
            for rule in self.rules:
                flow = ET.SubElement(root, "flow")
                flow.attrib["direction"] = str(rule.direction)
                flow.attrib["priority"] = str(rule.priority)
                if rule.statematch != None:
                    flow.attrib["stateful"] = "true" if rule.statematch else "false"
                rule.matchfield.format_xml(flow)

                action = ET.SubElement(flow, "action")
                self.format_extra_action(rule.action, action)
            xml = ET.tostring(root).decode().replace("><", ">\n<")
        except:
            logging.exception("catch exception")

        return xml

    @staticmethod
    def _parse_flow_from_xml(flow, matchfield):

        def string_to_field_and_mask(field_name):
            v = flow.get(field_name, None)
            v = v.split("/", 1) if v else None
            field = v[0] if v else None
            field_mask = v[1] if v and len(v) == 2 else None
            return field, field_mask

        # Layer 2
        matchfield.dl_src, matchfield.dl_src_mask = string_to_field_and_mask("dl_src")
        matchfield.dl_dst, matchfield.dl_dst_mask = string_to_field_and_mask("dl_dst")

        # Layer 3
        matchfield.dl_type = flow.get('dl_type', None)
        matchfield.nw_proto = flow.get('nw_proto', None)

        # We only accept dl_type as name and its hex string form, other is unacceptable
        if matchfield.dl_type == "ip" or matchfield.dl_type == "0x0800":
            matchfield.nw_src, matchfield.nw_src_mask = string_to_field_and_mask("nw_src")
            matchfield.nw_dst, matchfield.nw_dst_mask = string_to_field_and_mask("nw_dst")
        elif matchfield.dl_type == "ipv6" or matchfield.dl_type == "0x86dd":
            matchfield.ipv6_src, matchfield.ipv6_src_mask = string_to_field_and_mask("ipv6_src")
            matchfield.ipv6_dst, matchfield.ipv6_dst_mask = string_to_field_and_mask("ipv6_dst")
        elif matchfield.dl_type == "broadcast":
            matchfield.dl_dst, matchfield.dl_dst_mask = ("01:00:00:00:00:00","01:00:00:00:00:00")
            matchfield.dl_type = None

        # In the context of icmpv6, icmpv6_type, icmp_type are same, similarly icmpv6_code, icmp_code are same
        if matchfield.nw_proto == "icmp" or matchfield.nw_proto == "1" or matchfield.nw_proto == "icmp6" or matchfield.nw_proto == "58":
            matchfield.icmp_type, matchfield.icmp_type_mask = string_to_field_and_mask("icmp_type")
            matchfield.icmp_code, matchfield.icmp_code_mask = string_to_field_and_mask("icmp_code")

        # Layer 4
        # :) blame me to borrow tp_xxx_mask as xxxportend. in xml, tp_src=A/B, B is actually tp_src_end not mask; i.e., in this
        #  stage, tp_src_mask is xxxportend. This also applies to tp_dst
        if matchfield.nw_proto:  # udp, tcp etc with no strict judgement here
            matchfield.tp_src, matchfield.tp_src_mask = string_to_field_and_mask("tp_src")
            matchfield.tp_dst, matchfield.tp_dst_mask = string_to_field_and_mask("tp_dst")

    def __str__(self):
        return "type=%s,name=%s,uuid=%s,version=%s" % (self.type, self.name, self.uuid, self.version)
