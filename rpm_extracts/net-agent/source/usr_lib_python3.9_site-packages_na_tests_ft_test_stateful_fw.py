# this file implements test about VFW (ie., stateful firwallwall with each rule set statematch=true)


class TestStateFW(testbase.TestCaseBase):

    def myinit(self):
        super(TestStateFW, self).myinit()
        self._start_ovs = True
        self._start_netagent = True

    def test_fw_config(self):
        ret = Utility.cmd_execute('''
cd {test_dir}
ovs-vsctl add-br br0 -- set bridge br0 fail-mode=standalone -- \
    add-port br0 p1 -- set interface p1 ofport_request=1 type=dummy -- \
    add-port br0 p2 -- set interface p2 ofport_request=2 type=dummy

ovs-ofctl mod-port br0 br0 noforward
fw_by_add_config_fw_default() {{
    cat <<EOF
<filter name='fw_config' chain='mac_fw' priority='-800'>
  <uuid>15fdfa87-ce43-4402-9927-3f6f74524691</uuid>
  <rule action='drop' direction='in' priority='4' statematch='true'>
    <all/>
  </rule>
  <rule action='drop' direction='out' priority='4' statematch='true'>
    <all/>
  </rule>
  <rule action='drop' direction='in' priority='4' statematch='true'>
    <all-ipv6/>
  </rule>
  <rule action='drop' direction='out' priority='4' statematch='true'>
    <all-ipv6/>
  </rule>
  <rule action='accept' direction='out' priority='10' statematch='true'>
    <tcp srcipaddr='1.2.3.5' srcipmask='32'/>
  </rule>
</filter>
EOF
}}

fw_by_add_config_fw_default > {nwfilterdir}/{fwconfig}.xml
ovs-appctl revalidator/purge
na-ctl add-listen --id=1 --event="set Interface p2 other_config:nwfilter={fwconfig}"
ovs-vsctl set Interface p2 other_config:nwfilter={fwconfig}
na-ctl wait --id=1
na-ctl nwfilter update {fwconfig}
ovs-ofctl dump-flows br0 > br0_flows.txt
grep "ct_state=+new-est,tcp,reg1=0x.*/0xffff,nw_src=1.2.3.5" br0_flows.txt

if test $? -ne 0; then
    exit 2
fi

ovs-appctl revalidator/purge
na-ctl add-listen --id=2 --event="remove Interface p2 other_config:nwfilter"
ovs-vsctl remove Interface p2 other_config nwfilter
na-ctl wait --id=2
ovs-ofctl dump-flows br0 > br0_flows1.txt
grep "ct_state=+new-est,tcp,reg1=0x.*/0xffff,nw_src=1.2.3.5" br0_flows1.txt

if test $? -ne 1; then
    exit 3
fi
'''.format(**{'fwconfig': 'fw_config', 'nwfilterdir': self.nwfilter_dir, 'test_dir': self._test_dir}), True)
        self.assertEqual(ret[0], 0, "test fw config failed: shell return code:%d" % ret[0])


