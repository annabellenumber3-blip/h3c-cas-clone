#!/usr/bin/python3

import unittest
import logging
import os
from net_agent.ovs_model import file_monitor
from na_tests.common import testbase
from na_tests.common import utility
from na_tests.mock import mock


class TestFileMonitor(testbase.TestCaseBase):
    
    def setUp(self):
        super(TestFileMonitor, self).setUp()
        file_monitor.net_lib.OPENVSWITCH_RUN_DIR = os.getenv("OVS_RUNDIR", "/var/run/openvswitch")
        self._message = []
        self._mon_file = None
        logging.info = mock.Mock()
        logging.error = mock.Mock()

    def _file_monitor_event_handler(self, event):
        if event.name.endswith(".pid"):
            self._message = [event.maskname, event.name]

    def test_file_mon_start(self):
        self._mon_file = file_monitor.FileMonitor(self._file_monitor_event_handler)
        self._mon_file.start()

        self.assertFalse(utility.Utility.cmd_execute("touch %s/test.pid" % self._test_dir, shell=True)[0])
        # in ubuntu the following line is ok, not ok in centos. ubuntu may implements well (IN_MOVED_TO | pyinotify.IN_DELETE)
        # self.assertListEqual(self._message, [])  # comment this line for centos to run well

        self.assertFalse(utility.Utility.cmd_execute("mv %s/test.pid %s/test1.pid" % (self._test_dir, self._test_dir), shell=True)[0])
        self.assertEqual(self._message, ["IN_MOVED_TO", "test1.pid"])
        self.assertFalse(utility.Utility.cmd_execute("rm %s/test1.pid" % self._test_dir, shell=True)[0])
        self.assertEqual(self._message, ["IN_DELETE", "test1.pid"])

    def test_file_move_to(self):
        self._mon_file = file_monitor.FileMonitor(self._file_monitor_event_handler)
        self._mon_file.start()
        self._mon_file.stop()

        self.assertFalse(utility.Utility.cmd_execute("touch %s/test.pid" % self._test_dir, shell=True)[0])
        self.assertEqual(self._message, [])

        self.assertFalse(utility.Utility.cmd_execute("mv %s/test.pid %s/test1.pid" % (self._test_dir, self._test_dir), shell=True)[0])
        self.assertEqual(self._message, [])
        self.assertFalse(utility.Utility.cmd_execute("rm %s/test1.pid" % self._test_dir, shell=True)[0])
        self.assertEqual(self._message, [])

    def tearDown(self):
        self._mon_file.stop()
        logging.info.reset_mock()
        logging.error.reset_mock()
        super(TestFileMonitor, self).tearDown()


if __name__ == '__main__':
    unittest.main()




