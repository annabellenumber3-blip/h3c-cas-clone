#!/usr/bin/python3

import unittest
import logging
from na_tests.common import testbase
from net_agent.base import net_lib


def get_log_file(logdir):
    return logdir + "/cas_net_agentd.log"


def get_log_conf(logdir):
    return logdir + "/log.conf"


def mock_log_init(logdir):
    formatter = logging.Formatter(
        '%(asctime)s|%(levelname)s|%(funcName)s:%(lineno)d|%(message)s',
        '%m-%d %H:%M:%S')

    level = mock_get_log_level("net_agentd", logdir)
    logging.getLogger().handlers = []
    logging.basicConfig(level=level, filename=get_log_file(logdir))
    return


def mock_get_log_level(module, logdir):
    logdict = {'DEBUG': logging.DEBUG,
               'INFO': logging.INFO,
               'WARNING': logging.WARNING,
               'ERROR': logging.ERROR}
    level = logging.DEBUG

    # match string bond is ' ' or '='
    mstr = "Logging_Level_%s" % module
    contents = net_lib.Utility.read_file(get_log_conf(logdir)).strip()
    for line in contents.splitlines():
        if line.startswith(mstr):
            strlevel = line.split('=')[-1].strip()
            level = logdict.get(strlevel, level)
            break

    return level


class TestNetLog(testbase.TestCaseBase):

    def myinit(self):
        super(TestNetLog, self).myinit()

    def test_get_log_level(self):
        content = 'Logging_Level_net_agentd = INFO'
        net_lib.Utility.write_file(get_log_conf(self._test_dir), content)
        self.assertEqual(mock_get_log_level('net_agentd', self._test_dir), logging.INFO)

    def test_log_init(self):
        self.test_get_log_level()
        mock_log_init(self._test_dir)
        # net_lib.Utility.cmd_execute("ovs-vsctl add-br br0")
        # net_lib.Utility.cmd_execute("ovs-vsctl add-portsss br0 p1")
        # self.assertEqual(net_lib.Utility.cmd_execute("grep 'add-portsss' /var/log/caslog/cas_net_agentd.log ")[0], 0)
        logging.warn("testlogtext")  # FIXME: warn -> info can only runs ok in single test
        self.assertEqual(net_lib.Utility.cmd_execute("grep testlogtext %s" % get_log_file(self._test_dir))[0], 0)


if __name__ == '__main__':
    unittest.main()